<p>“有这等事？”</p>
<p>一身大红袍的赵玖微微一怔。“十统制仿效太祖结义？”</p>
<p>“官家！”下方为首的马皋在地上惶急难耐。“好教官家知道，俺们十兄弟结义只是寻常草莽结义，求得自家义气，乃是江湖上常见的事情，如何敢说效什么太祖爷爷结义？又如何说什么心存不轨呢？”</p>
<p>“官家，好教官家知道，此事诚不可赦！”万俟卨立在那里居高临下，看都不看马皋一眼，却只是抬手指着此人继续凛然以对。“十统制结义之后自成一体，从此之后，一人违逆，十人俱从，一荣俱荣，一损俱损，继而东京留守司数万兵马根本无可制约……此等人物聚大军于东京内外，岂不是将官家与朝廷安危握于手中，肆意操弄？”</p>
<p>“臣等绝无不轨之意，又哪里敢操纵官家？！”马皋越听越怕，却又看向了自己左前方的岳飞岳鹏举。“岳太尉，请救一救俺们东京留守司兄弟！你须知道俺们清白！”</p>
<p>岳飞张口欲言……他本是这些人熟人，有一份香火情，而且按照赵官家此番安排，东京留守司马上解散，应该是跟他的济州镇抚使司下合二为一，编制为御营前军，算是他的下属。</p>
