<p>说实话，很坑，对自己身体不好，说不定真就会猝死，而且非常伤害追更书友的感情。</p>
<p>但偏偏就是管不住自己这个散漫的性格。</p>
<p>大神约下来之前，我一度已经开始趁着疫情跟公司蹭裁员了，但说实话，且不说最后结果，真要是全职了，恐怕是会更散漫。</p>
<p>总而言之，先给大家认个错，这个更新和这些个请假，以及我的散漫是对不住大家的追更和大家给我的成绩的。</p>
<p>然后，回到内容上来。</p>
<p>这一卷是说什么呢？</p>
<p>很简单，如果说第一卷是主角这个人的立足，这一卷就是主角这个‘官家’的立足……两者是有区别的。</p>
