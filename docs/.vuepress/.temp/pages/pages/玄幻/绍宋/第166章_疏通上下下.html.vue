<p>可为什么呢？</p>
<p>岳鹏举扪心自问，而且很快就得出了结论，一来是他自己性格使然；二来是张所去世，宗泽又死，两位被他视为半父一般的长官忽然去世，给他留下了一个心理上的门槛，他一时迈过不去；三来，却是因为赵官家之前这几个月的表现给他留下了极好的印象，让他忘了这个穿着大红袍子，不说话时一点表情都无的年轻人，其实是个官家，是个天子。</p>
<p>而且是个姓赵的天子！</p>
<p>且不说下面的人如何胡思乱想，面无表情的赵玖听到那句‘太祖也是柴氏忠臣’后，差点没撑住，几乎要笑场……</p>
<p>须知道，王善和万俟卨都是他从东京带过来的，但他为防弄巧成拙，却只提点了王善，让这个在宗泽席前对自己效忠的军将出来冒头自爆，好扯出这个话题，再图借题发挥，却未尝叮嘱过万俟卨来做白脸，只是让小林学士必要时敲下变故，却不料这厮自己跳出来，还发挥到如此境地。</p>
<p>果然是个帝王就喜欢奸臣的吗？</p>
<p>而且，赵大真是柴氏忠臣吗？</p>
<p>五代十国的逻辑能往这年头套？</p>
<p>这大宋皇帝们得多没自信，才能在自己的国家繁荣了一两百年后还屡屡被这句话给弄出灵魂震颤来？</p>
<p>当然了，赵玖肯定是想不明白的，因为他虽姓赵，却不是这家赵氏的种，赵氏工科狗自有赵氏工科狗自己的可笑逻辑。</p>
<p>而终于，随着这位赵官家一路想到这里，却是真就笑出声来了……而闻此一笑，下方正板着脸的宰执、帅臣们，外加文武百官，还有十几位当事人不免心情复杂。</p>
<p>恐惧的更恐惧，不耐烦的更不耐烦，惊喜的更惊喜，而茫然的更茫然。</p>
<p>“朕以为，诸位说的都有道理。”</p>
<p>赵官家收起笑意，一开口就有点荒唐，这种事情便是都有道理，又如何能‘都有道理’？既然扯了出来，还能有个善了？</p>
<p>“朕是真心信得过马将军，情知十统制彼时结义皆无逆心，否则何以不计生死得失，往鄢陵助阵？也以为岳卿所言极是，鄢陵-长社一战，诸统制功在社稷，朕不得不牢记于心，尽量保全。但偏偏万俟卿所言，却也极有道理……自古以来，哪有军中将领擅自结义，脱离公序私成体系的？此事一出，若不处置，他们迟早会落不到一个好下场，而若要处置，以此罪责，或杀或剐，翻遍史书来，谁又能说什么呢？”赵官家满口废话，却让下方不知道多少人听得心惊肉跳。</p>
<p>毕竟，这话听起来，落到最后却还是一个‘或杀或剐’！</p>
<p>“不过，宗留守逝前曾有言语与朕，让朕务必妥善处置东京留守司诸军。”言至此处，赵玖不禁顺风幽幽一叹。“诸卿，你们或是朝堂宰执，或是国家名将，或是学富五车，或是经验老道，难道就没有人能起个两全其美之策吗？既能使此事风险消诸于无形，又能保全诸位统制官，不使功臣寒心吗？”</p>
<p>这好像又绕过来了，似乎还是要杯酒释兵权，而若是此论，大家就一个比一个熟了，且绝对能玩出花来。</p>
<p>果然，在稍微停滞了片刻之后，很快便有中书舍人范宗尹出列，其人行礼之后，正色相对：“官家，臣以为可以鄢陵殊勋，额外加十统制品级，然后使之三三两两为镇抚使，分往东南、京东、荆襄、、广南、巴蜀，以作靖安之任。”</p>
<p>平心而论，这是个好主意，加官进爵，然后脱离主力战斗序列，再将这十人打散分开，相互之间还要定个主次，同时还能提高地方上的治安力度，显然算是一个加强版、且有时代特色的杯酒释兵权了。</p>
<p>只能说，范宗尹这人在没有什么过硬资历、功劳，却年纪轻轻（今年刚刚三十）做到中书舍人，而且很受南阳上下推崇，甚至，张浚西行巴蜀之前一度想把此人推荐给赵官家做御史中丞……是有他一份道理的。</p>
<p>赵官家闻言也难得犹豫了一下，但还是缓缓摇头，并下了定论：“不妥，这是本末倒置！”</p>
<p>这话有点严重，范宗尹微微一滞，立即便朝几位宰执的方向看去。</p>
<p>而首相吕好问无奈，只能本着保护后进英才的想法起身出列：“官家，臣等实不知官家心思，如此处置便有不妥，又何论本末倒置？”</p>
<p>“此事简单。”赵玖见到宰执终于出面，却是干脆亮了底。“所谓今朝一胜，不过是挡住了金人攻势而已，区区鄢陵-长社奋力一击，也不过灭贼万人有余，天下大势依旧未曾动摇，而鞑虏一日未能尽除，则天下事则一日以抗金为本，而十统制如今一时自陷迷途，却非是存了什么歹心……”</p>
<p>说到此处，赵玖微微一笑，方才继续言道：“其实，莫说没有什么歹心，便是真有什么歹心，以眼下大局，朕都能忍，只要他能抗金便可！所以，今日处置，只该尽量保存十统制及其部战力，以图将来才对，焉能自毁长城？朕还指望着他们将来驱除鞑虏，替朕迎回二圣呢！”</p>
<p>前面说的还好，最后一句‘迎回二圣’，初来乍到刚刚接触这位官家的诸位臣子自然以为这是官家在宣扬什么孝悌之道，但接触日久，尤其是那几位已经跟这位官家熟悉了不少的文武却心知肚明……这话从这位官家口中说出来，却有几分虎狼之词的意味。</p>
<p>于是，吕好问等人即刻警醒，赵官家怕是早已经有腹案了。</p>
<p>一念至此，吕好问干脆拱手以对：“官家若有想法，何妨直言相告，臣等绝无异议。”</p>
<p>好嘛，赵官家言语未发，宰相便直接无异议了。下面几位颇有骨气的文臣面面相觑，却又无奈……这也就是一堆事还没结果，也就是眼下，若放在神宗、这种朝，这种宰相怕是三日都坐不稳。</p>
<p>然而，话还得说回来，从官家‘落井’后驱除黄潜善、诛杀康履算起，这位宰相从区区临时补任的尚书右丞，一路坚挺到今日，都快两年了……这个年限，放在常平年岁都显得可贵，何况人家还高升了一层成了正宰相，乃至于御前实际首相。</p>
<p>凭啥啊？</p>
<p>“朕也是刚刚想到的。”见到吕相公这般好拿捏，赵玖当即笑对。“十统制结义，最大的错处便是在公制外另起私制，这样长久下去，便是他们初心再如何，也迟早会落得张遇一般结果，所谓不可轻易试探人心便是此意……”</p>
<p>已经跪在地上许久没有说话的马皋等十人听到张遇二字，也是惊恐心再度大起，却又有了一丝期待。</p>
<p>“既如此，何妨化私为公呢？”赵玖忽然正色。</p>
<p>“何为化私为公？”吕好问是真没想明白。</p>
<p>“私制扩大一点，大到和公制一般大小，不就公私合一了？”赵玖循循善诱。</p>
<p>闻得此言，下方万俟卨和十统制中几个脑袋活泛的早已经目瞪口呆，但如吕好问、范宗尹这种世家人物、诗书种子，几辈子都是紫袍出身的，却明显还是有些难以理解。</p>
<p>赵玖见状却也不再遮掩，而是干脆挑开了谜底：“这样好了，今日御营诸军，除御营后军尚在东南外，各部主力云集各军主官，只要实领个数千兵马的，眼下八成都在此处……朕的意思是，既然东京留守司上下都讲义气，靠聚义结成一体，那御营全军何妨一起讲义气？台上诸统制、还有那几位暂居统领的，一并出来，就在这河阴之地，让几位相公、太尉一起做个见证，然后指着朕聚个大义，这不化私为公了？”</p>
<p>这下子，莫说吕好问、范宗尹，满场上下，文武百官，一起目瞪口呆。</p>
<p>“官家，这不妥吧？”</p>
<p>半晌之后，在赵官家的静候之中，和这位官家想的一样，两位枢相中脾气最大的吕颐浩居然忍住了没有出列驳斥，倒是许景衡忍不住直接座中起身驳斥。</p>
<p>“朕知道不妥。”赵玖端坐其上，朝许景衡微微一叹。“可还有更好的法子吗？宗相公逝前曾托付东京留守司于朕，明言这些兵马出身军贼、土寇颇多，不可不制，但又言国家艰难之时，又不可不用……稍许朝廷体统，能换十个统制官与他们部下数万大军安心为国效力，又什么不舍得呢？这买卖做的！”</p>
<p>“官家圣明！”许相公刚要再言，中间万俟卨便伏地叩首，大呼响应。“臣囿于眼界，心思狭窄，竟一时起了文武分界的阴小心思，着实惭愧！殊不知，眼下抗金为先，自然万事当以军务为上！”</p>
<p>许景衡瞪大眼睛扭头去看万俟卨……一时语塞之余也是一时气结。</p>
<p>非止是许相公，汪伯彦、吕颐浩、刘子羽等枢密院要员，几乎是齐齐去看此人……南阳许久，他怎么就没发现枢密院里居然藏着这种人物？</p>
<p>而且，怎么忽然就有了底气？</p>
<p>官家给的？还是自己寻的？</p>
<p>当然了，看归看，万俟卨一语还是塞得几位相公和所有想反对的人话都说不出来，</p>
<p>检阅台上，再度鸦雀无声。</p>
<p>片刻后，倒是赵官家凛然相唤：“马统制你们觉得朕这个法子如何？”</p>
<p>“臣等自然无话，只有感激。”回过神来的马皋在地上连连叩首。</p>
<p>“那就好，朕还以为你们嫌弃朕不够格做这个聚义指誓之人呢……”赵玖说着复又看向自己右侧那些目瞪口呆的御营左军、右军、中军统制官们。“你们又是如何言语。”</p>
<p>“臣……”</p>
<p>且说，或因出身，或因举止行径，有人其实是看不上其余同僚的，闻得官家问讯，本能想起身推辞，但听到刚刚那言语，却不由头皮发麻，又即刻改口。“臣等以为此事极为妥当。”</p>
<p>“良臣以为如何？”赵玖见状再去问韩世忠。</p>
<p>“官家心思真是绝妙。”韩世忠赶紧起身拱手。“只是统制官们指着官家聚义，臣等又该如何？若是也一并指着官家结义，岂不跟他们错了辈分？”</p>
<p>韩世忠的着眼点总是有点让人难以捉摸，不过赵玖本就没有让他们牵扯进来的意思，所以愕然一时后便旋即摇头：</p>
<p>“你们自是朝廷大臣，以朝廷法度为准，不用结义……座中有父子、叔侄的，也可只取长辈来聚义。”</p>
<p>“那臣便觉得可行。”韩世忠干脆应声。“臣愿做这个见证。”</p>
<p>“臣也以为可行。”岳飞赶紧跟上，对他来说，此事虽然也有些荒唐，但如果能够避免马皋等人被废弃，东京留守司兵马被遣散闲置，那总归是个最好的选项。</p>
<p>“臣赞同。”张俊赶紧跟上。</p>
<p>李彦仙见状环顾左右……且说，他对此事其实是极为不爽利的，因为这太掉朝堂脸面了，而且他自诩功高，又是西军正经出身，还一直谨守忠义，对这些反反复复做过军贼的人天生看不顺眼……但无奈上下左右帅臣皆同意，又经昨日教训，他也不好再跟官家闹别扭，便无言起身侍立，算是与诸帅臣同列了。</p>
<p>李彦仙既起，王彦、王德、闾勍俱起身侍立，而七名帅臣起身，下方诸统制、统领再无犹疑之态，纷纷攘攘，即刻起身离席，来到当中随马皋等人一起朝赵官家下跪，万俟卨也赶紧拽着范宗尹离开了此处。</p>
<p>赵玖抬手示意，自有杨沂中上前辨认唤名在场军将。</p>
