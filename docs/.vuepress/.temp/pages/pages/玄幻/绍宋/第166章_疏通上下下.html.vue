<p>所以在当时，五毒俱全的韩世忠看不起这些人，动辄骗过来杀掉是真有理由的，李彦仙从一开始就渴望被朝堂认可为正规军也是有理由的……谁想被当成这种人啊？</p>
<p>文官们厌恶这些人更是合情合理。</p>
<p>当然了，和这些人大略相同，都是河北流亡军士的岳飞却是个特殊的例子，不可以拿他跟其他人相比的，这个人能成为上千年来的中国军人的楷模是有他的独到之处的……我常在群里跟人辩论说，岳飞这个人身上两个点，一个是朴素而伟大的爱国主义……也就是靖康耻犹未雪，臣子恨，何时灭；一个是突破时代朴素的底层视角情怀，同时真把底层老百姓当人的……也是饿死不掳掠，冻死不拆屋了。</p>
<p>这两条任何一个都可以让一个人突破时代桎梏，何况是两条？而有这两条在身，什么大小眼，喝酒醉打人，是啥啊？是让他从神活过来变成人的要素而已。</p>
<p>实际上，岳飞这个名字，本身在中国文化里，已经有了一定的特定情境，类似的，甘地和萨拉丁居然需要两个人共享‘圣雄’这个外号，反而显得不足。</p>
<p>一个人伟大到一定程度，说出的名字，就有了抒情的感觉。</p>
<p>不过，我们也不能因为他的光环，就反过来把跟他类似出身的上下左右都想的太好了……回到历史情境里，一定要避免这种情况。</p>
<p>总之，回到跟前，扯了这么多感慨，还得再说回来，这些人再成分复杂，再堕落，你还不得不承认，那年头局面已经成这个样子，想要在军事上收拾局面，残留军队、义军，所有在军事上能团结的对象都必须要团结起来，继而约束起来，使用起来，只有这样才能尽可能的早一步获得胜利。</p>
<p>而正如赵玖质问杜充时说的那般，只有结束了战争，这些摧残人性的东西才会消失，人才有资格去做一个人。</p>
<p>实际上，这正是宗泽的伟大之处，他看的更清楚，更懂得深层的矛盾或者说矛盾根源，他知道这股力量的必要性。</p>
<p>相对而言，官僚体系中的人，单独拎出来都是个正经好人，一起成了体系后，却又不可能在短期内变成一个高效的机器，更何况，金军也不会给主角这个整合官僚体系的机会……又或者是，幸亏金军来得快，没让他被官僚体系的安乐窝给同化掉了。</p>
<p>所以，到了本卷后半段，面对着军事上现实困境，清醒过来的主角不在沉默中死去，就只好在沉默中雪夜上梁山了。</p>
<p>于是有了后半段情节上的忽然激昂。</p>
<p>但是这里必须要说两件事……一个是从情节上讲，主角这是贪天之功，他其实是在用宗泽的遗产和余威来完成他的救赎的，没有宗泽替这个国家收拾了这么一堆人，主角连本钱都没有，所以他才会在面对宗泽时彻底醒悟，然后写下了那首词，因为他在宗泽这里找到了自己缺的另一条腿，和一条真正的道路；</p>
<p>而另一边，从作者的角度来说，我要说自己这段写的其实并不好……因为本可以更好，大家知道那段事情连续准时更新，做的很棒，棒到我自己都有点骄傲。但事实上，很多时候到了很晚的时间点，我其实一章都只写了一半或三分之二，这个时候就经常很仓促的去只花半小时、一小时写最后三分之一甚至一半章节出来，干干巴巴，毫无润色，这就使得原本可以更<strong>的剧情有种到了章节末尾</strong>不到位的感觉。</p>
<p>所以还是得道歉。</p>
<p>但不管如何了，赵官家找到了自己的灯火阑珊处，又下了这么多生死符，咱们就不扯什么感性分析了。</p>
<p>例行说几个小剧情。</p>
<p>历史上，赵构的确是靠着抬高苏轼，以苏轼为引子，来宣扬自己政治立场，然后大约算是继承了宋钦宗的旧党姿态，却以一种更模糊和更婉约的方式，来展示出来的。</p>
<p>除了追赠苏轼太师的政治姿态外，他也确实有在苏轼真迹上题字表露心迹的历史段子。</p>
<p>而历史上，张浚罢相之后，也有过一个推荐人的段子，当时宋高宗想让他推荐秦桧，结果昔日跟秦桧关系极佳的张浚这时候已经发现对方是个烂人了，所以宋高宗一问，他立即直言此人无德，不可为相。</p>
<p>然后赵构再问，其实是想他再推荐赵鼎，结果张浚明知道对方心意，却不说话了……他知道赵鼎比秦桧强，却囿于二人对立局面，没有吭声。</p>
<p>于是赵构启用了赵鼎，但赵鼎却不可能再与张浚默契，昔日靖康之变太学中逃难出来的一伙人，就此分道扬镳。</p>
<p>接着就是赵张辗轧，秦桧趁势而起……只能说文官，他这个体制内的思维就很过分……</p>
<p>但反过来说，书里面张浚走前最终推荐了已经跟自己发生了剧烈矛盾的胡寅，其实就是段子反用了……最起码主角对他交心和信任，让他还是无脑选择了顺着官家心意来做。</p>
<p>十统制结义没有历史原段，但在历史上东京留守司崩溃后，确实有拉帮结派，然后结义的戏码，而且还不止一处。</p>
<p>甚至拉帮结派后，几波山头还有相互火并。</p>
<p>其中，历史上马皋作为忠心大宋的代表人物，被造反其他统制官害死后，一丈青应该是投奔了闾勍，因为闾勍认她做了干女儿，并通过联姻又将他嫁给了张用，后来闾勍也败走，或者战死，张用和她一起做了流亡军贼，岳飞又招降了他们公母，算是有个好结果。而从历史叙事上，也看的出来，马皋、一丈青、张用这样的人，多少是有点底线的，裹挟老百姓当军队肯定少不了，但相对于其他人还是很有朴素道德感的。</p>
<p>不过有意思的是，一丈青改嫁后，依然自称马夫人，上阵就打着马夫人的旗号，号称忠烈……反正张用应该是没意见。</p>
<p>王善这个人也很有意思，在那个混乱阶段，前期兵马最多，势力一度最大，张用也依附他，所谓很多义兄弟也跟着他混，而他也在攻打大宋官军防守的城池时，说出过那句‘重定贫富、贵贱’的言语。</p>
<p>并且这个人，前期活跃中始终没有投降金军和伪齐，是坚持底线之余努力想往上爬的。</p>
<p>不过，后来他母亲随他辗转过程中，一次失足落水，直接去世，让原本很有野心和能力的王善立即就丧失了奋斗动力，最后直接心灰意冷，投降了金军或者伪齐，从此在历史中消失不见。</p>
<p>而这些人，王善、李逵、病关索李宝，改嫁的一丈青，哦，还有扈成，甚至包括杀孕妇取胎儿占卜，却又英勇抗金的邵青，应该就是后世《水浒传》的灵感所在。</p>
<p>他们是一个极度复杂的，整体来说比较堕落，却又堕落的有缘故（被世道逼着选择了滥用武力）的群体。</p>
<p>所以，这里面的关键是，他们之所以会普遍性、整体性的堕落，只是因为缺乏一个人来接替宗泽约束整理他们。</p>
<p>而书里面主角出现了，还下了生死符。</p>
<p>那么因为主角的存在感，原本抱憾的人可以释然而去，原本会堕落的清白人士免于堕落，原本会天日昭昭的……那就更不用说了。</p>
<p>这就是历史穿越小说的根本爽点所在，他应该是大家内心深处某种朴素的正面的道德感与历史还有小说相互共振的结果。</p>
