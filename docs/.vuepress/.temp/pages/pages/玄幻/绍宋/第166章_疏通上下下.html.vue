<p>这些复杂念头，在岳飞脑中转了一圈，却只是一瞬而已，一瞬之后，岳飞便咬牙起身，拱手相对身后官家，并严肃以对：</p>
<p>“官家，好教官家知道，这些人结义之事确实有欠考虑，但他们多出身草莽，行事草率，若说心怀不轨，未免严重，还请官家看在他们长社一战有功，从轻发落……”</p>
<p>岳飞毕竟是新晋帅臣、颇有圣眷，而且真正内行的人都知道，此人乃是河北人，是赵官家用来接收东京留守司这个河北流亡集团（也就是眼下跪着的这群所谓十统制等人）的特定人选，本身地位也是极为稳固。</p>
