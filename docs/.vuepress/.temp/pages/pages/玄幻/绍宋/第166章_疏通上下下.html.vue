<p>作息颠倒了，想调整下，提前更了一章，然后照理说该万事大吉，早点休息，好好放松一天，弄个好状态来结束这一卷。但实际上，那天晚上我刷了知乎，补了新番，最贱的是我在群里某位大佬的推荐下于凌晨买了《骑砍2》，然后就一直玩到天明。</p>
