<p>同人10梦京华——楚茗淮鲤</p>
<p>正式迁都燕京的那个夜晚，杨沂中做了一个很长很长的梦。</p>
<p>金国殄灭，敕约已成，北疆大定，又花了几年谋划、营造，慢慢迁都至此，尘埃落定后，官家在拿原金国皇城旧物利用改建的宫中摆了一场宴席，几乎喝到酩酊大醉。杨沂中和邵成章一左一右，把官家从设宴的大安殿扶回昭明宫（注1）时，清楚地听见这位官家嘴里在说着胡话，什么“普普通通大学生”、“京城啊我回来了”、“帝都现在就这就这”、“要搞紫禁城吗算了太花钱了……”</p>
<p>邵成章一脸茫然，杨沂中心底转过了千百个念头，一句话也没说。只屏息静气地听着官家的梦呓渐渐被呼噜声取代，他才回到自己的宿处，忽然动容。</p>
<p>迁都已定，燕云尽复，千秋功业，已然奠下根基。酒意蒸得他头脑昏沉，此时再想起靖康之前东京旧梦，建炎元年风雨飘摇，八公山上国仇家恨，只令他神思恍惚，不知是何夕何年，何处梦中。</p>
<p>迷迷糊糊间，杨沂中被灯光晃醒了。他睁开眼睛，发现自己站在一座高得有些超出想象的大楼前。他的面前摆着一个巨大的，有着六个轮子的大盒子，十几个二十来岁的青年正排成一队往里走。他们衣着打扮都颇古怪，男子居然都是短发髡头，女子衣着则颇为大胆，肩颈手臂大腿都能露在外面，看众人表现，似乎也不以为然。</p>
<p>“赵玖，快上车！”等到大家都进了盒子之后，一个年长些的男子自盒子口探出身子来：“就等你了！”他的口音颇类北地方言，虽有微妙不同，倒也勉强能听懂几句。</p>
<p>杨沂中乍听得“赵玖”的名字，也未太惊讶，只当同音，但随即响起的熟悉声音，却把他吓了一跳，下意识地就想喊官家。</p>
<p>“来了来了！”一个背着包的年轻人一路小跑冲了过来，三步并作两步跳进了那个盒子。杨沂中下意识地跟了过去。这个人身量不矮，不过还是显得有些瘦弱。五官与官家并不相似，但显出的情态倒有些像官家……不，杨沂中目光落到对方衣服上那只抱着脑袋，神情呆滞的大头鸭子上，突然怀疑自己的眼力，他怎么会觉得这只鸭子与官家也有几分神似？</p>
<p>杨沂中也进了那个盒子，可盒中众人像是没看到他一般。赵玖抱着包跑到最后一排坐定，年长者挥手对最前排那把着一个古怪圆盘的中年人道：“我们人齐了，师傅开车吧。”</p>
<p>盒子突然动了起来。杨沂中猝不及防地向前倒去，他下意识地扶住座椅，却看到自己的手从中穿了过去，丝毫没有受阻。他站稳了身形，才发现自己的脚也是虚浮在地面上的。</p>
<p>这是梦吧。杨沂中挪到后排，在那名为“赵玖”的年轻人面前挥了挥手，对方毫无反应。他立刻释然了，既然是做梦，那么一切都无所谓了，只要随心所欲就好。</p>
<p>他把视线投向窗外，就再也移不开眼了。虽是夜间，街边的灯盏却依旧大放光芒。宽阔的道路上居然有大桥凌空飞架，也不知是何用途。而在路口，还有会自动改变颜色的灯指挥方向，红绿橙黄，井然有序。街边高楼鳞次栉比，各色店铺虽未开门，但门前都有彩灯闪烁，远远望去，恍若霓虹。街边虽暗，也看出有绿树成荫，鲜花盛放，一派盛景。</p>
<p>真是奇景。杨沂中怀疑自己来到了什么神怪世界，但他在路边的路牌上发现了一些熟悉的名字，比如玉渊潭，莲花池，甚至还有一条“金中都路”（注2）。“日有所思夜有所梦。”他对自己说，这几日随着官家看中都周边地图，都要把这些地名刻进脑仁里了，连梦里都要出现。</p>
<p>这个“轮盒”在路上飞驰了不知多久，杨沂中忽然看到了那座他在心中描摹了千百遍的城楼。它从那枚小小的硬币上跳出来，忽地有了颜色，熹微的晨光里，他望见金顶红墙，大红灯笼，高挂的人像面容端庄慈和。这里，不是官家的故乡吗？</p>
<p>他茫然地随着这群人下了车，环顾四周，却发现虽是凌晨，身后那片宽阔广场上竟然是人山人海，也有零星几人，穿着像是汉家装束，依稀辨得出上襦、褶裙、褙子之类衣装，却也不太像是大宋风尚。</p>
<p>大早上的这么多人挤在这里做什么？准备进城？可是这里只见城楼不见城墙，不远处楼台林立，显然就是在城中。这种城中之城……他心中一动，莫非，是都中宫城？还没等他猜出个所以然，盒子里的人已经全出来了。带队的那个年长者点了点数，打起写着“XXXX大学暑期文博实践”的小旗子，朝着广场中心高耸入云的纪念碑走去。</p>
<p>这里是官家的来处。杨沂中走在那名为“赵玖”的年轻人身边，心里千回百转。难道这个人是官家？长相虽然不同，可是声音和名字未免太过巧合了。</p>
<p>他决定继续观察。这个时候，对方看不见自己倒也是一种方便。杨沂中跟着呵欠不断的学生队伍一路往广场中心走去。这一队青年有男有女，彼此之间谈笑自若。这里的女子似乎颇不受拘束，能够自由出行，与男子交流。而那面旗子上的所谓“大学”，似乎也允许女子读书……只不知道这大学是个什么样式的学府，难道与太学类似？又或者同书院仿佛？不管如何，总归是个读书明理的地方。</p>
<p>一路胡思乱想着，杨沂中已经跟着队伍走到了广场中心那座米白色的纪念碑前，看着他们四散开去，掏出各式各样的扁平方块对着这座纪念碑一顿点来点去。</p>
<p>“人民英雄……永垂不朽。”这碑上的字迹杨沂中倒是认识，意义却令他有些不明就里。这些英雄，会是谁？他的目光落到了碑底那些足有真人大小的精美浮雕上，依然看不出到底是在刻画什么。正在他迷茫的时候，远处又走来一队小孩子，脖子上都戴着红色领巾。他们的带队老师掏出了一个喇叭样的东西，大声讲解了起来。</p>
<p>“同学们，现在我们看到的就是人民英雄纪念碑底座上的八面浮雕，大家猜猜看这都刻的是什么？有没有小朋友说一下？请举手——对了，就是虎门销烟……”</p>
<p>杨沂中聚精会神地听着，由于语音的差异，他听得很费劲，所幸给孩子们讲课的老师语言浅显，娓娓道来，倒也能勉强听懂一些。似乎在近百年之前，官家的故乡正处在风雨飘摇之中，内忧外患，屈辱无比。朝廷无力对抗外贼，丧权辱国。内部也纷乱不断，多有起义……</p>
<p>等等，这起义，难道不就是造反？杨沂中浑身一震，造反有理，还能这样公然立碑纪念？难道这造反的后来取了天下？可是这老师分明说这“太平天国”没几年就被平了啊……而后来的武昌起义，南昌起义，其大逆不道的程度更是一浪高过一浪，打得他整个人晕晕乎乎，什么反帝反封建，推翻腐朽落后的封建王朝？国不可一日无君，不要皇帝了，这天下怎么过？这里的小孩子从小就学这些东西？</p>
<p>杨沂中脑子里嗡嗡响，后边的什么创立民国，革命战争他已经听不太懂了，但是抗日战争……日本？是哪个日本？杨沂中一口气险些憋在喉间，片刻后又迷惑起来，如果真是日本，官家身为此国之人，如何还能留着平清盛他们？不早想法子像灭西夏那样把东瀛平了？</p>
<p>他在那里丈二和尚摸不着头脑，那老师可没等他想清楚，顺着浮雕讲下去，日本人到底还是被赶走了，又打了几年仗，这片土地终于硝烟散尽，算是太平了下来。直到这时，队伍绕着纪念碑转了一圈，杨沂中才看到纪念碑背面那段碑文。</p>
<p>“三年以来，在人民解放战争和人民革命中牺牲的人民英雄们永垂不朽！“三十年以来，在人民解放战争和人民革命中牺牲的人民英雄们永垂不朽！“由此上溯到一千八百四十年，从那时起，为了反对内外敌人，争取民族独立和人民自由幸福，在历次斗争中牺牲的人民英雄们永垂不朽！”</p>
<p>人民英雄。又是人民这两个字。这两个字究竟代表什么呢？杨沂中揉了揉眼睛，那八块浮雕上，有男有女，有老有少，有头缠布巾的农人，有拿着书本的学生，有拿着刀棍的武人，也有穿着布衣的平民。唯独不见衣着华贵的帝王将相，不见青史留名的风流人物，连那频出虎狼之词的老师讲学时，也未提起画中有哪个有名有姓。</p>
<p>他正在胡思乱想，那边的一群学童已经在老师指导下，往碑前献上了一束尚含露水的白菊，随后列队整齐，把手举过头顶敬礼。赵玖他们这群大学生不需要老师一面面浮雕讲解，各自拍照、参观完毕后，也由班长和团支书带头献上了花束，并一道鞠躬致意。而那碑前除了鲜花，也有游人摆上瓜果，奉上灯烛，往来凭吊。</p>
<p>杨沂中望着这一幕，忽然想起了淮水上随水而去的点点橘灯，尧山上密密麻麻的灵位，还有岳台大祭时那正中居首，却没有名姓的巨大牌位！在这一刻，他模模糊糊地懂了点什么。</p>
<p>夜色将尽，东方已经泛起了霞光。带队老师再度打起旗帜，领着学生们往城门楼走去。杨沂中也跟着走到那座洁白大理石修建的桥梁前，他望见了三座城门，却没看到卫兵。他正在盘算自己这个状态是否能直接入内时，朱漆的城门豁然洞开，一队身着暗绿短装，高筒黑靴的青年列队而来，步伐齐整，踏地铿锵。为首之人擎着红旗，左右执兵护卫在侧，雄赳赳，气昂昂，在众人的欢呼声中行过玉桥，停在了那根高耸的旗杆前。</p>
<p>一队精兵。杨沂中立刻就做出了判断。体格，队列，还有步伐，气势，单论这几点，这区区十几人展现出的风貌丝毫不逊于御前班直中的精锐。还没等他思考出这一小队精兵是干什么的，宏大的音乐声就不知从何处响起，将杨沂中吓了一跳。红旗展开，似曾相识的五颗金星让他心神一震，而海潮般的哼唱声从身后涌来，拥着红旗，迎着东方的霞光，缓缓向天际升去。</p>
<p>杨沂中身前的几个幼童把手举过了头顶，更多人则只是站直身体，目光紧随着逐渐升高的红旗。当晨风在旗杆顶将这面旗帜完全展开的时候，第一缕阳光也刺透东方的层云，洒在了朱红色的城楼之上。</p>
<p>杨沂中望着那面绣着五颗金星的旗帜，忽然意识到了这个仪式的意义。他也明白了为何天不亮就有这么多人聚集在广场上，他们就是在等待这面红旗随着旭日一同升起！而那奏响的乐声，上至耄耋老人，下到总角孩童，都能跟着唱起，更是让他感到不可思议。</p>
<p>这果然是一个不寻常的国度。</p>
<p>日头已经渐渐高了。天光大亮，文博实践的学生队伍转出广场，过了一条道路，说要趁着博物馆还没开门去吃点早饭（注3）。</p>
<p>杨沂中着实不明白这路上怎么就变出这么多长轮子的盒子，有的大有的小，有的黑有的白，明明没有牛马拉，却跑得比战马快多了。而这路也着实宽阔平整，东京城的道路与之一比，就像乡下赶集的草街。</p>
<p>过了这路后，赵玖和几个同学说要去吃“开封菜”，杨沂中反复看了几遍那红艳艳的店招，怎么看都是“基德肯”三字，店里食客在吃些炸鸡、肉饼、肉卷之类的东西，也有豆浆、油条等稀松平常的早食，与东京菜色毫无相似之处。</p>
<p>反正这里他弄不懂的东西已经很多了，也不多这一样。杨沂中放弃了深究，随即又看见了一样特异。赵玖他们点餐，既不去柜台，也不唤小二来，只是在一块大方屏幕上戳了几下，找了桌子坐好后，便有人叫号取餐了。杨沂中站在一边吸吸鼻子，居然也觉得有些饿了。</p>
<p>赵玖要了一盒炸鸡，配着什么薯条吃得欢，狼吞虎咽一番后，又美美地灌了几口那棕黑冒泡，唤作“可乐”的饮料，叹道：“还是吮指原味鸡好吃！”</p>
<p>杨沂中再次忍不住把这年轻人和官家对比起来，这吃相和官家实在是别无二致，但家平日喜爱的吃食，似乎没有炸鸡这一样。在八公山和南阳，向来是有啥吃啥，回到东京，先是吃了数月的野兔，后来又开始吃自养的鱼、鸭，鸡倒也是有喂的，只是炸物费油，很少专门做来。只是这东西他明显是爱吃的，菜色也无非是鸡肉腌好，裹些面粉炸熟罢了……</p>
<p>这青年若真的是官家……难道官家为了北伐，连这点饮食上的小奢都弃了？杨沂中长叹了一声，有这样的官家，何愁北伐不成？</p>
<p>吃过早饭，学生们三三两两集合，又过了一次完全见不到马的“马路”，回到了那朱红色的城楼前，此时三扇大门都已经敞开，游人如织，络绎不绝。杨沂中随着众人进了那座城门，这才抬眼看见了内门上的匾额。</p>
<p>故宫博物院。这个名字让杨沂中有些心慌。故宫，哪一朝故宫？果真如那帮学童的老师所言，这国中没有皇帝，宫城也可以这般供人随意出入？可博物院又是何意？而且，方才那一小队精锐，自升旗之后便回转内城消失不见，他们是谁？是哪家的兵士？那红旗既然在宫城前升起，必然意义非凡，红底金星，是哪一国的旗帜？</p>
<p>无数个问号在杨沂中心头盘绕，但是却无人可以解答。赵玖和他的同学们凭身份证刷了安检，过了午门，一路往前，小跑着冲下午门后的斜坡，在太极殿前分散开去，自由活动。</p>
<p>中轴线人流太多了，不是第一次来游览故宫的赵玖直接避开了三大殿，转到西侧文华殿看书画去了。杨沂中跟在赵玖身后进了书画馆，陈列室里灯光柔和，文物们静静躺在展柜里，再次震撼了大宋的御前统制官。</p>
<p>如此多的珍贵书画……颜真卿《竹山堂连句册》，顾恺之《列女仁智图》，周昉的仕女图，还有许多作品他并不知晓作者名姓，但能与颜真卿顾恺之等人同列的自然也不会是庸才劣作。而看展品下的标签，都注有年代，作者，简介。</p>
<p>杨沂中不是所有字形都认识，但“宋”字还是知道的。耐人寻味的是，这些展品，还分北宋、南宋……这不由得让他有些恼火，官家分明已经打到中都，收复了连太祖都没能收入囊中的燕云故地，哪里来的什么南宋！等等，官家的故乡，怎么也有秦汉唐宋之史？而这画……他看向作者，心里一抽。</p>
<p>“《芙蓉锦鸡图》，宋徽宗的花鸟。”赵玖盯着这幅画，撇了撇嘴。“画是好画，人真是个烂人。”</p>
<p>一个同学鄙夷地道：“徽钦二帝，人中之耻，谁赞成谁反对？”</p>
<p>“呵，靖康之耻，整个国家脊梁骨都打得稀碎，亲眷被抢被杀被辱，要我早一根绳子吊死了，他们还能在五国城苟活那许多年呢。”赵玖冷笑着道。</p>
<p>“他倒是想过自杀，这不是到了儿不敢嘛。”另一位女同学凉飕飕地接过话来：“他是优秀的书法家和画家，做皇帝，只能说可怜了大宋天下。”</p>
<p>众人你一言我一语，把赵家父子给骂了个狗血淋头，杨沂中满耳朵的大逆不道虎狼之词，只能干站着想，辱骂二圣如此妙语连珠，不愧是官家的乡人。这群年轻学子丝毫不顾及天家颜面，一句比一句诛心。正在慨叹时，只听众人又骂到了什么“宋高宗”。</p>
<p>“完颜构吗，这人更不是东西。”赵玖顺口道，殊不知完颜这个姓氏险些把杨沂中给吓跌了。</p>
<p>“不然呢？就是条狗当皇帝，也比他有用。”</p>
<p>“至少狗不会十二道金牌把岳飞骗回来杀。”</p>
<p>“狗也不会写‘臣构言’给金人。”</p>
<p>杨沂中一开始还尽力安慰自己，那什么“高宗完颜构”可能是金人立的伪帝，就如济南刘家父子一般，可是当一位学生嘴里骂出“赵佶父子三人一个比一个垃圾，赵构尤其垃圾”时，他再也不能虚饰半分了，只觉得天地虚幻，万事荒谬！</p>
<p>官家爱重岳飞，宛如爱惜自己手足，如何会杀他？官家白马绍兴宁死不和，如何会向金人称臣？官家**平北方打得金人一败涂地，如何会使江北尽丧，偏安一隅？</p>
<p>还是说，如果官家不来，如果还是那个逃亡途中还不忘寻觅浣衣娘的官家，还是那个醉生梦死只想着安稳到扬州的官家，大宋就会如这群学子所言，被打碎脊梁，一蹶不振，良将无用武之地，忠臣被天日昭昭，就此慢慢走向灭亡？</p>
<p>他一个激灵，突然想透了。人种，语言，文字皆相似，历史一脉相承，而且他们都自认身出华夏中国……</p>
<p>“居然是这般吗……”杨沂中喃喃念叨着，险些落下泪来。天不弃我大宋，他们有了官家，何其有幸！</p>
<p>杨沂中默念着那几个字，宋，元，明，清，民国，共和国，他靠着一些展品信息中展示的只言片语，纪念碑下的那一场小学生历史课，还有玉渊潭，莲花池，甚至还有一条“金中都路”的地名，生生拼出了一个解答。</p>
<p>原来如此。这就是官家生活的地方，这里是千年以后。</p>
<p>难怪我会到这里来。他如释重负。虽然世殊时异，但千年以后的燕京，依旧是华夏天下，而且，还如此壮丽，如此繁华。原来官家来自这么一个国度，这么一个世界，难怪，难怪。</p>
<p>他慢慢睁开了眼睛，一夜好梦，神清气爽。“正甫昨晚睡得可好？”赵玖昨夜明显是喝多了，眼下还有点恹恹的，看见他精神饱满地进来，略有些惊讶。</p>
<p>“好叫官家知道，昨夜做了个美梦。”杨沂中拱了拱手。</p>
<p>“梦到什么？”赵玖顺口问。</p>
<p>“臣梦见与官家同游燕京。”他顿了顿，才状似随意地补充道：“北京城繁华壮丽，人民富足安康，宛如仙界一般。”</p>
<p>赵玖愣在了那里。片刻后才笑道：“是吗？朕也做了个梦。”</p>
<p>“官家梦见什么？”赵玖看着他不说话，一君一臣相视良久，慢慢地都笑了起来。</p>
<p>满江红</p>
<p>夜游京华，稍得全君臣缘法。望城头红旗招展，碧空如画。十年磨剑试霜刃，千载沧桑思华夏。小童儿碑前听旧事，捧素花。</p>
<p>此间事，黄龙了；那边厢，风波罢。数帝王将相，孰为罪大。黎民北上血满襟，江河东去泪无涯。后来人书中共一梦，记闲话。</p>
