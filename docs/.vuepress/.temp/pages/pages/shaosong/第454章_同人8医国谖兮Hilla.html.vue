<p>说罢，潘国丈根本不等杨沂中回复，便拂袖而去。</p>
<p>“扑哧。”</p>
<p>杨沂中回过头，看着不远处被皇城司连哄带强邀过来监督拣药熬药的御药局博士在他的视线下马上像受惊的鹌鹑一样瑟缩起脖子，努力降低着存在感，忍不住叹了口气。他不必像对方一样背过《本草》《圣惠方》也能听懂潘永寿是在骂他犯了痰症。不过他随即就转过身去，压着疲惫，继续盯着班直们称量药材，将那博士抛在脑后——他早就发誓，这次的汤药从拣选药材到熬制出锅，任一个步骤都不会逃过他的眼睛，又哪里顾得上计较这种微不足道的冒犯。</p>
<p>“炙甘草三十两！”</p>
<p>他此前寻人问过了方子，生背下了方中的君臣佐使。甘草便是这十几味药中的使药，调和表里，又味甜。</p>
<p>而那人……应该也是喜欢甜品的。刚从井里出来，便想着雪糕。可当年的潘娘子亲手做了，到最后对方也没动一口，竟是将美人辛苦做出来的甜品全分给了赤心队的士卒。就杨沂中所知，眼下的赤心队私下若是聚众饮酒，喝到高处，排资论辈，夸耀功勋，总有一两个老人儿跳出来，炫耀自己尝过宫中贵人的亲手赏赐，并把雪糕的滋味吹上了天。但多年的同僚默契让他心知肚明，皇城司报告中次次不发一言只负责结账的刘晏其实与他一样，今生都压根再不想听到雪糕二字了。</p>
<p>风雨飘摇的朝廷狼狈南逃，衔尾而来的金人步步紧逼。“失忆”的天子分了点心又夜宿在赤心队营帐里以示与众人同甘共苦，反而激起了无知蠢货悖逆的心思。平叛，安抚，对个人命运的忐忑，忧天倾难挽的惶恐。那一夜在他们这些真正知晓大局之人眼里，滋味委实难言。</p>
<p>那一夜，杨沂中隔着帐幕，下定了决心。</p>
<p>“芎?三十两，去皮茯苓，去芦人参各三十两！”</p>
<p>三味佐药，芎?行血、茯苓除湿，人参逆流挽舟，固本助元。</p>
<p>淮水雪渡舟中，那人一席话让年轻的御史中丞泪流满面，杨沂中怔然望着对方身影，只觉贴身所藏的奇异花纹金属圆片都仿佛被那人话语激得滚烫起来，与他心头翻滚的一腔热血隐隐相应。小舟离岸的那一刻，他便明白，哪怕他素不信怪力乱神，那个秋天却真有奇迹自井而生，于他面前睁开眼睛。</p>
<p>后来世人都说他的老上级张俊张伯英嗜财擅赌，以一座下蔡城博来了一世富贵功名。但只有杨沂中知道，他观察过，猜疑过，犹豫过，但早在明道宫时便以一念四字为注将皇宋近二百年国运托给了天意。</p>
<p>幸而，天意未曾相负。</p>
<p>“去苗柴胡，前胡，桔梗，枳壳各三十两！”</p>
<p>四味臣药，助解表理肺，行胸中不畅之气。</p>
<p>尧山一役，天下震惊。那时他新伤刚愈，便陪着那人将一叠叠书名白纸流水一样送往后山新立的神庙。这是项沉闷重复的工作，不多时他对那神庙便如同御帐一样熟悉。而随着御营伤亡统计名录不断更新，那人要抄录的名字愈发多了起来，他就带着御前班直承担起从帐中到山腰庙中往复递送的任务。而当地民夫工匠将天子亲书牌位一事逐渐传开后，便有附近的西军家属百姓得了消息，三三两两过来提前拜祭。只要不往御帐方向去，那人也不让他们阻拦，到后来西军将士前来祭拜者越来越多，甚至曲端都托词汇报军情过来转过一圈——据在场的班直说，此番立下大功的曲都统进了庙门，罕见地一言不发，只觑着眼睛寻找熟悉的名字，在里面足足呆了半个时辰。</p>
<p>一天他刚刚送完新的一叠名录，因为这次名录中有他手下战殁的御前班直，于是多停留了一会，想要按工匠们的雕刻进度序推算这些神主将被摆放的位置。等他大致估算出方位，想着御前无事，便与一个刚刚换班的老工匠攀谈起来。</p>
<p>一聊才知道，那工匠来自熙河路，家中三子五孙，长子和两个孙子早年殁于王事。尧山之役，次子被发为民夫，三子跟了大刘经略，自己则成了随军工匠，家中只剩老妻和几个儿媳照料年幼的三个孙子。</p>
<p>那工匠年老眼花，看不清他身上的细甲，也不识得他的身份，只觉得他特意带上的麟州口音多少有些亲切，便以为也是附近前来祭拜同袍的西军后生，竟絮絮叨叨跟他说了好久，诉说往年金人的凶狠，挂念家中的老妻幼孙，末了还托他打探三子的下落——有风声说刘经略溃军了，他提心吊胆，日夜都为自己的三子担心。</p>
<p>他知道刘锡的熙河路残军眼下就在附近休整，如果此刻仍没有消息，老工匠的儿子多半凶多吉少，但望着对方期盼的眼神，他一时为难，竟没想出该怎么开口。</p>
<p>那老工匠听他半晌没有动静，眯了眼睛去瞧他表情，然后叹了口气，反倒朝他挤出了一个难看的笑容，“后生，我近日问了不少人，心底大抵也晓得是怎么回事了，也不用你费心编话哄我老汉。我儿，我儿若是真不在了，那我日日细细刻这些牌位，便是刻我的儿。我要告诉他，那是官家亲书的姓名，官家没忘了他。”</p>
<p>他心下震动，抬眼望向天井周围的牌位，恍惚间又想起自己家中自书姓名招魂祔葬的祖父与父亲，一时失神，只听那工匠侧身对着那侯丹神像后絮絮念着，“往年都打不赢，官家一来就赢了。儿啊，你安心，这一遭，终是真龙天子带着咱们打退了金人，老汉听军中的秀才说了，往后便能有太平的年景……”</p>
<p>他不忍再听，借口要误了归营时辰，胡乱一抱拳，转身出了庙门。可刚出门，他就发现那人默默立在外面，不知道在庙外听了多久。</p>
<p>他连忙请罪，那人随意摆了摆手，让他起身，却望着他许久没有出声，最终只道：“正甫，这神庙供奉的是本次尧山中战殁者的神主，至于靖康以来殉国之人，如李若水学士，如你父祖，还有牺牲的无数百姓——我早就有意，日后于东京举行大祭。”</p>
<p>他心头一酸，俯身下拜，却觉胸中舒畅，知道那人猜到了他之前想到了什么。此番娄室授首，他祖父若在天有灵，亦可瞑目。而尧山一战，攻守转为相持，就像那工匠所言，日后这片他父祖守护过的土地当有太平的年景。</p>
<p>他的下拜真心实意。</p>
<p>“羌活三十两！独活三十两！”</p>
<p>两味君药，祛风散寒，扶正祛邪。</p>
<p>汴梁数载，他望着渐稠的东京城袅袅人烟，念着新复的兴庆府汉家故地，领皇城司抄家拿人行事无忌，上朝时敢直视大宗正的眼睛，自认丝毫无愧于赵氏的江山社稷。只有建炎五年那一次，他低下了头，在白马渡新归的太后面前格外恭敬，任由一丝如晨雾般稀薄的怜悯掠过心间，却又忍不住自嘲自己的虚伪。</p>
<p>因为说到底，无论站在他面前执手相问的是尊贵的太后，还是殷切的母亲，他本质都不在意。</p>
<p>而那人也是如此。</p>
<p>先前对方交代他去先迎太后之时，语气坦然，神态平静，话音里听不出一分夺舍妖邪的自觉和心虚。</p>
<p>而他下午回转后屏退众人，一一交代太后妆貌衣饰，最后终于让本该留于黑夜中的私心占了上风，抬头望向对方，想要确认在即将到来的考验面前，他的同谋是否做好了准备。</p>
<p>那人回望过来，神色如常，像以往每一个白日一样向杨沂中温声道了句辛苦，然后便挥手让他退下。大殿天光下那人与那身红袍金带几乎融为一体，仿佛一个天生的皇帝。</p>
<p>可转日白马渡前，对方就从他腰间拔出利刃，划断天子衣袍，宣言惊世，誓与旧宋的丰亨豫大势不两立，而激切的言语中，对两河百姓亿兆生民的挂念，又失体面到压根不像一个应与士大夫共治天下的官家。</p>
<p>被掠去的宗室贵女无辜，那人并不在乎，这具身体的血亲太后方归，那人不给脸面。从梁山泊的张荣进入托孤名单到东京城的婢女成为发作宰相与他的案例，一次又一次的事实早就证明了对方的关注重点与世人迥异。自建炎元年的秋日以来，杨沂中便将圣贤的一些话语抛在了脑后，而自原学传世，他甚至开始怀疑历代儒家大贤是否真有人领悟大道。但当那人岳台大祭，杨沂中眼中望着无数无名有名牌位，又回忆起尧山庙中的天井。</p>
<p>天听自我民听，天视自我民视。</p>
<p>圣贤生而知之，可知十二冕旒下是妖邪，还是……</p>
<p>不过毕竟当世没有圣人，吕公相也不像要立地成圣——就算成圣了知道的内情都不见得会比杨沂中多。那人割破了手指会流血，饥饿时需要食物，那么约束他的应该还是人间的律法。</p>
<p>于是他找来新修的刑统律条，又辗转托人搜罗了许多《十洲记》《酉阳杂俎》这样的志怪随笔。夜深时他听着那人的呼吸，盘点着刑统中对藏在皇帝躯壳里的妖邪与知情不报者的刑期。</p>
<p>厌胜，魇镇，弑君，谋逆……首犯与从犯怕是不止大辟或流三千里。</p>
<p>他从来将这些心思压在心底，拒绝让光怪陆离的臆想与恐惧入侵他的白日。可形势逼上眼前，他昏沉宫中的天子方中君药号称扶正祛邪。</p>
<p>那么，孰为正，孰为邪？</p>
<p>何人……堪配为君。</p>
<p>而君药中恰有独活一味——他微微垂下眼睛，不愿继续盯着戥秤上那单凭名字就让他心浮气躁的浅棕褐色块根，又不敢真的让它脱离视线。</p>
<p>若独活一人，何人当生？</p>
<p>他知道自己的心意。</p>
<p>漫长的药材拣选称量终于结束。御药局的博士已经转过身，在一名班直的护送下朝煎药房的方向行去了。杨沂中朝面前另几名捧着称量好的药材等他指示的班直点点头，示意他们跟上，自己也站起身，只是在前往煎药所之前朝同样等着他命令的两位亲信统领之一沉声下令。</p>
<p>“给我盯好了潘氏一族，如有异动，即刻报来。”</p>
<p>那统领恭声唱喏，领命而去。</p>
<p>但他当然知道对方的真实想法——若是寻常人家，公子的病还没好，亲从却往死里得罪前来诊治的大夫。那么必有亲朋好友出来，美言不要钱一样的说，缓颊圆场。何况大夫还是便宜岳父，衙内亲从虽然日日鞍前马后，又怎比得上同床共枕的软玉温香。</p>
<p>前两天被他派出去调查却一无所获的另一位亲信统领已经毫不遮掩地劝过他了：“统制忠心奉上，为国忘身，属下感佩。可毕竟疏不间亲，而潘医官是贵妃亲父。统制圣眷无人能及，只是属下一点拙见，再深的圣眷，若是恶了宫中贵人，长年累月之下，枕边……”</p>
<p>他还记得那人望着他面无表情的脸嗫嚅起来，指斥乘舆的话语逐渐没了声息，到最后都慌不择言：“是属下妄言了，官家英睿，必不至于此。属下糊涂，可对官家和统制的一片忠心，苍天可鉴啊……”</p>
<p>他板着脸训斥了对方一刻钟，责以君臣大义，最后才和言抚慰两句，算是安抚了手下最亲信的统领，回转过来心中却苦笑一声。再想起那‘枕边’二字，只觉说不出来的荒谬，一时不知是该庆幸自己与那人的真实关系在皇城司上下瞒得太好，还是该对自己最亲信手下的判断能力彻底绝望。</p>
<p>不过，前车之鉴在彼，自此再也没有人试图劝他回心转意，皇城司上下当面只剩一片钦服赞美之声——杨统制忠不可言，不畏外戚权贵，为国不惜己身，正是我辈楷模。而消息传出去后，平素视他如鹰犬爪牙的李光、马伸等人这几日投来的眼光都复杂了些——虽然台谏该递的皇城司扰民请斩折子不但一份没少，反而上得更急了。</p>
<p>他懂，都是公忠体国的大臣本分。</p>
<p>后世戏文中，那人当是英明神武的官家，金銮殿上的相公御史则个个是命世的忠良，韩岳李张与君王风虎云龙，而他这奸佞必自有人涂白了脸，细细扮起。</p>
<p>戏台之下，大抵无赖子少不得两句笑骂，道学家应不吝几声叹息，叹那杨沂中不肖子孙，辱没了老令公祖宗家名。</p>
<p>他都懂。</p>
<p>他在乎过。</p>
<p>他甚至嫉妒过。</p>
<p>岳飞岳鹏举。起初他有过极荒唐的猜测，但很快就明白自己的猜想当不得真。可那人对那河北庸耕子出奇地信任，落井之后第一个开口问的人便是对方，在鄢陵长社又将身家性命押了出去。杨沂中后来借着精忠报国大纛一事的缘法，仔细观察过岳鹏举，着意亲近这圣眷最隆的将军。而对方也投桃报李，主动谈起配合进剿李成的经历，显然同样有心结交他这个天子近臣。虽说没三两句他便明白此人本质严肃端谨，绝非圆滑善佞之辈，但他仍然有几分莫名的失望与不平。</p>
<p>后来他案上的皇城司汇报越积越高，岳节度治军的名声越传越广，官家对此人的信重越来越深，灭夏后他几乎就要真心服气，然而去年天子巡河后，他听陪侍的刘晏罕见地三两句讲完经历，还捎来一封张俊的亲笔书信，从刘晏为难的神色和老上司信中隐晦的抱怨里拼凑出了真相。接信后第三天，他实在忍不住，再次违逆了他给自己订下的规矩，在绝不该提起政事的夜里劝那人三思，甚至做好了被再度反问‘要做贤臣吗’的准备。</p>
<p>可情理之外又在他隐隐预料之中的是，那人压根没在意，甚至没注意到杨沂中这次提起的内容有什么不同，只当是往日一般的随口闲聊，语气理所应当，谈起岳鹏举和他的军队竟像孩子展示心爱的玩具，言罢又有一丝不好意思，反而问他，他心目中的理想军队应是什么样子。</p>
<p>“令行禁止，所攻必克。”他犹豫了一下，一边唾弃自己利用对那人的了解故意钓对方回应的心机，一边给了个中规中矩到无聊的答案。</p>
<p>不出意外，那人果然笑了。</p>
<p>不是讥嘲，没什么恶意，但确实带着一分非极熟稔这位官家之人注意不到的若有若无傲慢。</p>
<p>在笑他，在笑他们，在笑这个天下。</p>
<p>而这种笑，他认得。</p>
<p>事实上，天子身边的亲近大臣都认得，只是默契地不会向外人提及。甚至某一次这笑容出现时他专门去留心张浚张枢密与吕好问吕公相的表情，果然察觉了他们细微的肢体语言改变。</p>
<p>他收回目光，确认了大家都知情。</p>
<p>而在场的林尚书后来与他对望一眼，那一眼中甚至带了一份同情。</p>
<p>他至今不愿细思那份同情的含义，也不知道这位公认心思缜密、最懂官家心意的前学士猜出了几分那人的来历，清楚了几分他与那人之间的关系。但对方从来不提，偶尔公事交集，也是文官一贯的疏离客气。反正对方上门拜帖里没夹着韩嫣或韩子高的传记，年节时赠礼也只有平常的笔墨书籍，他便可以自欺欺人，佯作不知。</p>
<p>他刚刚收回心思，便听对方笑声停下来了，然后开了口。</p>
<p>“不，正甫。令行禁止，所攻必克是好的，但不够。我心中的军队，出身并无军户平民之别，俱是人民子弟，冻死不拆屋，饿死不掳掠，知晓自己为何而战，挥戈所向为削天下不平，不为一家一姓。由是，解民倒悬，放伐桀纣——”</p>
<p>“而旌纛所至之处，百姓箪食壶浆，以迎王师。”他想象着此军模样，轻声和上了天子最后的结语。</p>
<p>那人又笑起来，这回发自真心，点点头，眼里有光，有追忆，有怀念，有赞许，有希冀，有同道的光——那大概是三十三天之上升平世的光，让他倾身不顾的光——随即转过头，认真地望向他，告诉他岳鹏举和他的岳家军是这个……是离他所愿最近的那支队伍。</p>
<p>杨沂中再次确认了自己永远也不会懂他和岳鹏举。但他不再嫉妒。</p>
<p>因为岳节度永远不会懂赵玖人的那一面。岳太尉能见到知遇的官家，能建天子心中的王师，岳鹏举若再僭越一些，敢称与天子同心同德，同道同志。可那些属于人，属于赵玖的秘密，只有杨沂中能见到。</p>
<p>那人咽下一个词，改口时在他面前懒得遮掩的模样，只有他能见到。</p>
<p>他望着捧药材的班直们已经走到了几步开外，然后朝硕果仅存的最亲信手下淡淡开口：</p>
<p>“以及，给我盯好了宁德宫和……成平宫。”</p>
<p>听闻此言，手下霍然抬头，望过来的眼神难以置信。但杨沂中像上次一样面无表情地盯着他看了数息，那人吞咽了一下，终究没有反驳，叉手行礼后便悄然离去。</p>
<p>他们不清楚他没有冒险的本钱了。他们不知道他赌过一次，已经花掉了自己一辈子与皇宋二百年的运气。</p>
<p>之前的胜负局，天意未曾相负。可杨沂中有自知之明，就算戏文里的主角，史书上的传奇，也不可能受天眷到以天下为注赌第二次还会赢。</p>
<p>当年有个被绑缚在地的赤心队逆贼跪在一边，火光映衬下的面孔混着恐惧与不服，被他问起叛逆缘由时犹自强声自辩，恨称兴亡皆命，赵宋国运已尽——那次他绝不愿信，于是脑中念着国仇家恨四字，放手一搏。</p>
<p>前两日，那被他隐去具体信息只询问方子打探病情的大夫见他脸色实在难看，竟劝起他生死皆数，人力或而有穷。一句话出口，跟着他的班直差点拔刀，他反而笑了一下，背后摆摆手，转身离去——这次他更不肯信，但是心间刺着另外四字，他搏不起。</p>
<p>他加快步伐，追上了捧药的班直，目送那些经炮制的枝茎根叶落入干净的陶锅，清水注入，汤汁滚出细小泡沫与热气，任水汽与药香在漏声中侵入他的鬓发袖领。他一错不错，盯着那服棕黑色汤药从出锅到入罐的每一个步骤，然后取来银匙。</p>
<p>那天他试图压制汤药带来的困意，在半梦半醒间拼拼凑凑，勉力尝试于脑内勾出一个妖邪的原型，可怎么也做不到。</p>
<p>他只见到光，和赵玖含笑的眼睛。</p>
<p>那人傲慢任性，时而不讲道理，脾气与日见长，又信着许多奇奇怪怪的规矩，白日里指使起杨沂中从不客气，黑夜中也不告诉他自己的来历。</p>
<p>“统制，林尚书半个时辰前已经出门了。”</p>
<p>杨沂中再度放下手中的银匙与药罐，眨了眨眼。</p>
<p>“备马，去西府张枢相宅。”</p>
<p>那人独一无二，世间无匹。</p>
