<p>同人8医国——谖兮Hilla</p>
<p>杨沂中望着面前的一大堆药材，眨了眨眼。</p>
<p>为了不让可能的有心人打探清楚宫内用药明细，他亲自挑了两个班直带人将城南药材货栈中与伤寒沾边的药材照着各五十两的分量一网打尽。可当大包小包真正摊在他面前，作为一个连麻黄柴胡都分不清楚，更别提挑出哪堆是羌活哪堆是独活的人，他还是怔了一下才回过神来。而这一怔就让一直狠狠盯着他的潘国丈瞧出了破绽。</p>
<p>“早听人说杨统制忠心不二，旁人难及，老夫今日算是领教了。但统制莫要忘了，你大可在这边慢慢对着医书拣选，官家的病却是耽误不起！”潘国丈眼神中的怒火几乎烧得化成实质——明摆着被当面怀疑人品、侮辱业务水平，却顾忌着皇城司的名声不敢和他撕破脸皮。将心比心，若不提官家二字，杨沂中几乎都要生出几分怜悯。</p>
<p>然而此刻他只是语气平平地开口道：“既然如此，还请潘医官确认，若无药材缺失，等这边御药局博士监督称量好分量，便可送去照方煎药了。”</p>
<p>“——人参、薄荷、茯苓、半夏……倒是齐全。”潘永寿仔细辨认了一圈，哼了一声，从牙缝里勉强挤出了承认。而杨沂中心下稍微一松，挥了挥手，示意身旁等待已久的班直们开始拣药。一时间，从立在他身后的亲信统领到误入现场被勒令不许离开的青衣仆役，一院子大气儿不敢出的人仿佛解除了《西游降魔杂记》里的定身法一样，有人急匆匆发号施令，有人笑谄谄小心应声，招呼清点搬运洒扫，立刻忙碌或假装忙碌了起来。</p>
