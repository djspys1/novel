<p>原济州镇抚使麾下独立领兵将领：张宪、汤怀、李逵、傅选，合计四人；</p>
<p>原西京三衙步帅闾勍麾下将领：牛皋，计一人；</p>
<p>原御营中军独立领兵将领：呼延通、傅庆（后者护送宰执大队而来），合计二人；</p>
<p>原王彦麾下八字军独立领兵将领：孟德、焦文通，合计二人；</p>
<p>原陕州李彦仙部独立领兵将领：翟兴、翟进、邵隆、绍兴、邵云，合计五人……</p>
<p>林林总总，必然有不少人来不及赶来……譬如留守南阳、襄阳的几位中军统制官，东线与济南府对峙的扈成、王贵等人也是如此，而杨沂中和刘晏虽然官职与这些人齐平，却是居高临下，俨然不算其中的……但不管如何，大略算来，改编后的御营诸军中除了后军以外，各部核心主力大略都在，拢共三十六人。</p>
<p>“你们在扶沟是怎么结的义啊？”在几位宰执和大部分文官匪夷所思的目光中，赵官家不慌不忙，问起了专业流程。“可要拜关公，烧香火？”</p>
<p>“好教官家知道，当时并未拜什么神仙，而是歃血为盟。”马皋硬着头皮做答。“十人割掌出血入酒，然后捧酒发誓，最后共饮血酒，便算成了。”</p>
<p>“那就好办了。”赵玖抬手示意。“良臣去，就在台下，亲自搬一坛御酒来……”</p>
<p>不知道为何，韩世忠也开始心慌了，却事到如此，又不敢不去……而须臾之后，酒水搬来，赵官家便站起身来，从身侧杨沂中腰间取来一匕首，方才在座前出言：“都抬起头来！”</p>
<p>三十六员将领一起抬头，却见到那红袍子的赵官家直接以赤手握刀，微微一蹭，然后便当众将数滴血滴入眼前酒坛之中，然后便收起匕首，示意倒酒，看这意思，却是不用下面这些人一起混血饮酒了……而见此一幕，上下文武也全都凛然起来，因为他们意识到，这位官家不是在瞎胡闹，最起码是没有用瞎胡闹的心态来对待这件事情。</p>
<p>俄而片刻，随着官家口谕，七位帅臣挨个倒酒捧碗，给下方三十六将一起奉上血酒，而杨沂中也和刘晏一起，替官家撤下了身前御案，好让官家与这些将领之间再无隔阂。</p>
<p>“诸卿，朕回东京，看眼前凄凉，只觉昔日繁华盛景，尽化为一场春梦。”赵玖早已端坐回位中，却看着尚在渗血的手掌，微微叹气。“彼时宗留守油尽灯枯，却生怕朕将来会有苟且求安之态，临死了还要强着朕立誓兴复两河，却不知，朕那时反而觉得区区一誓不足以明朕之志……两河上百州军，千万子民，血仇如海，哪里几句誓言能表胸中愤愤的呢？但反过来说，那时与眼下情状，除了几句誓言，又何以明心呢？故此，今日十统制擅自结义，闯下大祸，而朕却绝无苛求之态，只要你们聊表抗金救国之志，则万事朕皆可忍！”</p>
<p>言至此处，上下已经鸦雀无声，不少愤愤文臣也都安静下来，而赵玖环顾上下，却是肃然以对：“都起来捧碗吧！”</p>
<p>三十六员统制、统领官一起起身，小心捧碗。</p>
<p>而七位帅臣不敢怠慢，也一起肃立于右侧；诸宰执相顾无言……事到如今，却也只好一并率左侧文臣百官起身肃立。</p>
<p>“朕说一句，你们跟着念一句！”赵玖端坐位中凛然出声。“今日我御营军将三十六人，一并代御营上下二十万军士起誓……”</p>
<p>下方慌乱了一下，一时没有起声。</p>
<p>但也就是这时，杨沂中忽然捏着那柄匕首，引刘晏一起，左右上前半步，然后扬声以对：“今日我御营军将三十六人，一并代御营上下二十万军士起誓！”</p>
<p>“今日我御营军将一并起誓！”三十六将微微一滞，旋即捧碗合力重复跟上。</p>
<p>“饮此血酒，拜天子，结兄弟谊，死生相托，吉凶相救，福祸相依，患难相扶。”赵玖张口便来，显然今日之前便有腹稿。</p>
<p>“饮此血酒，拜天子，结兄弟谊，死生相托，吉凶相救，福祸相依，患难相扶。”随着杨沂中的重复，这一次下方言语不再参差不齐，而是渐渐整洁如一起来。</p>
<p>“指此一生，雪靖康之耻，兴宋灭金，扶民救国，绝无二志！”赵玖继续领誓，眼神却有些冷淡起来，因为他知道下面这些人是他的绝对指望，但也正是这些人，其中绝大多数都只是一群祸乱百姓，争功推诿，习气极坏的**、军贼，以及所谓义军，他的道路既阻且长。</p>
<p>“指此一生，雪靖康之耻，兴宋灭金，扶民救国，绝无二志！”众将也继续发誓，事已至此，原本混乱的心思也各自收起。</p>
<p>“若有临阵违令者；</p>
<p>“若有临阵违令者；</p>
<p>“囿于门户、得失，弃兄弟者；</p>
<p>“囿于门户、得失，弃兄弟者；</p>
<p>“涂乱百姓、牵连无辜者；</p>
<p>“涂乱百姓、牵连无辜者；</p>
<p>“临国事存私者；</p>
<p>“临国事存私者；</p>
<p>“必共讨之，使之生无可恋，死如阶下残尸！”</p>
<p>“必共讨之，使之生无可恋，死如阶下残尸！”说到最后一句，下方诸将，早已经惶恐，有些人是真信这个，有些人则是从这些誓词中察觉到了一些官家的决意。</p>
<p>而左右文武更是凛然，韩张李等人也都面色涨红，浑身警醒……他们没有立誓，但这也绝对是赵官家对他们的警告和约束。</p>
<p>“饮下吧！”赵玖在此稍微停了片刻方才出言。“但须想好，饮下此酒，你们便是一起上过阵、结过义、立过誓的生死同袍了。而朕既然做了你们的指誓之人，便也不会不管你们……自今日后，御营诸将，今日立誓者与不在此处的统制官们，若逢军中不公事，遇地方纠纷事，有私下疑难事，皆可直接写札子飞马至今日领誓人、御前班直统制官杨沂中处，他本就提举皇城司，自会将你们的言语，直接送到朕跟前……”</p>
<p>听到最后一句，韩张李几个大帅一起心中冰凉，吕颐浩、许景衡二人更是齐齐一惊，但此时哪里能出言语？而下方三十六将也自无一人会憨傻到此事上有半分犹豫，却是在官家话音刚落之际，一起仰头满满饮下那根本没有血味，却也不知道是啥味道的一碗血酒。</p>
<p>然后，一起低头俯首，又朝上方亮出碗底。</p>
<p>“砸了吧！”端坐在那里的赵玖终于失笑。“砸了以后，今日咱们便放肆一场，大碗喝酒、大口吃肉，不然哪来的聚义气魄？”</p>
<p>下方一时轰然，但夯土之台，却未免能碎，如李逵、牛皋、翟冲这般精细人物，却干脆又将没碎的酒碗捡起来，藏入袖中，准备带回家当个物件。</p>
<p>俄而片刻，酒宴重开，下方军官，甚至那些低级文官多有释然之态，而几位宰执和几位帅臣，却多悚然起来。</p>
<p>但不管如何，随着官家的御案被抬上来，新鲜的酒肉上桌，气氛很快还是热闹了起来。</p>
<p>见此情形，趁着热闹，一直闷不吭声的小林学士主动探身，隔着座位给自己前一位的御史中丞胡寅倒上了一杯酒。</p>
<p>后者一脸复杂的回过头来，却又见那林学士从容捧杯相对：“今日河阴云淡风轻，将来却道阻且长，但幸得军士同起报国之念，国家显然要得一时安稳，这是好事，胡中丞何不多饮几杯？”</p>
<p>胡寅沉默片刻，方才举杯相对：“诚如林学士所言，国家能得片刻安宁，军士能起振作之态，当然是好事。”</p>
<p>言罢，二人相对一饮而尽，而二人饮罢，胡寅却终究难掩一叹……道阻且长，以今日局面，天下人何不如此？</p>
