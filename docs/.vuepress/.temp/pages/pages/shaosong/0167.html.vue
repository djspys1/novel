<p>汉家河北一百州，遗民南望皆期期。</p>
<p>问君北贼何足道，坐守画地如穷愁。</p>
<p>不共戴天是此仇，生不杀贼死不休。</p>
<p>诸公但能安身计，更无一点英雄气。</p>
<p>遂令多士皆沉醉，绝口不复言时事。</p>
<p>恭惟主上天勇智，皦日平生复仇志。</p>
<p>春色平场千万骑，望裹亭亭龙纛移。</p>
<p>六军拜手呼万岁，报恩便欲无生意。</p>
<p>南风微微天无云，牙领蟠冢愁黄曛。</p>
<p>草莽亦得拜天子，壮士何得愁朱门?可怜泾渭胸中分。</p>
<p>愿起沔阳死诸葛，作我大宋飞将军。</p>
