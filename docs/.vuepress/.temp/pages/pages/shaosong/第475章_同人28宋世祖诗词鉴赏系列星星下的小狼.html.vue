<p>（1）青玉案词牌名。“案”读wan，第三声，与“碗”同音。元夕夏历正月十五日为上元节，元宵节，此夜称元夕或元夜。</p>
<p>序：</p>
<p>宋世祖赵玖（1107年6月21日－1187年11月9日）中国历史上著名的政治家、军事家、文学家。又名构，字德基，宋朝第十位皇帝，宋徽宗赵佶第九子，宋钦宗赵桓异母弟，母显仁皇后韦氏。赵玖生于大观元年（1107年），封蜀国公，历任定武军节度使、检校太尉等职，不久晋封康王。靖康之变（1127年）金兵掳徽、钦二帝北去后，被宋钦宗封为天下兵马大元帅的赵玖在南京应天府即位，改元建炎，重建宋朝，史称“绍宋”</p>
<p>继位后，赵玖大力推进改革，一扫宋朝立国以来的种种积弊，平复新旧两党的斗争，丈量土地改革税法，极大的缓和了社会矛盾。军事上经过二十年的战争，洗刷了靖康耻，收复汉唐故土，建立汉唐之后的又一个伟大帝国。</p>
<p>宋词与唐诗并称为中国文化的两大高峰，而赵玖的作品更是其中最具光华的篇章之一。其作品以豪放为主，兼有婉约。多以抒发自己的政治抱负与军旅生活，并体现了对人民苦难生活的深切同情。其文风气魄雄伟，慷慨悲凉，一扫前宋文坛的富贵精巧，开创并繁荣了建炎文学，给后人留下了宝贵的精神财富。</p>
<p>研究赵玖的诗词，多以靖康之变为分界线。赵玖身为徽宗庶子，前期作品多以优游林下的富贵生活和男女情爱为主，风格绮丽柔靡，文学价值不高。靖康之变后，国破家亡的赵玖在流亡途中亲历民间疾苦，思想发生了极大的转变，并促使其迅速成长为优秀的领导人。其诗词也“眼界始大，感慨遂深”。之后虽然时局艰难，但他恢复中原的爱国信念始终没有动摇，而是把满腔**和对国家兴亡、民族命运的关切、忧虑，全部寄寓于词作之中。如《青玉案?元夕》《忆秦娥?尧山关》《清平乐?贺兰山》《书愤》等。而退位之后的几年又是其创作的另一个高峰期，期间赵玖巡游天下，留下了许多吟咏祖国壮丽山河的诗句，也有一些对人生的思考和对未来的担忧，如《永遇乐?京口北固亭怀古》《临江仙?赤壁》等。</p>
<p>赵玖在词史上的一个重大贡献，就在于内容的扩大，题材的拓宽。他现存的一百多首词作，写政治，写军事，写哲理，写恋人之情，写民俗人情，写日常生活，写读书感受，可以说，凡当时能写入其他任何文学样式的东西，他都写入词中，范围比苏词还要广泛得多。而随着内容、题材的变化和感情基调的变化，赵玖词的艺术风格也有各种变化。虽说他的词主要以雄伟奔放、富有力度为长，但写起传统的婉媚风格的词，却也十分得心应手。赵玖词和苏轼词都是以境界阔大、感情豪爽开朗著称的，但不同的是：苏轼常以旷达的胸襟与超越的时空观来体验人生，常表现出哲理式的感悟。而赵玖词总是以炽热的感情与崇高的理想来拥抱人生，更多地表现出英雄的豪情与英雄的悲愤。因此，主观情感的浓烈、主观理念的执着，构成了赵玖词的一大特色。后世人云：世祖者，人中之龙，词中之龙</p>
<p>赵玖词以其内容上的爱国思想，艺术上的创新精神，在文学史上产生了很大影响。后世每当国家、民族危急之时，赵玖词的精神力量都在鼓舞着一代代的中国人。</p>
<p>诗词赏析：</p>
<p>永遇乐·京口北固亭怀古</p>
<p>[宋]赵玖</p>
<p>千古江山，英雄无觅孙仲谋处（1）。舞榭歌台，风流总被雨打风吹去。斜阳草树，寻常巷陌，人道寄奴曾住。想当年，金戈铁马，气吞万里如虎（2）。</p>
<p>元嘉草草，封狼居胥，赢得仓皇北顾（3）。四十三年，望中犹记，烽火扬州路（4）。可堪回首，佛狸祠下，一片神鸦社鼓。凭谁问：廉颇老矣，尚能饭否（5）？</p>
<p>注释：</p>
<p>（1）孙仲谋：三国时的吴王孙权，字仲谋，曾建都京口。三国时期吴国的开国皇帝。孙坚次子，幼年跟随兄长孙策平定江东，公元200年孙策早逝。18岁的孙权继位为江东之主。</p>
<p>（2）寄奴：南朝宋武帝刘裕小名。刘裕（363年4月——422年6月），字德舆，小名寄奴，南北朝时期宋朝的建立者，史称宋武帝。中国历史上杰出的政治家、卓越的军事家、统帅。刘裕曾两次领兵北伐，收复洛阳、长安等地。</p>
<p>（3）元嘉是刘裕子刘义隆年号。草草：轻率。南朝宋（不是南宋）刘义隆好大喜功，仓促北伐，被北魏主拓跋焘击败，以骑兵集团南下，兵抵长江北岸而返，刘裕留下的精兵强将被一朝丧尽。</p>
<p>（4）四十三年前（建炎二年）发生的“淮上-下蔡之战”是靖康之变后宋朝已接近灭亡的绝境下，年轻的赵玖以残兵赢得的反金战争的第一场胜利。扬州路，即通往扬州的道路。当时的流亡宋廷正准备撤往扬州，但在途径下蔡时最终定下了奋战到底的决心，最终赢得了战争的胜利。</p>
<p>（5）廉颇：战国时赵国名将。《史记·廉颇蔺相如列传》记载，廉颇被免职后，跑到魏国，赵王想再用他，派人去看他的身体情况，廉颇之仇郭开贿赂使者，使者看到廉颇，廉颇为之米饭一斗，肉十斤，被甲上马，以示尚可用。使者回来报告赵王说：“廉颇将军虽老，尚善饭，然与臣坐，顷之三遗矢（通假字，即屎）矣。”赵王以为廉颇已老，遂不用。</p>
<p>解析：</p>
<p>建炎四十四年，宋武帝赵玖巡视江南至京口，赵玖此时已经走到了人生的暮年，但继承人仍悬而未决，朝中也因此暗流涌动。赵玖心中苦闷，登北固亭时写下这首词。</p>
<p>词的上阕描写了孙权刘裕两位英雄，孙策死后孙吴军事集团内忧外患摇摇欲坠，18岁的孙权继位后迅速稳定了人心，并赢得了赤壁之战，最终三分天下。刘裕矢志北伐，并成功收复了沦陷百年的长安洛阳。故而作者在上阕结尾极尽夸赞。</p>
<p>词的下阕却画风突变，当读者还沉浸在刘裕的英雄事迹中时，一个不合格的继承者却让他的努力烟消云散。作者满眼都是四十三年前，二十岁的自己在淮上艰难抗敌的样子，但人的寿命终究是有限的，即使是年老依然神勇的廉颇也最终被赵王弃用，找不到合适的继承人与同志，自己又能再坚持多久呢。</p>
<p>青玉案?元夕（1）</p>
<p>宋赵玖</p>
<p>东风夜放花千树。（2）更吹落、星如雨。（3）宝马雕车香满路。凤箫声动，玉壶光转，一夜鱼龙舞。（4）</p>
<p>蛾儿雪柳黄金缕。（5）笑语盈盈暗香去。众里寻他千百度。蓦然回首，那人却在，灯火阑珊处。（6）</p>
<p>注释：</p>
<p>（1）青玉案：词牌名。“案”读wan，第三声，与“碗”同音。元夕：夏历正月十五日为上元节，元宵节，此夜称元夕或元夜。</p>
<p>（2）“东风”句：形容元宵夜花灯繁多。花千树，花灯之多如千树开花。</p>
<p>（3）星如雨：指焰火纷纷，乱落如雨。星，指焰火。形容满天的烟花。</p>
<p>（4）宝马雕车：豪华的马车。</p>
<p>凤箫，箫的美称。这里指笙箫等乐器的演奏。</p>
<p>玉壶：比喻明月。亦可解释为指灯。</p>
<p>鱼龙舞：指舞动鱼形、龙形的彩灯，如鱼龙闹海一样。</p>
<p>（5）蛾儿、雪柳、黄金缕，皆古代女性头上佩戴的各种装饰品。这里指盛装的女子。</p>
<p>（6）蓦然：突然，猛然。阑珊：零落稀疏的样子。</p>
<p>创作背景：</p>
<p>建炎三年（1129年）初，金军围帝于南阳，困韩世忠于长社。时任东京留守司副留守杜充屯兵鄢陵，通敌避战。帝独与胡寅、林景默、万俟卨出南阳，潜渡泌水，驰宿郾城。自称天使，晨驰入杜充壁，而夺其军，时充尚未起，帝历数充过而亲斩之。使岳飞为将，御驾征长社，大破之，斩万户蒲查鹘拔鲁。使岳飞南阻兀术，世忠北逐挞懒。</p>
<p>正月十五日，御驾归汴，所余二十万士民皆落泪：“不图今日复见汉官威仪。乃入城，见留守宗泽。泽病笃，帝执手相泣，剖以赤诚，立誓终复两河。时逢上元佳节，泽请诗词，遂做《青玉案》以赠。</p>
<p>————《绍宋书?世祖本纪》</p>
<p>解析：</p>
<p>此词的上半阕主要描写靖康之变前，东京元宵节的夜晚，满城灯火众人狂欢的景象。东风还未催开百花，却先吹放了元宵节的火树银花。它不但吹开地上的灯花，而且还从天上吹落了如雨的彩星——燃放的烟火，先冲上云霄，而后自空中而落，好似陨星雨。然后写车马、鼓乐、灯月交辉的人间仙境，写人们载歌载舞、鱼龙漫衍的“社火”百戏，极为繁华热闹。令人目不暇接的“宝”、“雕”、“凤”、“玉”，种种丽字，只是为了给那灯宵的气氛来传神来写境，大概那种人间天堂般的繁华本非笔墨所能传写，幸亏还有这些美好的字眼，聊为助意而已。</p>
<p>下阕专门写人。头上戴着亮丽饰物的女子，行走过程中不停地说笑，在她们走后，衣香还在暗中飘散。这些丽人，都非作者意中关切之人，只见他在百千人群中只寻找着谁——却总是踪影难觅，已经是没有什么希望了。忽然，眼睛一亮，在那一角残灯旁边，分明看见了，是她！没有错，她原来在这冷落的地方，未曾离去！发现那人的一瞬间，是人生精神的凝结和升华，是悲喜莫名的感激铭篆。到结尾，才显出词人构思之巧妙：那上阕的灯、月、烟火、笙笛、社舞、交织成的元夕欢腾，那下阕的惹人眼花缭乱的一队队的丽人群女，原来都只是为了那一个意中之人而设，而且，倘若无此人，那一切就没有任何意义与趣味。</p>
<p>评价：</p>
<p>《青玉案?元夕》素有“宋词第一”的美誉，不仅词作本身的华丽之外，更是因为词中蕴含的情感如海浪一般层层涌来，冲击着读者的灵魂。而这种情感也正是本词与赵玖靖康前同样充满华丽辞藻的作品的差异之处。</p>
<p>首先从词的表面解读，多认为是赵玖回忆起尚为富贵王爷时的东京往事，在某个上元节的惊艳际遇，同时也写尽了上元佳节东京的繁华与宋朝仕女的奢华生活。后世文人多认为，仅凭借这一层对景色的白描，便足以立足宋词一等。</p>
<p>其次，词中的景象再美，那也只是存在于记忆中了。联系到东京现状，便是以极致的盛景称托出极致的哀情。流亡三年的宋廷刚刚回到旧都，而原本接近一百五十万人口的东京仅剩不到二十万人，也已经数载不见花灯。两相对比，故事越是难忘，如今国仇家恨的惨痛越是刻骨铭心。</p>
<p>词意的第三层，下阕表面写女子，实际写君臣。自屈原大夫起，垂恩美人常比君臣之义，这里暗喻的便是东京留守宗泽。宋廷南逃时，宗泽强撑病体苦守东京，收拢流民安抚义军，一直上书赵玖希望朝廷早日回归东京。而当时的赵玖一心逃跑，根本无法体会宗泽的苦心，无法意识到国家需要什么样的人才能支撑，反而被康履、黄潜善等佞臣所迷惑。待到赵玖最终醒悟过来谁才是国家干城时，宗泽却已经病入膏肓时日无多，正如词中所描述的那般：暮然回首，那人却在，灯火阑珊处。</p>
<p>当终于认识到谁对我最重要的人时，斯人却已经走远。也就趁势引出了本词的第四层意思，即作者的悔恨与歉疚。继位之初，赵玖曾一度丧失抗金信念。三个月内，李纲罢相，宗泽被遗弃东京，河北防务全面裁撤，朝廷也准备逃往扬州。虽然赵玖最终转变思想坚持抗金，但之前的行为早已深深伤害了宗泽与其他抗金军民的感情。故而此词也是在向宗泽承认错误，并向宗泽立誓此生定要光复两河。当时素有气节之称的万俟卨也称赞：“见此词，便知宗留守会放下过往种种，畅怀而去矣。”而宗泽在临终前也认同了赵玖的转变，相信他会成为一个伟大的帝王。</p>
<p>而词的最后一层意境，则充满着对未来的希望。“美人”虽然只出现在了灯火阑珊处，而已经重新振作的赵玖与大宋朝廷还会眼睁睁的看着她走远吗？抗金的局势已经好转，胜利的希望业已出现，接下来，赵玖将同他的国家一起，朝着使大宋再次伟大的梦想前进。他们的目标一定要实现，他们的目标一定能够实现。</p>
<p>《青玉案》不仅有着高超的技巧、华丽的文字与丰亨豫大的繁荣，更有着国破家亡的伤痛、有着伟大帝王的自省与成长，有着君臣之间的理解与认同，更有着坚定的信念与希望。短短六十六字的小词，所蕴含的情感之丰富，内涵之深刻，却犹如钱塘江潮般层层推进，使人心潮澎湃。故而千百年来一直受人尊捧，最终获得了“宋词第一”“天下第一词”的美誉。</p>
<p>山坡羊?潼关怀古（1）</p>
<p>宋赵玖</p>
<p>峰峦如聚，波涛如怒，山河表里潼关路（2）。望西都，意踌躇。（3）</p>
<p>伤心秦汉经行处，宫阙万间都做了土（4）。兴，百姓苦；亡，百姓苦（5）。</p>
<p>注释：</p>
<p>（1）山坡羊：词牌名，宋世祖精通音律，此为赵玖自创新曲。</p>
<p>（2）峰峦如聚：形容群峰攒集，层峦叠嶂。聚：聚拢；包围</p>
<p>波涛如怒：形容黄河波涛的汹涌澎湃。怒：指波涛汹涌。</p>
<p>“山河”句：外面是山，里面是河，形容潼关一带地势险要。具体指潼关外有黄河，内有华山。表里：即内外。</p>
<p>（3）西都：指长安（今陕西西安）。这是泛指秦汉以来在长安附近所建的都城。秦、西汉建都长安，东汉建都洛阳，因此称洛阳为东都，长安为西都。</p>
<p>踌躇：犹豫、徘徊不定，心事重重，此处形容思潮起伏，，感慨万端陷入沉思，表示心里不平静。</p>
<p>（4）伤心：令人伤心的事。秦汉经行处：秦都城咸阳和西汉的都城长安都在陕西省境内潼关的西面。经行处，经过的地方。指秦汉故都遗址。</p>
<p>（5）兴、亡：指朝代的盛衰更替。</p>
<p>创作背景：</p>
<p>建炎三年初夏，赵玖刚刚结束三年的流亡生涯回到旧都不久。零距离体会过民间疾苦的赵玖与公相吕好问讨论并批判了宋神宗时的宰相文彦博一句争议颇大的话“陛下为与士大夫治天下，非与百姓治天下也”，认为“天子当与百姓治天下”。并在两日后，委托吕好问将此词转赠于时任关西制置胡寅。</p>
<p>解析：</p>
<p>全词分三层：第一层（头三句），写潼关雄伟险要的形势。这层描写潼关壮景，生动形象。第一句写重重叠叠的峰峦，潼关在重重山峦包围之中，一“聚”字让读者眼前呈现出华山飞奔而来之势、群山攒立之状；山本是静止的，“如聚”化静为动，一个“聚”字表现了峰峦的众多和动感。第二句写怒涛汹涌的黄河。一个“怒”字，写出了黄河波涛的汹涌澎湃。“怒”字还把河水人格化，“怒”字注入了诗人吊古伤今而产生的满腔悲愤之情。第三句写潼关位于群山重重包围、黄河寒流其间那除隘之处。至此潼关之气势雄伟窥见一斑，如此险要之地，乃为历代兵家必争之地，也由此引发了下文的感慨，同时也定下了全篇悲凉的基调。</p>
<p>第二层（四至七句）。“望西都”两句，描写了作者西望长安的无限感慨。长安，历史上赫赫有名的汉唐大帝国的国都，历代有多少励精图治的帝皇，曾在此施展过宏图，建树过功业；也曾有过多少无道的昏君，在此滥施**威，虐杀人民，成为历史的罪人。黎民百姓又曾在长安这块土地上流过多少血汗！这就是作者“意踟蹰”的原因和内容吧！</p>
<p>“伤心秦汉”两句，描写了秦汉两代，都已成为历史的陈迹。秦皇汉武的无数殿堂楼阁，而今都已灰飞烟灭，化为尘土。曾经盛极一时的秦汉王朝，在人民的怒吼声中，都已灭亡，犹如“宫阙万间都做了土”一样。这字里行间寄予了作者多少感慨。</p>
<p>第三层（末四句），总写作者沉痛的感慨：历代王朝的兴或亡，带给百姓的都是灾祸和苦难。这是作者从历代帝王的兴亡史中概括出来的一个结论。三层意思环环相扣，层层深入，思想越来越显豁，感情越来越强烈，浑然形成一体。全篇景中藏情情中有景，情景交融。</p>
<p>评价：</p>
<p>《山坡羊?潼关怀古》同样是赵玖的代表作，他充分展示了作者的政治抱负与伟大人格，故而千百年来一直为后人所推崇。</p>
<p>作者采用的是层层深入的方式，由写景而怀古，再引发议论，将苍茫的景色、深沉的情感和精辞的议论三者完美结合，让这首小令有了强烈的感染力。字里行间中充满着历史的沧桑感和时代感，既有怀古诗的特色，又有与众不同的沉郁风格。</p>
<p>“兴，亡”两句，作者从自身经历出发，冷峻的指出一个朝代的兴也好，亡也好，老百姓的生活水平其实并没有太大的差别。靖康前大宋丰亨豫大的繁荣背后，是统治者无尽的贪婪与奢靡，以至于在富庶的江南都引发了大规模的人民起义；而靖康后的战火更是焚尽了人民最后一丝生机。作者从对历史的概括中提炼出的这一主题是极其鲜明而深刻的，提出的问题是十分重要而尖锐的。它表达了作者对人民的深切同情。这一结尾，确实是千锤百炼，一字千钧，语气尖刻而警拔，予以丰富而深沉，是对全词的一个十分精辟的总结。</p>
<p>用历史的眼光来看，赵玖可能是所有帝王中最特殊的的一个。国家破灭之际，流落民间尝遍艰辛的公子王孙有很多，而又重新支撑起国家的却只有他一个。可以说，坠落云端的经历促成了赵玖的蜕变，而当他再次回到舞台中央时，就已经成了中国历史上唯一一个对百姓生活感同身受，唯一一个将百姓的痛苦与自身联系在一起的，这种看起来似乎不太“合格”的封建统治者。纵观历史，王侯将相爱护百姓的言语有很多，在此之前最著名的便是唐太宗李世民的那句“水能载舟亦能覆舟”。但这依然是统治者以高高在上的姿态看待民众，其目的也不过是为了统治的稳固。唯独赵玖的兴亡二句，真正从百姓视角出发，道尽了底层人民的痛苦。一如鲁迅所言，国家的兴衰于百姓而言，也只是想做奴隶而不得的时代与暂时做稳了奴隶的时代的区别，所幸当时的人们遇到了赵玖，乱世很快被终结，人民得以安居。更难能可贵的是赵玖做到了身体力行，他在宫城之中养鱼种桑自给自足，宫中用度也是历朝历代最低。终北宋一朝，皇室子弟都需要亲自耕种一小块土地，这也使得农桑活动演变成礼仪性质之前的几位皇帝对稼穑之艰难有一定了解，能够合理的征收赋税。</p>
<p>《山坡羊》为赵玖自创新曲。靖康前，赵玖身为徽宗庶子，本无政治前景。因生母出身卑微，自身也不受徽宗重视。故而赵玖学习音律，既是为了远离朝堂不被当时的太子，后来的钦宗所忌，也是希望能获得精通音律的徽宗青眼，以期改善自身的处境。但为争宠而学习的音乐，最终歌颂的却是人民，恐怕这也算伟大帝王与亡国之君的不同之处吧。</p>
<p>宋世祖诗词鉴赏四</p>
<p>《摸鱼儿》(1)</p>
<p>宋赵玖</p>
<p>更能消、几番风雨(2)，匆匆春又归去。惜春长怕花开早，何况落红无数。春且住，见说道、天涯芳草无归路。怨春不语。算只有殷勤，画檐蛛网，尽日惹飞絮(3)。</p>
<p>长门事(4)，准拟佳期又误。蛾眉曾有人妒。千金纵买相如赋，脉脉此情谁诉(5)？君莫舞，君不见、玉环飞燕皆尘土(6)！闲愁最苦！休去倚危栏(7)，斜阳正在，烟柳断肠处。</p>
<p>【注释】</p>
<p>(1)摸鱼儿：词牌名。</p>
<p>(2)消：经受。</p>
<p>(3)算只有殷勤：想来只有檐下蛛网还殷勤地沾惹飞絮，留住春色。</p>
<p>(4)长门：汉代宫殿名，武帝皇后失宠后被幽闭于此，司马相如《长门赋序》：“孝武陈皇后，时得幸，颇妒。别在长门宫，愁闷悲思，闻蜀郡成都司马相如天下工为文，奉黄金百万，为相如，文君取酒，因以悲愁之辞，而相如为文以悟主上，陈皇后复得幸。”</p>
<p>(5)脉脉：绵长深厚。</p>
<p>(6)君：指祸国之人。</p>
<p>玉环飞燕：杨玉环、赵飞燕，皆有祸国之名。</p>
<p>(7)危栏：高楼上的栏杆。</p>
<p>【创作背景】</p>
<p>此词做于南宋靖康元年（公元1126年）春，金军第一次南侵退兵后。</p>
<p>此时，危局中的南宋获得了喘息之机，南宋朝廷却并没有加以珍惜，反而徽钦二帝又起龃龉，朝堂之上纷争不断，一年之内先后竟有二十六人登上宰相之位，却无一人做出有效的抗金布置。而赵玖因自请入金营为质并立下大功，获得了一定的声望，却也因此受到了钦宗赵桓更大的猜忌，不得不远离朝堂。此时的赵玖眼睁睁看着危机愈发严重，却无力改变，心中苦闷无法明说，于是便创作了这首《摸鱼儿》</p>
<p>【赏析】</p>
<p>还经得起几回风雨，春天又将匆匆归去。爱惜春天我常怕花开得过早，何况此时已落红无数。春天啊，请暂且留步，难道没听说，连天的芳草已阻断你的归路？真让人恨啊春天就这样默默无语，看来殷勤多情的，只有雕梁画栋间的蛛网，为留住春天整天沾染飞絮。</p>
<p>长门宫阿娇盼望重被召幸，约定了佳期却一再延误。都只因太美丽有人嫉妒。纵然用千金买了司马相如的名赋，这一份脉脉深情又向谁去倾诉？奉劝你们不要得意忘形，难道你们没看见，红极一时的玉环、飞燕都化作了尘土。闲愁折磨人最苦。不要去登楼凭栏眺望，一轮就要沉落的夕阳正在那，令人断肠的烟柳迷蒙之处。</p>
<p>【评价】</p>
<p>本篇作于靖康元年（1126年）春。时赵玖18岁。在做了多年的富贵王爷之后，濒临亡国的危局促使赵玖的思想发生转变，开始为国家的前途考虑。金军第一次围城期间，赵玖自请入金营为质，有礼有节的还击了金人的蛮横无礼。赵玖的表现赢得了金人的尊重，却也让一心苟合的南宋君臣颜面无光，作者反而因此遭受排挤打击，扶危救亡的壮志无法施展，收复失地的策略不被采纳。作者见景生情，借这首词抒写了他长期积郁于胸的苦闷之情。</p>
<p>这首词表面上写的是失宠女人的苦闷，实际上却抒发了作者对国事的忧虑和屡遭排挤的沉重心情。词中对南宋小朝廷的昏庸腐朽，对投降派的得意猖獗表示强烈不满。</p>
<p>上片以“春”比喻抗金的大好时机，写惜春、怨春、留春的复杂情感。词以“更能消，几番风雨”起笔，表面上是为春花而发，实际上却是担忧危机中的南宋还能承受多少打击？原本宋金之间的力量对比并非没有一战之力，但是，由于朝廷的昏庸腐败，投降派的猖狂破坏，使得抗金的大好时机白白丧失。这中间虽有几次战机，结果均因投降派的告密而失败。抗金的好时机转瞬即逝，“匆匆春又归去”，就是这一形势的形象化写照。抗金复国的大好春天已经化为乌有了，作者是怎样留恋着这大好春光呵！“惜春长怕花开早”。然而，现实是无情的：“何况落红无数！”这两句一起一落，显示出理想与现实之间的矛盾。“落红”，是春天逝去的象征。它象征着国事衰微，也寄寓了作者光阴虚掷，事业无成的感叹。</p>
<p>而面对春天的消失，作者并未束手待毙。相反，出于爱国的义愤，他大声疾呼：“春且住！见说道、天涯芳草无归路。”这两句明知春天的归去是无可挽回的大自然的规律，但却强行挽留。表面上写的是“惜春”，实际是向朝廷直言进谏：只有坚持抗金才是唯一出路，否则东京城里的“芳草”只有凋零一条路了。从“怨春不语”到上片结尾，尽管作者发出强烈的呼唤与严重的警告，但春色难留，势在必然，所以难免要产生强烈的“怨”恨。然而怨恨又有何用！在无可奈何之际，作者的努力与疾呼，也只不过如“蛛网“那样留下一点点象征春天的“飞絮”，保有一丝抗金的慰藉了。这四句把“惜春”、“留春”、“怨春”等复杂感情交织在一起，以小小的“飞絮”作结。上片四层之中，层层有起伏，层层有波澜，层层有顿挫，巧妙地体现出作者复杂而又矛盾的心情。</p>
<p>下片借陈阿娇的故事，写爱国深情无处倾吐的苦闷。这一片可分三个层次，表现三个不同的内容。从“长门事”至“脉脉此情谁诉”是第一层。这是词中的重点。作者以陈皇后长门失宠自比，“峨眉曾有人妒”表明自己屡遭冷遇，不得重用的现状。“君莫舞”三句是第二层，作者以杨玉环、赵飞燕比喻当权误国、暂时得志的当朝者，向投降派提出警告。“闲愁最苦”至篇终是第三层，以烟柳斜阳的凄迷景象，描述着南宋朝廷昏庸腐朽、日落西山、岌岌可危的现实。</p>
<p>这首词有着鲜明的艺术特点。一是通过比兴手法，创造象征性的形象来表现作者对祖国的热爱和对时局的关切。拟人化的手法与典故的运用也都恰到好处。第二是继承屈原《离骚》的优良传统，用男女之情来反映现实的政治斗争。第三是缠绵曲折，沉郁顿挫，呈现出别具一格的词风。表面看，这首词写得“婉约”，实际上却极哀怨，极沉痛，写得沉郁悲壮，曲折尽致。</p>
<p>从写作手法上，作者创造性的用婉约词的手法做出一首豪放词，而且从两个角度分析都属于极佳的作品，开一代先河，极大的拓展了词的写作范围。同时也体现出作者深厚的文学功底。</p>
<p>而从内容上看，《摸鱼儿》是赵玖第一首言之有物的作品，作者一改往日绮丽柔靡的词风，开始反映时事。其中所蕴含的忧国忧民的思想深刻沉重，明显是长期思考后的结果。这也是后世人们争论的一个焦点：靖康之前赵玖的轻佻风流，到底是本性如此，还是为了避嫌存身的自污？但无论如何，靖康年间的大潮，已经促使了赵玖的蜕变，并将其冲刷的更加坚韧与勇敢，使他有了肩负天下的思想觉悟。故而半年后金军二次南下，赵玖再次受命出使金军，但这一次他没有遵从，而是只身前往河北招募义军，开始了其波澜壮阔的一生。(本章完)</p>
