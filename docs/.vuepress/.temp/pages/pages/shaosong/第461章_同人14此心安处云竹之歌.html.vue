<p>张府之人到来时，曲端正于府中特意开辟出来的演武场边抱臂欣赏夏侯远托举石锁，意兴颇隆，夏侯远天生神力，此刻**了上身演练，两个一百斤左右的大石锁竟被他舞的虎虎生风，周围逐渐聚集起来的老兵们也俱都连声喝彩。有看门老仆把张府下人引过来，待来人恭恭敬敬的说明原委，曲端接过请柬，不过略一沉思，便随意摆手:“还请回复张枢相，下官届时必到。”嘴里说着眼中却不离演武场，到了极精彩处也是不觉脱口而出:“好！”而张府下人早已习惯这位御营骑军都统的做派，也不以为意，干脆利落的稽首告退。</p>
<p>曲端看了一会，忽又想起岳台诗会当晚，因有不少军中同僚找夏侯远敬酒，夏侯也来者不拒，到最后却是大醉。曲端念着好歹梯己人一场，亲自搀扶了人去军营中休息，等他皱着眉头给自家小醉崽子卸甲时，不妨夏侯远却突然睁开眼睛直视着他，灯光下目光清耀如中天月色，“孤灯不明思欲绝，卷帷望月空长叹”，这两句月字诗被夏侯远一字一顿的道出，声如金铁。</p>
<p>“美人如花隔云端”，曲端不停顿的接下去，忍不住伸手弹了弹夏侯远的额头:“这是看上了哪家高门大户家的小娘子，如此相思煎熬？”夏侯远却再不理他，闭上眼睛翻身把自个儿埋在被子里似是睡去。</p>
<p>回想到此处，曲端摩挲下巴沉思，夏侯远如今也是年过而立，竟还未有家室，未免太不像话。瞥了一眼演武场内放下了石锁又开始练起狼牙棒的夏侯，曲端心中暗自叹息。其实夏侯长得颇有说书人口中“平平无奇丁鹏少侠”的风范，每次打马出街人群里总有不少荷包往他身上扔，也不知夏侯远到底眼光高绝到看上了哪家女郎，成家之事一再拖延到如今。中秋节后曲端也曾再三追问，夏侯却只是不说，逼问急了扔下“神武门下，流水修竹”八个没头没脑的字转身就溜。曲端自忖神武门附近多为文官雅士，他于士林之中名声却很是不好，索性托了小林尚书打听，今晚张枢相夜宴之机来的倒是正好。</p>
<p>至夜，众人齐聚，曲端惊讶的发现林景默不仅把自家如今极为亲密的侄子梅栎带了过来，竟还带了一众家伎，说是有夜有酒岂能无歌，索性他们都是从父辈时就养在家里的，性命前途都系在主家手里，口风极严却是不妨碍的。张浚不愿拂了这位“林九章”的面子，只好设了屏风把一众乐伶隔绝在外堂，任由他们自去唱官家新作《白蛇传》的调子，自家在主座坐定后便难得主动开口提及那则市井流言。</p>
<p>林景默、曲端乃至于梅栎都面色不动，曲端甚至心中几乎要发笑了:就这？就这？就这点小事也值得你张德远大张旗鼓的把人喊来搞团团伙伙？林景默摇头不止:“当今官家非是一般，只怕倒还高兴二位相公决而能和、斗而不破，如此才能上下一心协力北伐。”吕祉却忽激动起来，以手拈须自得而笑:“不然，东西两府，本来就该有个争斗的样子，须知千年以降，朝堂权衡之术乃是正理。”</p>
<p>曲端嗤笑一声，只觉得这位吕侍郎说话很没道理，其人心思既歪，重点怕是偏了。果然接着就听张浚期期艾艾的说:“正如深穆所言，官家今日召我同元镇兄入宫，却是极力赞赏我二人各司其职携手同舟来着。”吕祉叹了口气，竟显得颇为遗憾，倒是刘子羽适才一直担忧的看向张浚，此时闻言才放松下来，击案赞叹:“官家英明！”</p>
<p>曲端暗自翻了个白眼，只觉得这位张相公不愧是最大的幸进小人，他现在算是明白张德远今晚把人喊过来的目的了，无非是“相忍为国一心北伐”那套慷慨陈词，一时也懒得搭腔，慢条斯理的去舀案上那一盅酒酿圆子——且说张枢相家不愧是蜀中名门，府上的厨子端得好手艺，同时不忘拿眼去觑林景默。这位公认有内秀的户部尚书方才给张浚送乐伶的做派也太过强硬不通人情了些，其中必有缘故。感受到曲端探究的目光，林景默侧头冲曲端眨了下眼，曲端一愣，复又失笑，再度低头去舀那酒酿圆子，毕竟，唯有祖安与美食不可辜负。至于夏侯那个小崽子的私事，日后再说！</p>
<p>九二，惕号，莫夜有戎，勿恤。</p>
<p>这会刘子羽正跟张浚谈论的入巷:“东南那位吕相公颇有手腕，有他在彼处压制，应当不至于让江南道学与白马之际被黜官员左右勾连上，此事应该另有幕后之人。”复又瞪了一眼曲端，口中继续分说不停:“两位使相，宇文相公那里着实软弱了些，西军大小军头，若有敢抗命的，要我说还是学吕颐浩吕相公都砍了清净！若是当初使吕相公安抚关西，按着曲都统跋扈飞扬的性子，只恐半世智勇功名，早随那北邙新垅埋没于石麟荒草里了。便是我去怕也是一样的。”</p>
<p>闻言林景默皱眉不止，伸手在席下轻按了一下略显惊慌的梅栎世侄，姿态优雅闲适的起身离席转至屏风外，影影绰绰间似是跟乐伎们吩咐了些什么。吕祉眯了眯眼，拈须不语冷眼旁观。张德远心中一突:因彼时尧山龃龉，刘子羽与曲端颇不对付，每每暗中针对，但今晚也不知彦修到底是怎么了，说话着实失了分寸，竟把那点私下龃龉摆在了明面上，甚是不妥。</p>
<p>曲端扔下汤匙，冷笑一声，本欲张口嘲讽，却不妨侧耳听到屏风外婉转清扬的白蛇传唱词不知何时转为沉郁苍凉:</p>
<p>“昨日沮授军中死，今日田丰狱内亡。”</p>
<p>曲端蓦然一怔，再度去看林景默，看到其人轻轻颔首，开口时却难得心平气和:“河山不改，百姓几迁，若待关西沦丧，你我之平生功业，后人记得与不记得，哪还有什么意义？”屏风外唱词不停，惹得吕祉也拧眉倾听起来。</p>
<p>“若使许攸谋见用，山河安的属曹家。”</p>
<p>张浚只觉得今晚的聚会就是个错误，揉了揉跳动不已的额头，紧急拿了些别的话儿去牵扯刘子羽的注意力。屏风外琵琶声愈发转急，突然一声划弦如裂帛——</p>
<p>“河北栋梁皆折断，本初焉不丧家邦！”</p>
<p>西风喧竹，窗外秋雨霏霏而至。</p>
<p>曲端本就觉不耐烦，这会借口秋雨先行离去，见此，林景默也冲张浚告罪后携梅栎随之而去。出了张府家门，曲端随即正色谢过林景默席中援手，这位小林尚书也只摇头轻笑:“懋修曾向我提及他自入朝起，每每觉这位兵部尚书杀心颇高，我这一番动作，只盼彦修多少能改改他那性子。”曲端复又上下打量了一番梅栎，转身也笑着对林景默道:“此汝家荀令。”说罢，也不打伞，直接在秋雨里打马而去。</p>
<p>刘子羽却在张浚这里多盘旋了一阵，待他最后一个离府时秋雨已密，细雨如丝如线从道旁飞檐上落下，街道迷离一片延伸至远处更是如烟似雾，刘子羽挥手退开仆从，也不乘车马，撑开和雨色同青的油纸伞，轻拢了砖红色的袍服，缓缓行步在几无一人的街衢上，片刻后突然失笑起来:“那唱词说的是我啊！”纸伞轻移，伞下人抬头看了一眼遮天雨幕，摇了摇头，复又轻甩左手衣袖负在身后，动作间一缕若有若无的玫瑰香气融入这雨中，莫名教人联想到“无力红妆卧晚枝”。</p>
<p>“停云霭霭，时雨濛濛……安得促席，说彼平生。”吟声越来越低，终于随着那道挺直如剑的身影一道消失在长街尽头。</p>
<p>九三：壮于頑，有凶；君子夬夬独行，遇雨若濡，有愠，无咎。</p>
<p>数日后，本已平复的街头巷议野火复燃，不过内容却是换了，乃是谈论曲端身为十大节度，一军实权都统，跟西府枢相张浚及兵部尚书刘子羽私下过往甚密，有文武勾结之嫌。夏侯远难得失态，焦虑地在书房走来走去，右手作拳颇为愤恨的砸在左手掌心，“这群御史怎地连无知百姓嘴里的荒唐之论也不放过！节度，他们就是跟你过不去！”曲端端坐在书案后，嗤笑一声，手上不停擦拭着一把宝刀，夏侯远眼尖认出那刀还是旧日在关西时打的。“明珠薏苡，说到底只在君王一念。夏侯你这是关心则乱了，当今官家襟怀之宽广，虽古圣君弗如。你不必多虑，且看！”</p>
<p>果然疏至御案，上皆没之，此事遂平。</p>
<p>九五:苋陆夬夬，中行无咎。</p>
<p>秋去冬至春来，三月三，上巳佳节。官家特许休沐一天，却又特意下旨招了杨沂中内宫伴驾。</p>
<p>这天曲端却没出门，而是在书房里招待了一个难得的客人，却正是户部尚书林景默，二人谈论许久，直待一壶茶都添没了，林景默才起身告辞。曲端恍恍惚惚地把人送出门，再次回书房，想起方才谈话之时小林尚书特意指出的本朝初那位“严明御下，尤傲狠”的王嗣宗，默然良久。而林景默临别时那句“曲都统怕是寻错方向了，只怕醉翁之意不在酒，曲都统也是能文的，当知夏侯校尉用典。”更是让曲端惊心动魄。</p>
<p>叹了一口气，曲端重重地敲上桌案上那叠书信，眉头皱的更紧了。信中的资料相当详尽而完备，神武门附近多是文官及僧寺道观，而其中并未听说哪户“绿竹流水人家”里有适龄女眷。转头看了看从一开始就杵在旁边装木头人的夏侯远，再次叹了口气。</p>
<p>满室静寂，春柳春花掩重门。</p>
<p>还是二张的到来打破了这份弥漫的尴尬，大概是因着节日，二张今日也换了文官打扮。张中孚略一瞥案牍上的文字，心中有几分明悟，看了一眼夏侯木头人，摇了摇头笑着解围:“节度何不趁此佳节跟我兄弟二人出门耍耍，莫要辜负好春色，夏侯之事，日后再说！”曲端正因夏侯意乱，本想回绝，忽思杨柳风轻，红杏枝头，心中一动，话到嘴边转而同意。只临出门时又咬牙让夏侯远也跟着来:“自个儿去马厩挑匹好马，歇歇那可怜的骡子吧！”</p>
<p>说话之时，似有一只狸猫从树丛花木里跳跃而过。</p>
<p>四人骑马沿汴河缓缓而行，只见风露含花气，春波漾日晖，道左逢游女，歌管传新音，歌曰:“随意杯盘虽草草，酒美梅酸，恰称人怀抱，醉里插花花莫笑，可怜春似人将老。”四人不觉驻马倾听，一曲歌毕，曲端击掌赞叹:“易安居士绝妙好词，如此胸襟，难怪官家之作常借易安居士名号。”谈笑之时，曲端也没忘一路观察自家亲卫对汴河如云佳人的态度，但每每转头之时总与夏侯远诚恳的目光对上，越看越觉憋气，终是忍不住轻声出言喝问:“你蹉跎至此，年过而立膝下也无一男半女，若是那人不应你，日后将奈何？”夏侯却难得大胆的折了一只花递至曲端面前，笑嘻嘻道:“这花开的却好，节度要不要也簪上？”</p>
<p>曲端定睛看了一眼那枝“照眼还若锦绣堆”的丁香花，心头一梗，手忙脚乱的拒绝夏侯远为自己簪花，二张只做不见，留下夏侯远自个儿在一旁委委屈屈:明明在关西都是我给节度簪花的，怎么到了京城节度就越发拘泥了！</p>
<p>与此同时，汴河一座酒楼里，昔日太学三人组竟也是趁此假日小聚一番，也是遥遥听到了这首新词，也各都赞叹:“难怪官家宁可提易安居士也不提二妃。”张浚摇头不止:“此官家私事，我等还是不要再提了。”赵鼎亲自替他酙了一杯，殷殷问道:“去年深秋那场街论风波，虽是虚惊一场，但德远实不宜在今日大张旗鼓请我和胡明仲宴聚的。”</p>
<p>张浚端起酒杯轻呷一口，眉开眼笑道:“区区小技上不得台面，我只盼元镇兄从此不要与我生分了才好。”闻言，胡寅放下筷子，刚想说话，却被赵鼎抢先一步:“咱们当初骤逢国乱，时局艰难里定下生死情分，更是得遇明主，一路扶持至今，而今我为首相，你为枢相，自当共镇庙堂，纵然是各有羽翼，也不妨共论风月。德远说的是，是我拘泥了。”胡寅欲待插嘴，那厢张浚已然握上赵鼎的双手:“今日上巳佳节，元镇兄快不要提国事军政了，咱们只论私下情谊。”赵鼎注目了他一会，微微而笑，心中惬意。</p>
<p>胡寅接连被堵了话头，左看右看，索性拂袖而起:“愚弟家中尚有文书，就不在此碍两位兄长的眼了，告辞！”说罢也不管赵张二人的起身挽留，毅然决然地转身下楼，呵呵，今天的场子我胡明仲就不该来！不如归家，报效大宋报效官家才能快乐。雅厢里，赵张面面相觑略觉惭愧，一只狸猫适时从从栏杆跳至窗格上，姿态闲适，只一双眼睛却似通人性般闪过看热闹的光芒。</p>
<p>夜幕到来时，汴河渐次亮起千家灯火，与初上的月色在粼粼水波里相映生辉。如此良夜，就连刘子翚都出门耍子，刘子羽却在府中喝酒，放眼望去，也只有素怀大志的吕祉陪着他。“事去千年犹恨速，愁来一日即为长。”酒到深处，刘子羽掷壶而歌。吕祉连连摇头，却也不去管他，只慢慢啜着杯中玉液，出神地望向中庭乌桕上鸦巢，“月明星稀，乌鹊南飞，绕树三匝，何枝可依？”手中核桃转的愈发急了。</p>
<p>一只狸猫不知道从哪里跳上树枝，惊起鸦声一片，随即这道灵活如电的身影跃入远处深沉夜色，直往内宫而去。不多时，在宫门口伫立了半天的杨沂中轻柔的接住了这只狸猫，小心且欢愉的抱着它的样子仿佛是抱住了一座江山。</p>
<p>千年城郭千秋月，几人青史几荒丘。</p>
<p>月到中宵，曲端府上却还是热闹非凡，府上老兵们起哄着架起了烧烤摊子，闹着要继续快活。喧闹声里，曲端翻来覆去的捻着夏侯远簪花被拒后不依不饶给铁象身上别上的满枝红杏，忽而回身问夏侯远:“你到现在还不与我说实话吗？”夏侯远手一抖，随即稳住心神，不闪不避的直视自家节度:“自从一见桃花后，直到如今更不疑。”曲端微笑着的点了点头，却又仰天轻叹。</p>
<p>身旁，几株桃树在月光下灼灼生华，千点飞花与杨条柳枝纠缠片刻后悠然旋下。良夜多情人应惜。</p>
