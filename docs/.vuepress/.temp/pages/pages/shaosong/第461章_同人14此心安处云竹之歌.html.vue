<p>推敲之间须臾诗成，此时张浚手里的茶尚未喝完一半。张浚忙放下茶盏，看向赵鼎，郁闷出声:“元镇兄诗文虽长于我，却也不能说颇擅此道，如何这么快就写好了？”赵鼎咳了一声，抬头看着书斋顶梁，回道:“既然是代笔，自是要在仿照德远你的风格上更上一层，这却不难。”</p>
<p>张浚深吸一口茶香桂气，算了，算了，他还是看看元镇兄写了些什么吧，至于此间口角便宜，他早晚能在朝堂之上讨回来。见修长而白皙的手指接过那一方素纸，赵鼎心中一动，低声调笑道:“画眉深浅入时无？”张浚闻弦歌而知雅意，也是回以一笑:“元镇兄可真是自知明艳更沉吟啊。”</p>
<p>“那德远誊抄时可千万记得把字写齐整一些，如今官家不是少林寺那位道君皇帝，不懂你行笔中那些‘写意’之态。”</p>
<p>叮当数声。</p>
<p>想来是某位被李宪台吐槽为“花瓶”的西府枢相在做些残害“同类”的轻佻之举吧。</p>
<p>自古功名亦苦辛，行藏终欲付何人。</p>
<p>当时黮暗犹承误，末俗纷纭更乱真。</p>
<p>街头巷议，尤为愚浅；流言蜚语，最是杀人。</p>
<p>中秋节又旬日后，一则流言遂在汴京市井中逐渐传开来，乃是说东西二府相公于台面上故作不合，实则私下往来密切，联手排斥异己阻断朝堂隔绝内外，甚至还有人信誓旦旦的说曾看到中秋前夕张枢相夤夜密会赵首相，且张枢相从赵府出来后甚至还春风满面，说得有鼻子有眼的，给这则流言增添了不少真实色彩。</p>
