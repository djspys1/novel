<p>然而这不是那位喜爱青词的皇帝还隔了三百年嘛，如今还是要作诗的呀。而今之计，也只能学刘子羽那般找代笔了。</p>
<p>不过自家一党里，林景默与吕祉也都是不擅长诗词这等小技的，虞允文尚在外省，刘锜更是从岳台回去后就收拾包袱去看他那个尚在黄河上当舵手顺便客串一把凿冰人的哥哥了。曲端倒是能文能武也在京师，然而就曲大那秉性——他张德远不要面子的吗！</p>
<p>至于那位万事无能，偏开创了江西词派的吕本中，正如曲端轻易不愿意面对李世辅一般，张浚也轻易不愿意去麻烦这位中舍人，毕竟他爹吕公相……还是很有声望的。</p>
<p>没奈何，只能再去麻烦元镇兄了。</p>
<p>至夜，三星在天，张浚也不带随从，孤身一人理直气壮就往赵鼎府上去了。也难得某位东府相公没有加班处理政事公文，而是在修剪书斋外的数盆花草。正当时令，建兰开紫，丛菊绽黄，两边的几株金桂也吐出玉颗珊珊，天香缭绕周身满袖。倒是墙角的梅花与腊梅尚未开花，劲干横斜，被月光映照在窗纸上，交辉成趣。</p>
<p>见张浚到来，赵鼎慢条斯理地放下挽起来的衣袖，行动晏晏间将张浚让进了书房。清气徐徐，馥郁的桂香隔了门窗递送过来后也觉淡泊了些许。二人坐定后，随即有侍者送上茶来后又惯例掩门退下。见此，张浚从怀袖中取出一方檀香木小盒来，开眉笑眼地递给赵鼎。</p>
<p>打开一看，既不是之前张德远曾送过的新合香也不是浣花笺或庭圭松烟，只不过数枚莲子罢了。赵鼎略一沉思，轻轻一笑:“莲子已成荷叶老，德远这是因汾儿而起幽风伐柯之意了？”他本就长得清瘦文雅，这一笑起来两颊的酒窝更加明显，衬着身上月白的布衣常服，一派“风宜清夜露宜秋”的好风韵。</p>
<p>张浚行云流水地探身拽住他的衣袖问:“这莲子可当得起元镇兄的润笔之资了？”赵鼎略一思索，便知张浚乃是为了节日颂圣诗而来，也是辛苦忍笑:“几颗莲子怕是不够，还得德远肯效李太白旧事为我研墨铺纸才好。”张浚闻言一挑眉，直接凑到书案前去取了砚台墨锭，抬眼而笑:“就这事，有何不可。”</p>
