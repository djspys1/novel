<p>第355章任务</p>
<p>“狗屁的罪不至死！”</p>
<p>炎炎夏日，杏冈之上，赵官家的怒气哪怕是隔着几颗老杏树的距离也能被清晰感触到，这不免让第一次入职班直的赤心队侍卫们大汗淋漓，并且紧张不安。</p>
