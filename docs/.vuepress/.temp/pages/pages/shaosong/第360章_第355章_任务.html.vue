<p>唯独，虽说是屈服于了赵官家，却又如何能轻易想到一个‘合法’杀掉勾龙如渊这种小人的法子呢？</p>
<p>故此，当日回去，压力最大的四位相公一筹莫展，偏偏又不好将此事与他人分说，好不容易熬到下值，各自回到家中，却又两两相聚，同时匆匆去请些要害人物一起商量。</p>
<p>其中，都省首相赵相公带着副相刘相公找的是吏部尚书陈公辅、礼部尚书翟汝文、开封府尹阎孝忠，外加工部尚书、这次的当事人胡寅本人。而另一头，枢密使张相公带着副使陈相公则找的是户部尚书林景默、兵部尚书刘子羽，以及他的‘智囊’吏部侍郎吕祉，外加一个骑军都统曲端……东西二府的首脑都没敢扩大化，也都没敢去找李光、马伸这种直性子。</p>
<p>邀请既然发出去，暂不说张府上聚会都已经成了惯例，另一边，赵鼎身为首相，素来讲究一个君子不党，此时难得作此行径，陈、翟、阎、胡等人倒是都晓得事情有异，却是不敢怠慢，纷纷抵达。</p>
<p>而待赵鼎领着几人在自家后院凉棚下团团而坐，并将此事小心说出来以后，却又引得几位大员各自愕然。</p>
<p>愕然之后，便是长久的沉默——大家不免要去看当事人胡寅的脸色。</p>
<p>孰料，胡明仲一开始虽然明显带了怒气，但不知为何，很快却又平静了下来，只是端坐不动，沉默不语。</p>
<p>过了好一阵子，眼见着胡寅没有开口的意思，众人将目光从他身上收回，稍作思索，乃是开封府尹阎孝忠率先打破了沉默：“如此说来，官家杀意已定，事情不可能回转了？”</p>
<p>“是。”与阎孝忠理论上算是一党的刘汲蹙眉以对，稍作强调。“但有万一可能，我等今日在延福宫便都劝下来了，但根本劝不下来……而若真到了出中旨强杀的份上，杨沂中、刘晏虽也曾苦劝，怕还是会即刻执行的。”</p>
<p>“那便是要顺着官家的，寻个妥当法子，使此人去死的意思了？”礼部尚书翟汝文插嘴相对。</p>
<p>“正是此意。”赵鼎也点了头。</p>
<p>“能不能想办法隐诛？”翟汝文追问不及。“去明告这厮官家决意，让他不要牵累……”</p>
<p>“不行！”不等翟汝文说完，阎孝忠便再度开口打断了他。“依着我看，非止是不要隐诛，还要明正典刑，最好是能将此人罪行公布天下，使天下人心理都明白他到底是因为什么死的才对……这才是官家本意！”</p>
<p>“不错。”赵鼎叹了口气。“便是我此时细细想来，既然此人必死无疑，那若不能杀一儆百，反而只是白死……不瞒诸位，我此时隐隐觉得，宁可让此人为官家强杀，也胜过隐诛，或者推到其他罪责上！”</p>
<p>“若是这般讲，此事岂不是无解？”翟汝文闻言稍稍蹙眉。“莫非真要坐视官家强杀一秘阁重臣？须知道，勾龙此举，固然可耻至极，却也极为狡猾……泉州番寺的事情不提，便是此番寻机弹劾胡尚书的事情，也最多说他道德败坏、小人嘴脸，却称不上是违背法度的。”</p>
<p>“所以，还是要想个法子，让他栽进去才行，而且最好是能趁机将他作为暴露出来……”刘汲再度强调了一遍上级要求。</p>
<p>“恕下官直言，这事并不必轮到赵相公和刘相公来想法应对官家。”但也就是此时，一直没吭声的吏部尚书陈公辅主动出言，而且言语惊人。“两位相公身为都省相公，不该盯着一个小人的死法犯难……官家那是发怒了，怒火攻心，两位相公也怒到那份上吗？”</p>
<p>“陈尚书这是什么意思？”赵刘二相齐齐心动，却还是在对视一眼后，由赵鼎主动出声询问。</p>
<p>便是同样沉默不语的胡寅，此时都与阎孝忠、翟汝文一起盯住了陈公辅。</p>
<p>“下官的意思是，勾龙如渊这个小人的事情，张相公那边更着急！”陈公辅不慌不忙，正色以对。“此人是张相公的乡人，此番进入秘阁大员之列也是张相公一力举荐的，所以如何处置勾龙如渊，如何让他自曝其非，本该张相公那边去想才对……何况，依着下官看，张相公那边，自有林尚书这般内秀、吕侍郎这般钻营之人，若真有法子，也必然脱不出他们手掌，两位相公又何必为那边闲操心呢？”</p>
<p>众人面面相觑，居然无法反驳，随即便有些放松起来。</p>
<p>而赵鼎稍作思量，却是觉得陈公辅不止此意，却又当即反问：“那敢问陈尚书，官家终究有此雷霆之怒，且施压下来，我二人这两个都省相公，此时到底该做什么才能对呢？”</p>
<p>“当然是从根本上为官家分忧。”陈公辅依然不慌不忙。“两位相公，官家此番震怒，只是向着一个勾龙如渊而来的吗？难道不是忧心小人钻营，从内里毁坏大局吗？而若如此，两位相公何妨弃了勾龙如渊，高屋建瓴，使官家从根本上放下心来，也好促成北伐大业？”</p>
<p>周围几人，一起若有所思，而赵鼎则愈发觉得对方与自己暗中心思相合，却是再三认真以对：“陈尚书，可有良策？”</p>
<p>“不敢说良策，不过是拾人牙慧罢了……”陈公辅坦**以对。“两个法子，一个是针对朝中上下官员的，乃是从户部林尚书建财之策，还有最近推行的大表格之法得来的想法；另一个，则是针对南方士气民心的，却是个老生常谈之论……其实，有些事情，若是我们不自己来做，怕是官家也要用其他人来做的。”</p>
<p>周围几人，包括胡寅，齐齐挑眉，终于忍不住齐齐打量了一下这位陈尚书。</p>
<p>“你四人昨晚呼朋唤友，可想到法子了吗？”</p>
<p>翌日上午，赵官家在石亭再度召见四位宰执，一见面便直接逼问，俨然怒气不消。</p>
<p>而四位相公面面相觑，却是任由枢相张浚张德远向前一步，在石亭前拱手相对：“回禀官家，关于勾龙如渊之事，吏部吕侍郎为臣出了个注意，或许可行！”</p>
<p>“说来。”</p>
<p>赵玖言语干脆。</p>
<p>“福建士人弹劾胡尚书一案，虽已平息，但工部左侍郎勾龙如渊曾在文德殿上亲口言语，说此事背后或有蹊跷，指不定便有如王次翁那般小人暗行不轨，明着弹劾胡尚书，暗中离间天家……臣等以为，他既如此热心，何妨迁他为大理寺卿，着他亲审此案，务必找出背后小人？”张浚额头微微沁汗，但言语顺畅，俨然是早有准备。“找到了，自然是有人要为离间天家、指斥乘舆负责，找不到，自然是勾龙入渊诬论无辜！”</p>
<p>赵玖怔了一怔，然后忽然嗤笑颔首：“这是请君入瓮？”</p>
<p>“是！”张德远颔首不及。</p>
<p>“可以！”赵玖点头应许。</p>
<p>整个石亭内外，一时皆松了一口气。</p>
<p>“除此之外。”就在众人以为此事已了之际，张浚却又继续认真拱手进言。“户部尚书林景默昨晚曾劝臣，说为相者不该耽于表而疏于里……官家之所以对勾龙如渊发怒，不光是勾龙如渊小人可耻，更是忧心朝廷官员风气不正，或有千里之堤毁于蚁穴之忧……故此，昨夜臣等参考了林尚书昔日建财方略一事，结合官家近来推行的表格制度，想出了一个对内监督之法！”</p>
<p>“怎么说？”赵玖注意到了张浚身后赵鼎、刘汲的异样，但依然忍不住心动，因为这话说到他心坎上去了。</p>
<p>“请以半年为期，着六部、九寺、五监各列半年当行之策，如立军令状，再以枢密院设诸科，监督诸部寺监……一者，逾期不作为者，自当罢免；二者，也是协助御史台确保各部官吏莫行不法不德之举。”张浚俯首诚恳以对。“不知道官家以为如何？”</p>
<p>“朕以为很好。”赵玖点了点头，怒气都消了几分。“朕何尝不知道，事情不能指望人心，只能指望制度……你和林卿能往此处想，乃是极好的大局观……比朕被气糊涂了的样子要强。”</p>
<p>张浚闻言大喜，却还是匆匆拱手：“除此之外，还有南方之事……官家，昔日绍兴下野之臣、南走道学书院，能在南方结为一体，屡屡影响中枢舆论，其实是有缘故的……说到根子上，终究还是南方士民赋税沉重，以至于锱铢尽上，以付军费，所以人心厌恶北伐，偏偏这又是人之常情，臣以为朝廷并不好只去强压，正该恩威并重才对！”</p>
<p>赵鼎干脆抬头去看石亭上的飞檐雕塑去了。</p>
<p>而赵官家果然也大喜：“德远还有什么主意？”</p>
<p>“这不是臣的主意，这是兵部刘尚书的主意……他以为，如今虽说前线还有小战，但大局无碍，官家何妨向南一巡苏扬，以安抚东南人心？”张浚愈发严肃起来。</p>
<p>赵玖闻言也严肃起来：“南巡要多少钱？”</p>
<p>“官家只带两千班直，不治车驾，不受贡物，只若往年冬日巡河姿态，又能要多少钱？”另一位西府相公陈规赶紧上前，展示了一下存在感。“天子巡视靡费，皆在铺张无度。”</p>
<p>赵玖怦然心动，却是微微颔首，而张浚、陈规也是大喜。</p>
<p>不过，赵官家到底还记得有个首相在那边站着呢，旋即又看向了赵鼎：“赵相公以为呢，张相公他们说的可行否？”</p>
<p>赵鼎一声不吭，只是从怀中讨出一本已经被汗水浸了一半的札子，沉默向前奉上。</p>
<p>赵官家亲自欠身接来，打开一看，随意一瞥，便清楚看到两个标题：</p>
<p>其一，请设六科属都省以监六部；</p>
<p>其二，请御驾南巡，以安人心。</p>
<p>“那就这么定吧！”赵玖终于失笑，却又在合起札子以后陡然转冷。“但要先杀了那厮再说！”(本章完)</p>
