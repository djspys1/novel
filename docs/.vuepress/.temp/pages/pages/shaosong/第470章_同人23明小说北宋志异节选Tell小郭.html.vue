<p>大义不可辱</p>
<p>建炎四年，娄室骤然聚兵，率寇南侵。金贼若食人之兽，所到之处杀人如麻，血流满山，无道之行恶不可言。世祖闻之若刀斧加身，油火灼心，伏案痛哭不起。群臣劝，世祖挥剑斩案，怒冲冠而目眦</p>
<p>裂，眉倒竖而碎银牙，喝日:“万民遭厄，朕无能也。金贼恶行如此，非宋、釔兵战，乃华夷之大仇也!宋虽羸弱，朕仍有一-身好胆，两肋忠肝在此，纵然身死，亦让四夷知中国尚有英雄也!”言毕，</p>
<p>世祖亲征，命两宫备棺禁中，嘱日:“朕驾崩，子幼而无力，难与金贼拼命，速立旁支长子为帝，继朕遗志。金贼与赵，-者存--，不死不休也。</p>
<p>随军枢相汪公，奉命驻洛阳。汪公者，时任国之宰执也，名伯彦，字廷俊，籍徽州祁门，登进士第，初，家贫困而人多才，昔祁门县怜汪公之才，特筑英才馆纳之，并秦桧师伯彦。汪公耿而不迂，</p>
<p>著有《春秋大义》十卷，传诵天下。</p>
<p>时洛阳者，天下之中也，世祖以洛阳必守，汪公临危受命驻之。冶诃之子阿鲁补、汉儿韩常寇掠洛阳，大军压城若巨浪压滩，甲光闪烁似大海涛光。左右皆惧，唯汪公临城而望，忽放声大笑，谓兵将</p>
<p>日:“如此巨寇，凶残难言。官家不惜决死，誓阻贼兵于尧山，何也?盖忧百姓之生计耳。吾为枢相，千金之躯，然身后即父老妻儿，虽临虎狼亦不能退也。诸君可有种乎?”言毕，诸将血贯瞳仁，披发</p>
<p>挽袖以为誓，传汪公之言于成卒，士气大振。</p>
<p>汉儿韩常未受教化，性情蛮劣不堪。洛阳久攻不克，常怒而兴兵，率亲兵蚁附登城。汪公亲冒失锋，擂鼓激将，城上滚木如云，擂石如雨，乱箭齐发，中者透甲而过，血流成河。贼大溃，韩常亲斩逃</p>
<p>兵数人而不得止，戟指城头，喝骂曰:“汪老南儿，何故以卵击石，阻我大金?杀伤大金勇士如此，不畏千刀万剐乎?”任公@日:“无知小儿，言若犬吠。汝若明大义，何故为蛮夷之犬!昔金贼戮北民</p>
<p>亿万，**掳掠无恶不作，北民人人不共戴天。犬儿试听此言:他日汝死，宗祖问血仇可报?汝何答耶?</p>
<p>韩常闻之，惭怒交加，气血攻心，扯衣而裸，弓拳殴马以泄愤，马临三拳而死。常怒不减，抽刀杀数贼乃止。引刀指城，喝日:“他日破城，必将老儿五马分尸，头悬十年，躯埋城门，经万代践踏方</p>
<p>泄吾愤!</p>
<p>贼势大，洛阳戍卒死尽而破。左右欲护汪公离城，公拒之日:”城卒健儿，死未旋踵，真英雄也!今天子乃天下之雄主，上下一心，金贼必败，冢中枯骨耳!恨只恨不得亲见官家犁庭扫穴，直捣黄龙</p>
<p>也!吾死后请焚之，化为清风逐官家去也!“左右洒泪分别。汪公举火，大喝”《春秋大义》尚无批注，吾以吾血祭之!”而死。</p>
<p>韩常破城，寻汪公不得，忽见城中举火数丈，天地变色，旋风呜咽若泣。常引兵而入，欲辱汪躯，旋风忽大变，乘灰烬若干而起，围韩常不散，灰塞常七窍，常口不能言，气不得入，以手抓面，七窍</p>
<p>流血。未几，常抓一目脱出，鲜血四溢，然刷不尽目中残灰也。须臾，常身死当场，惨不忍睹。贼兵大骇，跪地而拜，无敢辱汪公尸身者。</p>
<p>尧山大胜，世祖闻汪公薨，悲痛数日难已。题词祭之，半阙乃止，哽咽不得下笔。有飞来峰灵隐寺-疯僧游历洛阳，于汪公遗址提歪诗日:“本是英馆两奸酋，-伴狸猫一-随狗，想来引刀成一快，方</p>
<p>可不负少年头“。题罢大笑而去，众皆以其疯，不以为意。</p>
<p>-《北宋志异:正神、星宿篇》卷五</p>
<p>鬼武神将</p>
<p>建炎初，秦王任后军统制，其部反，戏杀谏臣于河。时相李纲大惊失色，俯</p>
<p>首叩曰:“吾大罪矣!事急至此，官家速避!”。遂逐御驾出帐。此节冬，暮间风</p>
<p>若刀矢，寒裂铁石。世祖行半，冷风更甚，天地--片混沌，日月星辰皆不得见，举</p>
<p>火视之，林深而不知所处也。</p>
<p>忽惊雷起，世祖坐骑嘶鸣，人立而起，摇首摆尾而不肯行，-骑将隐约现于林中。</p>
<p>借雷光视之，此将非人非鬼，似影似烟，身长九尺，髯长二尺，面若重枣，威风凛</p>
<p>凛，手持春秋巨刃，**宝马赳赳，咆之如龙吟虎啸。左右大惊，疾呼护驾，迎而</p>
<p>围之，行十步，此将拖刀而走。世祖异之，率甲士逐而问话，将亦不答。</p>
<p>世祖逐之愈甚而将行之愈速，世祖停，将亦止也。世祖似有所悟，呼喝班值快</p>
<p>马相从。行数里，星辰乃现，复行数里，得见明月也，左右皆有喜色，遂拜谢前将</p>
<p>引路之恩。忽雷声复起，将化为萤火，世祖细观止，非萤火也，乃后军统制中军大</p>
<p>帐之火炬也。</p>
<p>世祖叹曰:“此华夏之忠魂引朕至此，韩良臣必不负我。”遂大呼良臣之名，</p>
<p>引马入帐，言前事种种，后赐玉带勉励。</p>
<p>--《北宋志异:正神、星宿篇》卷三</p>
<p>梦赠至宝</p>
<p>建炎二年，刘光世弃御营前军统制张俊于下蔡，金军乃围下蔡城而望八公</p>
<p>山。八公山者，御驾之所在也。俊尝言:“吾之爱财，如刀口舐蜜，虽杀身亦难止</p>
<p>也!”。贪财者多惜命，八公山众人皆曰张俊必降。</p>
<p>一日暮，俊巡下蔡至西北，众哨多机敏见礼，唯一哨似睡非睡，慵懒懈怠而</p>
<p>不觉。此哨卒形貌甚恶，面目可憎，衣装不整，见俊亦不行礼。俊怒而擒之，哨卒</p>
<p>睥而喝之曰:“何以不教而诛?”俊曰:“汝奉命哨于此，然酣睡懈怠，惩之何错</p>
<p>之有?”哨卒仰天大笑，声如巨钟，日:“吾自酣睡，然哨未懈怠也”。遂席地而</p>
<p>坐，此卒指天划地，自晨至暮，金兵斥候几名，装具几许，目的几何，清晰而呈之</p>
<p>也。俊遣左右核之，无--有误。俊大奇，摆酒宴于堂中赔罪。卒亦不辞谢，片刻用</p>
<p>尽。俊自省日:</p>
<p>“往后切莫急躁，天下能人几多?疑人不用用人不疑也”。</p>
<p>是夜，俊欲就寝，忽堂中金光大作，-武将头顶金冠，冠似龙角，身着金甲，</p>
<p>甲似龙鳞，昂首而入。定睛观止，赫然前卒。卒日:“吾姓张名永珍，淮河河神</p>
<p>也。奉天帝之命而助天子。今日受汝一饭，当遗一物。吾知汝甚是贪财，所赠乃为</p>
<p>天下至宝也，片刻而至”。俊慌而拜之，问曰何宝?河神大笑日:“此二言汝需牢</p>
<p>记一慨然一诺，雪渡夜送仇人头;解衣推食，同案共分盐水鸭。</p>
<p>言毕，金光大退。俊猛然而醒，原来南柯一梦。恍惚见床前立--人，定睛观瞧</p>
<p>竟是世祖临危雪渡亲临。俊伏地大拜，心中诧异不已。世祖扶俊起，出言勉励安抚</p>
<p>而赠食盒于俊。俊启食盒观止，赫然为刘光世人头--枚，失一足咸水鸭一-只。</p>
<p>--《北宋志异:地神、散仙篇》卷五</p>
<p>九龙井</p>
<p>靖康元年，正月辛巳，飞沙乍起，昼夜不明，沙鸣若百鬼夜哭。二月戊申，大</p>
<p>风起于东北，白日降雪，沙雪混沌，扬尘翳空，风吼似万兽哀鸣。三月己己，夜五</p>
<p>更，大雾弥漫，遮天蔽日，雷声阵阵，如仙家呵斥惩戒，夙日不断。及夏，**雨霏</p>
<p>霏，连阴不解，夏行秋令，四季颠倒。而冬，冰雹伤稼，二十余日不霁，雪深丈</p>
<p>余，大风发屋折木，诸末世之相，不可胜数。</p>
<p>靖康二年，二圣北狩，世祖登基，初，心灰意冷，浑噩不知天命。行及亳州，</p>
<p>梦紫气堕于明道宫，遣人访之，答曰并无异常之象。-连数梦，皆梦于此。世祖奇</p>
<p>而亲至，遂见此宫大不同于往日，金光迸现，紫气逼人，神烟袅袅，仙乐阵阵。问</p>
<p>及左右，左右皆日不得见，与寻日无二也。</p>
<p>世祖得悟，私至宫内，宫内左进一九龙井，隐有雷霆之音。世祖捉而入井，初</p>
<p>不得路，复行十步，豁然开朗，或天，或地，或山，或水，井内竟自成世界。一道</p>
<p>衣老者现，喝曰:“吾三清也，道号机子，三百六十分位神仙之首也。汝自为天</p>
<p>子，却渐失天命也!”</p>
<p>世祖躬而问之，老者曰:“夫天命者，刀斧不能加其身，鬼神不能夺其魂。何</p>
<p>也?盖因其人，身负华夏千年之传承也。鬼神皆传承所化，岂能伤之哉?传承何</p>
<p>也?传承即万民也!</p>
<p>先辈筚路蓝缕，以启山林，方有尺寸之地。后辈或传千年文华以开民智，是故天命</p>
<p>之人自有大智者辅之。或逢蛮族，天下鼎沸，自有好汉别亲离子，慨然以赴，临刀</p>
<p>山而无惧，渡火海亦从容。是故天命之人自有猛士保之。.</p>
<p>启民智者万千，人可怀念--二，难怀万千矣。夫勇者万千，史书可志一二，难</p>
<p>志万千哉!汝自承天命，当知继华夏万民之志也!刀斧加万民之痛，当若加汝身之</p>
<p>痛;饥馁临万民之腹，当若临汝身之腹也!非承天命方可得天下，实乃代万民者方</p>
<p>可得天命耳!汝自去代万民抗刀兵之灾，承百姓避饥馁之祸，则天命自在身也。”</p>
<p>世祖大悟，三拜而出，遂立志绍兴，不负万民，承华夏之传承</p>
<p>--《北宋志异:三清、四御篇》卷一</p>
<p>讨口彩</p>
<p>建炎二年，世祖幸南阳，政通人和，万物复苏。宗弼畏世祖潜龙之资，点北</p>
<p>地之兵欲破之，然军议无良策。彼秦会之为完颜昌幕僚，宗弼惜桧之才，出则宝马</p>
<p>香车之奢以侍奉，入则鲜肥滋味之享以供养。每有大事，必躬而询之。</p>
<p>桧贪富贵，遂遗三计于昌、宗弼，成其侵南策也。临行嘱宗弼曰:“主自英</p>
<p>武，然南人文华千年，底蕴深厚，必有所持也。主当广开言路，习当地风俗，切莫</p>
<p>横遭异祸。”宗弼应之，不以为然也。</p>
<p>夙日，宗弼寇掠南阳，烧杀掳掠，屠戮百姓，左右劝之:“会之言于我等，</p>
<p>南阳此地天子驾临，必有灵气，切莫戕害生灵，免遭横祸。”宗弼藐而不悦:“南</p>
<p>人迂腐怯懦，无稽之甚，文华之言，陈年旧事矣!太祖骑射起家，吟诗诵书焉得主</p>
<p>北地耶?吾为狼也，生而王之，南人兔也，生而奴之，何以习为?”左右喏喏不敢</p>
<p>言。</p>
<p>南阳有茂林雄山，曰伏牛山，乃嫦娥神牛所隆也，四季如春，仙气氤氳。山</p>
<p>中有虺，五百年化为蛟，后修炼千年，不得正果。恰逢世祖临南阳，得此大机缘而</p>
<p>得悟，月余便走蛟而起，意欲化龙。然走蛟化龙需借人之力，临空中而望众人，众</p>
<p>人指日:“此龙也”，则化龙成。夫人者，万物之灵也，出言皆含因果，敬人如敬</p>
<p>己，恶语必反噬，遂民间传走蛟--说亦为讨“口彩”。</p>
<p>时逢宗弼于平地军议，南阳久攻不克，心绪烦恼日甚。忽金光闪动，祥云密</p>
<p>布，雄马膝跪嘶鸣，壮骡伏地呜咽，鹰隼啼走，一巨蚺现，盘桓于空。宗弼大惊，</p>
<p>抽刀怒指，喝日:</p>
<p>“何方妖物，何故扰我军心?</p>
<p>蛟龙大怒，日:“吾龙也!世人皆知走蛟之口彩，汝何故不知，敢以妖物辱</p>
<p>我?汝蛮夷之辈，不学无术，既入华夏，何以不识华夏文字，承华夏传统?圣人有</p>
<p>教无类，从不以血脉而轻尔等，大道三千皆可习之，汝受教则为华夏子弟也!然尔</p>
<p>等人面兽心，不服教化，屠刀能加一人，焉能加之于天下哉?吾观尔等不过冬末之</p>
<p>残冰，自认可比宝玉，然天寒尚可，天暖则销声匿迹矣!可叹秦桧那般华夏儿女，</p>
<p>图一时之享乐，献毒策谋害君父，自甘蛮夷，不以为耻，反以为荣，真教化之败类.</p>
<p>也。如今天下将暖，吾试观汝等残冰何处依存!”</p>
<p>言毕，龙吟震天，--声则飞沙走石，遮天蔽日，二声天降暴雨，平地生雷，三</p>
<p>声地动山摇，泥弹自天而堕。三声毕，缓缓而晴，风淡云轻，蛟龙踪影全无，然宗</p>
<p>弼之将校多卒于泥弹之威。</p>
<p>-《北宋志异:地神、散仙篇》卷八</p>
<p>烈士神庙</p>
<p>建炎四年，尧山大胜，金人亡去，有百姓逐水相聚，错落成村，或樵或耕，休</p>
<p>养生息。及五年春，人口渐兴，炊烟袅袅而起，花甲蒙荫而有所终，垂髫相戏而有</p>
<p>所养。青壮巧妇怀梦而勤之，面若有光。</p>
<p>时有稚子三五，临竹马以戏青梅，执粘杆或捉纸鸢。忽阴风起，乘纸驾而去，</p>
<p>群童大乱，呼唤逐之，跌撞扶持，含泪疾走而不得，未几，纸鸢不得见也。群童失</p>
<p>鸢，若失至宝，或滚伏于地，或泣涕而啼，或顿足捶胸，或抱头不语。须臾，群童</p>
<p>饥馁欲归，然不得路也。</p>
<p>天色渐暗，有野兽咆哮于林，群童慌而走之，目难视物，褴褛难行。咆声愈大，-</p>
<p>童失禁而泣，有悉索声缀于群童身后。及夜深，群童无力，咆声愈大，悉索声处有</p>
<p>绿光现，状若二目，似狼似虎。群童大骇，尖叫号之，呼爹唤娘，寻丛欲藏而不</p>
<p>得。绿目渐近，咆声现贪婪之音，腥风阵阵而起。.</p>
<p>忽一物破空而来，击绿目而毙，野兽惨声连连，一大汉于半空现，声若巨钟，</p>
<p>大喝日:“何方犬豸，欲伤我子弟耶?“兽惊而亡走，腥风平，咆声止。大汉呼朋</p>
<p>唤友，抱群童而去。童若身处父兄之怀，心中大定，饥困交加，甜睡而去。梦中有</p>
<p>官兵数人望童而笑，或抚童顶乐之，或执瓜果鸡脯相戏，官兵、群童皆开怀焉，事</p>
<p>毕，--汉指归乡道路于童，官兵散去也。</p>
<p>次日，群童渐醒，起而视之，身处一庙中也。童择路归村，村民大喜。及归</p>
<p>家，父斥骂而母怜爱，童以前事种种相对。父母大惊，串联村民访庙。时有高僧，</p>
<p>俗名李修缘，游历至此，伴村民而至庙中，四方拜曰:“此庙者，于尧山大战后，</p>
<p>官家所立也，所祀乃华夏英灵也。无此众英灵，便无当下之安宁也。吾尝闻，群墓</p>
<p>之处多阴风鬼火，然此庙虽墓万人，无阴风也，浩然之气也!天子者，受天命而代.</p>
<p>法则之神也。英灵受天子所封，必富泽万民耳，汝等须好生供奉</p>
<p>村民视之，果然暖风宜人，如抚肌骨，叹服而拜。孩童扰而英灵不愠，祭祀瓜</p>
<p>果食之他人而开怀，真父兄子弟也。此庙遂香火渐旺，四季祭祀贡品不绝，童子戏</p>
<p>于外，村民拜于内。非求功名利禄，止照看子弟父兄而已。</p>
<p>--《北宋志异:正神、星宿篇》卷一</p>
<p>算命</p>
<p>秦王，姓韩名世忠，字良辰，延安府绥德军人。天生神力，勇武过人，昔秦王</p>
<p>方加冠，有野马袭路人。秦王赤手巡山，欲降此恶兽。三日，-怪马现于山脊，蹄</p>
<p>分二_指，鹿角虎目，麋身牛尾，鬃飘似焰，体毛若鳞，吼则生风，行则踏云。秦王</p>
<p>凛而不惧，飞身上前，执鹿角跤之。马张口欲噬，秦王以膝抵之，马口竟不得开。</p>
<p>须臾，马渐力怯，秦王飞身骑之，左手捉鬃，右手握拳而殴。马头晕目眩，跪地不</p>
<p>起，开口竟出人言:“吾麒麟也，今斗你不得，请为坐骑。”秦王闻之大喜，引为.</p>
<p>坐骑，日后南征北战，建功立业，按下不表。</p>
<p>秦王年少性暴，尝游于市井。有卦者，长发道服，每日酣睡不已，一日三卦，</p>
<p>算毕则归，或分文不取，或取万金不止，所算无不应者，秦王大奇之，前往卜卦。</p>
<p>卦者坐卧酣睡，双目似睁似闭，不应秦王之求。秦王大怒，以水覆之。卦者叹</p>
<p>日:“何至于此?君命相贵不可言，怪哉，怪哉，卦日尧山贵人待汝相救，救则</p>
<p>王。”。秦王不解其意，细问之，卦者奇日:“不知贵人何故似狸耶?”秦王自诩</p>
<p>市井之徒，朝不保夕，误卦者讥讽，怒而殴之，卦者愤然曰:“吾师，华山扶摇子</p>
<p>也，今破金丹而化元婴，飞升成仙矣!汝如此辱我，必报汝后人耳!”秦王不以为</p>
<p>意。</p>
<p>数年，秦王参军，积战功而将，世祖登基，秦王护驾有功，得驾前重用。后伴</p>
<p>世祖南征北战，东征西讨，积功无数。时逢尧山大战，世祖御驾亲征。秦王思前卦</p>
<p>者言，暗自揣揣，私盯世祖不止。战正酣，娄室引亲兵，骤而袭世祖，欲刺王杀驾</p>
<p>也!众皆大惊，须臾不得拦阻，金兵杀声不止，御驾甲士起身当之，然触金兵即</p>
<p>倒。娄室欺驾前无人，与世祖引弓对失。官军恐世祖有失，皆大乱，唯秦王临而不</p>
<p>乱，指左呼右，唤来弓矢，喝曰:娄室何在?三声吼毕，麒麟携秦王人立而起，居</p>
<p>高临下，见一金将银盔金甲，目视己身，秦王吐气开声，挽弓如月，此箭若破空惊</p>
<p>雷，似飘渺流星，正中娄室。娄室凶蛮，仍欲刺驾，秦王**麒麟惊天一吼，娄室</p>
<p>坐骑心惊胆颤，马失前蹄也。世祖临危而不惧，指挥若定，斩娄室，宋军士气大</p>
<p>振，片刻破金军于尧山。</p>
<p>战后论功，秦王得封延安，然数年不得子。思昔日之言，悔年少莽撞，遂携夫</p>
<p>人华山还愿。然卦者闭关不得见。于是再三，得见卦者，叙前事种种，捐献供奉，</p>
<p>致歉连连。后终得子，尚公主也。秦王经此一事，修口德而存敬畏，与人为善与世</p>
<p>无争，北伐积功受赏，后随世祖迁都北京，赞誉满朝，得无憾善终。</p>
<p>--《北宋志异:地神、散仙篇》卷六</p>
<p>匹夫</p>
<p>有河北贝言者，猛将也。昔言为河北--民，以纹身刺青为业，习得武艺十八，</p>
<p>助老扶弱，村民敬仰。恰逢金贼虐河北，生灵涂炭，虐杀百姓，尝闻贼将引刀戮</p>
<p>民，计斩杀人数以为戏。天怒人怨之举恶不可言。至言村，言护乡梓隐于山林，途</p>
<p>杀贼兵数十，身中数疮，血流不止，及进山，人事不省也。乡梓以菜汤食言，数</p>
<p>日，粮断，仍不忍断言之汤。金贼围山，举火焚之，哀鸿遍野，欺厉惨叫不可闻，</p>
<p>村民死者大半，言妻儿皆死。</p>
<p>余二三子欲护言亡走，然火烟有毒，中者皆伏。忽白光起，二三子负言乘风</p>
<p>而起，掷于河边。风中一僧出，体腥臭不堪，歪冠漏履，似笑似哭，举止疯疯癫</p>
<p>癫，出言沾牙倒齿，搓泥丸入言口，言醒，谓言前事种种，言闻之，悲怒难已，扯</p>
<p>发大呼妻儿乡梓，以指捉树，入木三分。须臾，心若死灰，欲自刭随家小乡梓而</p>
<p>去。僧阻止，日:“吾降龙罗汉也，有同门金翅大鹏于佛祖身前，闻佛祖讲经。鹏</p>
<p>嫉恶如仇，因杀一鼠罪佛祖，佛祖以降世罪之。汝尚有因果在此。”言毕，以手指</p>
<p>路，随风而去。</p>
<p>言诧异不已，随路行之，数日遇一官兵营寨，岳字旌旗迎风而扬。营兵军纪</p>
<p>斐然，戍卒昂首挺胸，声如洪钟，三人则成列，二人则成排。营门有百姓若干，箪</p>
<p>食壶浆以拜之。言惊喜交加，不想世间竟有如此天兵，大仇可报也，遂投军。自言.</p>
<p>入军，但闻次日与金贼交战，言必血贯瞳仁，夙夜不寐，磨刀不止，积功为将亦不</p>
<p>为喜乐，止盼每日杀贼。</p>
<p>建炎十年，世祖引兵三十万北伐，与金贼获鹿大交兵。魏王岳公引言压于左</p>
<p>翼，交兵数日，杀得天昏地暗，鬼哭神惊，不得胜负。言怒火攻心，献计以自身为.</p>
<p>牺牲诱之，魏王爱兵如子，欲躬身替言，言大拜叩首不止，以拳捶地作响，满营皆</p>
<p>惊。言赤目昂首日:“岳公天神下凡，绝不可失。言此去实自所愿也!倾尽三江四</p>
<p>海，也难洗尽吾与金贼之血仇!”魏王得机，自知贝言之因果，难逆天而为，遂许</p>
<p>之。言大喜，回营磨刀去也。言营中皆与金贼负血海深仇，满营磨刀霍霍之声震</p>
<p>次日，言引虎贲三千，须臾而至金营，-踹三连营，若虎入羊群，贼军大</p>
<p>乱。言以巨刃斩贼，平衣甲过，血流成河，贼片刻溃散。压阵贼将乌者，弹压贼众</p>
<p>欲起，言扬巨刃暴喝:“贼子受死!”打马而冲。乌者闻言如惊雷击顶，惨面颤抖</p>
<p>不得脱。言迎上，手起刀落，劈乌者于马下，捶胸大吼曰:“妻命得报也!”。复</p>
<p>有乌者之父背鲁，真经年老贼也。闻子死于乱军之中，惊怒交加，引兵欲杀言，举</p>
<p>大枪分心便刺。言咬碎钢牙，怒骂好贼，拍马迎上，舞巨刃似大雨瓢泼，迎头劈贼</p>
<p>而上。止三合，老贼力怯，虚晃-枪欲走，言亲兵有备，以手执贼马尾，马蹄言</p>
<p>卒，啼入腹而执不止，仍大呼报仇也。言迎上，连肩带头，一刀斩之，言复大</p>
<p>呼:“子命有尝也!”。老贼血溅言卒之身，卒以舌舐血，大笑无憾而死。金贼巨</p>
<p>寇阿里见连折二将，大怒，引全军扑言，言诈败于谷，未几，鼓声大震，见左右烟</p>
<p>尘滚滚，牛皋于左路杀出，张宪于右路杀出，魏王亲压大阵断阿里之后，阿里面如</p>
<p>死灰，率亲兵欲退，言复身缠斗，身披数创，失--臂，仍大呼冲锋。阿里不得脱，</p>
<p>魏王至，枪刺似点点梨花，照面即挑落阿里，阿里落于乱军之中，践踏为肉泥也。</p>
<p>诸将片刻杀贼尽，言三呼日:‘</p>
<p>“乡梓之仇得报也!”复喝:“金贼，可知匹夫无不</p>
<p>报之仇乎!”力尽而死。</p>
