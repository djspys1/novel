<p>倒是胡寅，依然面不改色：“六岁时读过。”</p>
<p>“《礼》有言：夫鲁有初。还有令尊讲学时也曾引用《列子》的话说：太初者，气之始也……胡尚书应该是知道这个‘初’的意思吧？”勾龙如渊继续认真询问。</p>
<p>“知道，乃是说万事万物皆有缘由和开始的意思。”胡明仲依然从容以对。</p>
<p>也就是从此时开始，彻底糊涂的贺铸明智的放弃了插嘴的意图，老老实实坐在那里听这二人对话。</p>
<p>“胡尚书，在知道‘凡事必有初’这个道理之前，下官曾在州郡沉浮十几年……”勾龙如渊喟然以对。“明明认认真真做事，明明努力去揣摩上头的意思，却总是因为这个因为那个不得伸张，反而屡屡一沉到底。后来随着年纪增长，才渐渐想通了这个道理。虽说再后来因为靖康之变，为大局所困，还是一时不能飞黄腾达，却终究能窥的朝局真谛，不至于浑浑噩噩了。”</p>
<p>胡寅看了看对方，认真再对：“这个‘初’这么厉害吗？”</p>
<p>“凡事必有初，如果能根据事情的‘初’去作为，那事情总会很简单，反过来说，没有看懂事情真正的‘初’在哪里、是什么，那一定会陷入疑难之地。”</p>
<p>勾龙如渊没有理会对方的嘲讽，而是愈发感慨不及。“从小事上来讲，当日泉州番寺一案的初便在于官家老早便展示过警惕番商的态度，不愿予他们皇家文书旗帜，可笑其余官吏皆以为朝廷会为了一点商税而姑息养奸，却根本没想过官家的脾气始终一如既往。再从大局上来讲，朝廷的初便在于靖康之变……有了这个‘初’，自然就明白，为什么朝廷人事上新旧两党不复存，而是战和、攻守、急缓之争；也自然醒悟，为什么官家与两位太上皇帝会有这般龃龉；更懂过来，为何朝廷大政皆在宋金之战上了。”</p>
<p>“不错。”胡寅当即颔首。“你说的是有道理的……建炎以来，国家政治、风气、人事一改，根源皆在靖康。便是泉州番寺一案，也是你相隔千里，窥的原初。”</p>
<p>“还有，为何战和之间是战？攻守之间是攻？急缓之间是急？其实也都有‘初’。”勾龙如渊抬起左手，右手扳起左手手指，一一认真言道，同样没有因为对方的认可而稍有松懈。“如陛下继位，这是第一个‘初’，他得位意外，必须要言战以正名，而又遭横变，所以常有非常之举……”</p>
<p>“淮上扼守，是第二个‘初’，一朝稍阻女真疲兵，知女真亦有力尽之态，明中国之大未必可不守……”</p>
<p>“移跸南阳是第三‘初’，晓示内外绝不苟安、宁死不屈之心……”</p>
<p>“还于东京是第四‘初’，明海内宋之未亡……”</p>
<p>“尧山拼死是第五‘初’，使天下知中国尚有可为……”</p>
<p>“一初叠一初，待到尧山之后，北伐大势便已经不可更改，可笑还有些人想降、想和、想守、想缓，却不知道，事情早已经注定。”勾龙如渊收起用来计数的手掌，摇头以对。“下官也就是从那时开始，再无顾忌，以至于行事皆能遂中枢大略……所以，转仕顺利……然而，下官明知这‘一初叠一初’，知道官家用人之‘初’在哪里，却还是鬼迷心窍，做了这种事情，也是同样可笑！”</p>
<p>“你到底想说什么？”胡明仲终于不耐烦起来。</p>
<p>“下官想让胡尚书转告官家几件事情……”</p>
<p>“说来。”</p>
<p>“其一，下官是晓得国家大政的，一朝行此龌龊之事，着实是权欲迷了眼睛，还望官家能稍留下官有用之身。”</p>
<p>胡寅一声不吭，只是冷冷去看对方，便是旁边的贺铸都忍不住斜眼去看这位同僚。</p>
<p>“其二，设立六科是必要的，但应该把重点放在对六部的监管与考核上，而非是监督与刺探人心……因为我勾龙如渊只是个才入京不过月余的小人，朝廷上下一时失察，没有看出来我，是很寻常的事情，请不要就此怀疑中枢官吏这么快就变质。”</p>
<p>胡寅终于颔首，但脸色一点都没变：“这件事，我一定会进言官家。”</p>
<p>“其三。”勾龙如渊继续认真相对。“六科既设，本身是台谏的延续，制度之初便在谏院，应该归于御史台。”</p>
<p>胡寅终于脸色稍缓。</p>
<p>“其四，官家下江南是对的，因为地方人心才是真正的初，但既下江南，与其抱雨露之心，不如持雷霆之力；与其探士大夫之心，不如观风俗士气；与其观名城大郡，不如窥乡野田土；与其看商税矿产，不如察田赋劳役……”</p>
<p>“这后面一串也是‘初’的学问吗？”胡寅终于发声。</p>
<p>“是。”勾龙如渊微微欠身以对。“前者是末，后者是初……能循初，就不必在意末了！”</p>
<p>“那你这番话的‘初’，其实还是其一了？”胡明仲坦然追问。</p>
<p>勾龙如渊沉默了一下，点头相对：“是……但于官家而言，于朝廷而言，下官的初反而只是末，下官的末，或许能成为官家的初……请胡尚书护臂转达下官这番言语。”</p>
<p>“我这就与何侍郎一起去见官家。”胡明仲沉默了一下，起身以对。“我自幼过目不忘，你的话我一个字都不会改，何侍郎会如你愿做见证……你是在此处等候，还是回家等候？”</p>
<p>贺铸彻底明悟，赶紧起身。</p>
<p>而勾龙如渊想了一想，也起身恳切拱手：“下官就在此处相侯。”</p>
<p>胡寅点了点头，便与一声不吭的贺铸一起离开公房，扬长而去了。</p>
<p>去了大概半个时辰，贺铸没有回来，胡寅也没有回来，却是大押班蓝珪引几名御前班直抵达了工部大院……后者甫一进入尚书公房，便对着浑身颤抖的勾龙如渊干脆出言：</p>
<p>“官家口谕：勾龙卿既知朕之初，便也该知道朕素来喜欢肆意无度，舍初留末。”</p>
<p>言罢，这位内侍省大押班直接转身离去，再不回头，只留下勾龙如渊彻底失声于房内……他哪里还不知道，自己最后一丝挣扎也没有成功？</p>
<p>然而，勾龙如渊始终还是留了一丝求生欲的，这一日，他在公房内足足等到天黑，以冀希望于胡寅和贺铸能回来跟他说上一句话。</p>
<p>然而，一直到天色黑的不能再黑，却始终无人归来，而勾龙如渊也只能在门前两位御前班直的逼视下失魂落魄转回家中。</p>
<p>回到朝廷发下的新舍内，这位新任大理寺卿唤来妻妾儿女，直言自己命不久矣，乃是将家中存的国债、金银一并分出，并让这些人明日一早便出门归川蜀故乡……而等到翌日天明，妻妾儿女们被仆役驱赶出门，掩面而走，勾龙如渊自己几度欲死，以求体面，却几次不能下手。</p>
<p>最后只能困于家中，坐以待毙。</p>
<p>真的是坐以待毙……这一日，工部右侍郎贺铸依次往都省、枢密院、御史台、刑部，当众举证，言大理寺卿勾龙如渊构陷同僚，离间君臣，还诿过于太上道君皇帝，分离天家，欺君罔上，罪在不赦。</p>
<p>一时朝堂哗然。</p>
<p>而因为是大理寺卿犯案，所以直接移交刑部处置，当日下午，两名刑部小吏便带着两名狱卒来到勾龙府中，直接将勾龙如渊牵出府邸，发入刑部狱中。</p>
<p>所谓拿一秘阁大臣，如牵一鸡犬。</p>
<p>这下子，乃是朝野哗然了。</p>
<p>事关重大，无人敢怠慢，仅仅是又隔了一日，刑部尚书马伸便以御史中丞为见证，以三位御史为辅，亲自开堂询问，当场传唤尚书胡寅、侍郎贺铸，以及被截留的福建乡人，对照‘推勘（调查审问）’。</p>
<p>待得到供状无误后，未及中午，又直接一式三份，分别送达御史台、都省，以及走枢密院转入御前。</p>
<p>赵官家片刻不停，当即批复：</p>
<p>“勾龙如渊包藏恶意，以私心而欺君罔上、构陷同僚、祸乱国家，而无复人臣之节、同列之谊、官职之操者，未有如此人也！当此战时，应行军法，追毁出身以来文字，斩立决！”</p>
<p>批复迅速从内侍省转回，而都省、枢密院则直接在批复的文书外加上了东西二府的封条，宛如处置什么寻常旨意一般。</p>
<p>而与此同时，对崇文院那边反应毫不知情的御史台上下得知官家批复消息后，却明显犹豫了一下，这才在乌台召开内部会议，待到傍晚才得到一个一致意见，乃是建议赵官家将此事拿到下次朝议进行公开讨论。</p>
<p>随即，李光亲自将文书带入崇文院，寻到枢密院，要求值守官员将文书明日一早即刻转入内侍省。</p>
<p>却不料，翌日上午，这封唯一公开反驳官家旨意的文书尚在流程之中时，一队御前班直便直接进入刑部大牢，先是出示了全部合法公文，将瘫成一团肉泥的勾龙如渊拽出，拎到宣德楼前，然后便当众公布罪行，随即一人按住，一人挥刀，宛如之前此地杀那匹御马一般利索，直接将这位前日还是秘阁大员斩首示众。</p>
<p>待刑部尚书马伸与御史中丞李光得知讯息，匆匆携手赶到现场后，却惊愕发现，此时连地上的血迹都已经洗干净了，只有那个早已经腐烂到只剩骨头的马首，挂在宣德楼上，被熏风吹动，居然一时呜呜作响。</p>
<p>刚刚还在讨论是不是要让勾龙如渊‘徒远地，不赦’的二人也是彻底无声。</p>
<p>又过数日，朝廷透过内部文书、邸报发布了官家与宰执共议结论，设立六科，意在考核，不在监察，收于御史台谏院。</p>
<p>又过数日，就在前线再度发起轮战之际，邸报却度刊登了赵官家另一道旨意，乃是说‘凡事必有初，朝廷中兴之初不在中原，不在兵戈，乃在江南，乃在士民’……官家将于七月启程，率一千五百御前班直，两千御营骑军，南下巡视荆襄、东南，并委国政于诸宰执、秘阁。</p>
