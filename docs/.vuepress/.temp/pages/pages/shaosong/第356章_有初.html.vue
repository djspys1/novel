<p>最后，这位工部右侍郎干脆一声不吭又去看向了胡寅胡尚书。</p>
<p>而出意料，胡寅还是没有任何表情变化，只是微微颔首……很显然跟贺铸想的一样，这位官家不惜自污也要死保的心腹大臣绝对是早就知道了此事的。</p>
<p>但下一刻，勾龙如渊便让何侍郎彻底停止了思考：“这案子不用查了，因为当日着人在那几位福建士人前说胡尚书与刘勉之有怨的不是别人，正是下官，而下官也的确是想将胡尚书撵出去，看看能不能再进一步。”</p>
<p>贺铸愣在当场，但胡寅却没有任何多余反应，只是微微点头：“我知道。”</p>
<p>而勾龙如渊稍作沉吟，却又微微叹气以对：“胡尚书读过《礼经》吗？”</p>
