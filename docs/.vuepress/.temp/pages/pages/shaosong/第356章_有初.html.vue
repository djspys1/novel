<p>胡明仲没有听到钟声直接下值回家的意思，此时从满桌的文书中抬起头来，先是瞅了瞅面色惨白的勾龙如渊，也是没有丝毫的动静，又低头看了看桌上文书，签了个名字以后，方才再度抬头。</p>
<p>而这一次，他看到了跟在勾龙如渊身后、明显面有疑惑的贺铸，这才微微欠身拱手，以作礼节。</p>
