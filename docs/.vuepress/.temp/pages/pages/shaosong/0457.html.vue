<p>第一部分：编制体系初探</p>
<p>一、传统四层指挥体系、置将法、亲卫体系发展脉络。</p>
<p>1、传统四层指挥体系：</p>
<p>北宋传自五代的传统四层指挥体系为都-营-军-厢，据《武经总要》前集卷1《军制》和卷2《日阅法》的记述，“大凡百人为都，五都为营（指挥），五营为军，十军为厢，或隶殿前，或隶两侍卫司”。但事实上并不会整齐划一。真宗时，已有一军辖十指挥的，有的军，下面却仅有1-2指挥（如云骑军等）。所以，军作为营（指挥）的上一级是确凿无疑的，具体员额却不必深究。国家博物馆藏有“神卫左第四军第二指挥第五都记”等铜印，表明了自都至厢的番号。</p>
<p>按上述，一都为百人（约数，下同），一营为四百人（骑卒）或五百人（步卒），一军为2000-2500人，一厢理应有员额两万至两万五千人，实际上很难达到。</p>
<p>实际上，北宋中后期，厢作为编制意义已经名存实亡，多作为番号标记或临时编组大军使用，如童公公的龙神卫四厢指挥使。</p>
<p>另，作为编制的军，与作为部队名号的军，并不是一个意思。作为名号的军，大致表明了该部队的来源（征募地）、编制特点（如带骁、捷等字则多为骑军）等。</p>
<p>2、置将法与队、部、将、军的新编制。</p>
<p>王安石行置将法以后，禁军形成了队-部-将-军的新编制。新编制中的“军”为一支军队分为前后左中右之“军”，非固定编制，即新编制实际仍只有队-部-将三级，作战时将若干将编组起来，再分为前后左右各军，另有选锋军等，各军战时委派一将担任统制官（此处可看出统制官非常设）。盖因陕西路为黄土高原地形，难以展开大军会战，后期宋又奉行结硬寨打呆仗的浅攻掘进风格，导致宋夏实际交战均以千人至万人规模为主，故不长设较大编制。</p>
<p>但将以下均为实际统兵且固定统属关系的编制。书中曾描述，吴阶就做过泾源路第一正将。但置将法应用地区非全部，至靖康前，仍与都-营-军-厢（此时名存实亡）并行。</p>
<p>新编制下，一大队基本与以前的大什同，约五十人，一部约二百五十人，一将统千人。</p>
<p>但按照神宗朝设想，将为一地设一将，编制也非固定数，非千人定额，当然后来没有实际执行下去，具体待考。</p>
<p>3、大将体制下的亲卫体系及阶级：</p>
<p>背嵬军体系（队-部-将-军）。</p>
<p>因为靖康总崩溃，军队失去中枢控制，自然演变为以大将为核心的军制，而大将为了确保控制军队，使得将领亲卫体系大肆膨胀，直至统军大将的亲卫高达一军（传统编制）之多。背嵬军作为韩、张、岳等大将亲卫部队，在得到朝廷认可后，最终在赵玖的统制官-统制部格外强化的背景下，获得了统制部的编制，与传统指挥体系重新结合。</p>
<p>4、统制官、统领官体系。</p>
<p>靖康后全国军事体系崩溃，上层军制混乱失效，为了抵抗金军，诸路州到处乱设制置使、经略使，对于乱世军头，不管是正规军残余，还是地方土豪、义军等，到处分发统制官、统领官，类似于现在非野战军体系的总队-支队-大队，统制官下辖员额不定，统领官下辖员额不定，一般以一部设一统制，如韩世忠出场就是统制，统制官下独立领兵就封为统领。统制官-统领官体系正式登上历史舞台。</p>
<p>后来御营体系建立，赵玖强化了高层军制中的都统与统制官的阶级区分，再结合下层体系，重新确立了基本阶级。基本将统制部员额数限制在2000-4000人（个别除外）。并追认统领官为正式阶级。</p>
<p>统领官阶层：御营军制建立后，除追认外，多以一统制部的副职或资深营指挥任命为统领官。实际上不属于军队编制体系的任何一级，但因为战场指挥权、亲卫体系，以及资历营指挥的存在，依然广泛存在，并且相当具有活力，并作为晋升统制的后备力量。</p>
<p>而更多的亲卫首领，则通过准备将-正将阶层与传统指挥体系下略显冗余的统领官相互转化流动。这也是统领官阶层具有强大活力的另一个重要缘故。亲卫首领想升统制，需出亲卫体系任统领官统带一营（或任统制部副职），获得战功后，在扩编中（或补任）升任统制。</p>
<p>二、绍宋中实行的军事体系</p>
<p>1、军制体系，改革与承认既定事实并存。</p>
<p>大致上，通过赵玖十年的权威压制与改革，基本上统一了上层军制。目前绍宋中上层所施行的，自上而下基本上为行军司（御营前后左右中军等）-统制部的编制。</p>
<p>中层军制，则并行指挥-都、将-部两套体系。</p>
<p>基层军制，因基层军制改革难度不大，且改动较小，与历史曾有编制基本相符，架空如下：步军均为都-大什-什-伍，一都105人；骑军为都-队-什-伍，一都65人。</p>
<p>总而言之，御营体制是一种靖康总崩溃后，赵玖为了控制部队，不得已结合历史发展进程（比如大将军制下亲卫体系泛滥、高层军制混乱），尽量制造出的一种兼有妥协与压制的复杂军制。</p>
<p>2、步军编制依据及战术目的：</p>
<p>因大将亲军多作为胜负手或主力部队投入决定性战斗或战役，故在此不做分析与说明。</p>
<p>（1）、编伍层面。伍、什，为基本编伍单位，如行军为一队，宿营为一帐等。</p>
<p>（2）、兵种层面。大什，为基本兵种单位，如一大什均为弓箭手，另一大什为长枪手，另一大什为刀盾手。</p>
<p>（3）、战术层面。都，为基本作战单位，下属至少有两个兵种搭配，配合起来具备基本作战能力（单一兵种无法独立作战，如弓箭手近战能力不足，刀盾手无法防冲击，长枪手无法独立面对远程打击）。</p>
<p>营（指挥），（550人左右）为基本战术单位，下面骑步各兵种齐全，可配合作战，独立完成战术命令。一指挥下辖有骑兵100人左右，长枪手2大什，刀盾手3大什，弓箭手3大什，可独立完成布阵。</p>
<p>（4）、行军（机动）层面。部，（统制部）为基本行军单位，有独立辎重部队，可承担防守时战略方向任务（进攻时不足以担任战略任务），可部分修理战具，有独立可持续作战能力，独立战略机动能力（独自长途行军）。</p>
<p>（5）战略层面。行军司为基本战略单位，可承担一个（防守时为数个）战略方向的进攻任务，具有自行补充员额、征集粮草、打造修理战具、临时任命地方守吏等能力。</p>
<p>3、骑军编制依据及战术目的：</p>
<p>因御营骑军中，甲骑为全新组建，轻骑统领李世辅对官家忠心耿耿且战功卓著，兵员来源党项人多为新征服或延边熟蕃，故整体从上到下整编较为顺利，基本规整化。</p>
<p>（1）伍与什，为基本编伍单位，行军、宿营时等为基本单元。</p>
<p>（2）队，为基本游弋单位（斥候除外）。</p>
<p>（3）都，为基本作战单位，可布设锋矢阵、鱼鳞阵等基本作战阵型，发挥骑军独特作战效能。</p>
<p>（4）指挥，为基本战术单位，可以都为单位，聚散成阵，执行正面冲击加侧击等较为复杂的骑军战术。</p>
<p>（5）、统制部，已可承担为大军前卫、骚扰粮道、追杀溃军等具有战略意义的任务，具备战略能力。</p>
<p>（6）御营骑军行军司，完全战略能力部队，具有多重战略意义及能力。</p>
<p>三、绍宋中的兵种组成</p>
<p>1、传统宋军兵种组成</p>
<p>宋军传统上兵种有两大类，即骑军与步军。</p>
<p>骑军下分为枪骑（着铁甲，非重骑）、弓骑（着皮甲）两大类。</p>
<p>步军下分为枪手、刀盾手、弓手、弩手四大类。</p>
<p>从《武经总要》前集卷2看，北宋时的军训队列，一般都是枪刀居前，弓弩在后。这种队列式是接近实战要求的。南宋也有类似记载。名将吴璘的军令规定：“逢敌欲战，必成列为阵，甲军弓、弩手并坐。视敌兵距阵约一百五十步，令神臂弓兵起立，先用箭约射之，箭之所至，可穿敌阵，即全军俱发。敌兵距阵约百步，令平射弓兵起立，用箭约射如初，然后全阵俱发。或敌兵直犯拒马，令甲军枪手密依拒马，枕枪撺刺。”由此可知，枪手紧靠阵前布设的拒马之后，也无疑居于弓手和弩手之前，以便当敌骑冲阵之际，进行白刃战。</p>
<p>2、绍宋御营军兵种组成</p>
<p>通过与金军多年作战，在实践中宋军摸索出了很多经验，因金军多使用重甲硬弓，于是针对性的重新配置了各军兵种，设定如下：</p>
<p>步军，设刀盾手，长枪手，长斧手，神臂弓手，弓手（重箭硬弓）。不再设一般意义上的步弓手与弩手。因神臂弓修理及生产限制，对兵种比例对比进行了优化（详见后文），不复之前宋军一都60%-70%均是弓弩手的情况。（宋仁宗时，尹洙说：“诸处马军每一都枪手、旗头共十三人，其余并系弓箭手；步军每一都刀（盾）手八人，枪手一十六人，其七十余人并系弩手”）</p>
<p>骑军，设弓骑，突骑，甲骑，着重加强对甲骑部队的建设，主要兵器也由统一制式长枪更换成了骑兵锤（骨朵）和可破甲的槊（锋刃长而较长枪重）等，副兵器由刀剑更换为了锏、鞭、短柄锤等重武器，弓骑部队不再以弓为主要作战武器，改为主要由蕃骑编成，并统一配矛用作格斗武器。甲骑着全身甲，有身甲、披膊、臂护、垂缘、膝裙、兜鏊等，马甲主要是护胸、护颈，不像铁浮屠那样全身马甲；轻骑上只有身甲为铁制札甲，其余都为皮甲，蕃骑全身基本都是皮甲（除掩心、头盔外）。而步军中的骑卒多承担斥候作用，一般上身着半身铁甲，下身皮甲，马着护颈，皮质胸铠。</p>
<p>3、基层军制与阵型编制</p>
<p>步军以一大什为单位设立兵种，如左什为刀盾手，右什为神臂弓手等。一都2大什，一营5都，除亲兵都外，共计8大什，一般2大什刀盾手，1大什长枪手，1大什长斧手，3大什弓手，1大什神臂弓手（射速较慢且修理不易，无法大规模配置）。外加亲兵都与斥候队的100余名骑兵。作战时一般2列刀盾手在拒马后（如有时间设置拒马），1列长枪手次之，1列长斧手次之，3列弓手次之，1列神臂弓手最后，可排出50*8的阵型正面作战。</p>
<p>进攻时，则长枪手在前，长斧手次之，1列刀盾手继之。后方约50步外，1列刀盾手保护弓手掩护逐步向前推进。</p>
<p>攻城时以刀盾手为主，长枪手在城下列阵保护弓手掩护，并掩护大阵后投石机等器械。</p>
<p>神臂弓射程：《梦溪笔谈》卷19说，这是“李定献偏架弩，似弓，而施干镫，以镫距地而张之，射三百步，能洞重札，谓之神臂弓，最为利器。李定本党项羌首，自投归朝廷”。另一说则是由百姓李宏研制。神臂弓实际上也是一种弩，射程远及二百四十多步，约合372米以上，“仍透穿榆木，没半簳”，足见其劲利。“熙宁初，造神臂弓成”，宋神宗“御延和殿临阅，置铁甲七十步，俾卫士射，未有中者”。宦官张若水“自请射，连中彻札”。神臂弓不像床子弩那样笨重，只用一人发射，易于推广，“施于军事，实有奇功”。</p>
<p>骑军以都为单位设立兵种，如一都甲骑，另一都为弓骑，一营甲骑中，仍有承担外围警戒、侦查任务的突骑（轻骑）一都。一营四都，或三都甲骑一都轻骑，或一都甲骑（亲兵都）3都轻骑。</p>
<p>进攻时（骑兵只有进攻，原地不动的骑兵还不如步兵），多以甲骑为前锋，弓骑兵为两翼或侧后，突骑为中军。甲骑阵型以4-8排（再厚了没有意义）不等。后方五十步外为跟进掩护的轻骑、突骑。</p>
