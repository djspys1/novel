<p>同人12宋金战争后世记叙及相关评价——蔡有明天</p>
<p>宋金战争是指南宋宣和七年（1125年）至北宋建炎十年（1137年），宋朝为抗击金军南侵、收复失地、犁庭扫穴进行的大规模战争。</p>
<p>主要指挥官：金国:完颜宗翰（粘罕）、完颜宗望（斡鲁补）、完颜宗弼（兀术）、完颜娄室、完颜拔离速；宋朝：赵玖、韩世忠、岳飞、李彦仙、吴阶、张荣、张俊、马扩</p>
<p>结果：战争前期南宋都城被破、皇帝被俘，金国势力达到顶峰；后期华夏联军灭金，建炎中兴。</p>
<p>背景：参见“金辽战争”、“海上之盟”，金在灭辽过程中，以战养战战术运用娴熟，女真族大发战争财，具有很强的扩张意愿。同时看破南宋朝廷虚实，繁花似锦的背后是虚弱不堪，故多次实施南侵。</p>
<p>战争过程简介：</p>
<p>第一阶段：金军进攻阶段。</p>
<p>1125年10月金军第一次南下，西路粘罕进山西、东路斡鲁补出河北，东西合击企图夺取东京，最终因天下勤王兵马到来，暂时撤退，南宋割河北三镇求和。</p>
<p>影响:金军第一次南侵标志着宋金战争的开始。</p>
<p>1126年8月金军第二次南侵，粘罕再次西攻太原，斡鲁补东攻真定，11月包围东京，赵玖奉命前往金营乞和，相约黄河为界，金军不允，次月东京城破，靖康耻发生。</p>
<p>影响:南宋灭亡。</p>
<p>1127年9月，金军第三次南侵，娄室为西路军主帅攻陕西，为李彦仙所阻，兀术为东路军先锋攻京东，宋世祖以韩世忠、张俊、刘光世领兵，层层阻敌，寿州攻防战爆发，然刘光世弃地在先，陷张俊于险地在后，宋世祖亲斩刘光世，只身入下蔡，安抚军心、重申军纪，宋军士气大振，金军败退。</p>
<p>影响：金军大规模进攻首次受挫，打破了金军不可战胜的神话，北宋迎来喘息之机，韩世忠、岳飞等为宋世祖赏识，为后续反击播下种子。</p>
<p>1128年8月，金军第四次南侵，南京应天府被攻破、张所殉国，孔彦舟、张遇、折可求投降，10月韩世忠被困，11月南阳攻防战爆发，金军东西路主力顿挫于南阳、陕州。12月东京宗泽病重，以杜充往替，宋勤王军猬集鄢陵一线，但杜充不思报国、畏敌怯战，谎报军情，欺上瞒下。其子杜岩深明大义单骑至南***言虚实，次年正月，世祖仅率亲卫潜至鄢陵，斩杜充，以岳飞为将，攻完颜达懒，大捷。</p>
<p>意义：宋军首次在大规模野战中获胜，女真不满万、满万不可敌的谣言被打破。此战策应了宋军在陕西、山东两翼作战，抑制了金军攻势，为宋军反击创造了良好条件，金军短期内灭亡宋朝的战略失败。</p>
<p>第二阶段：战略相持阶段</p>
<p>宋世祖重返旧都让女真贵族意识到，短期内灭宋的豪言已成空谈，故以局部重点进攻为主，试图在长期对峙中夺取主动。</p>
<p>1130年正月，金军第五次南侵发动，此次金军以试探为主，双方并未开展大规模交战。同年3月金军仗甲兵之力，违反天时，发动第六次南侵，5月攻破洛阳，北宋枢密使汪伯彦自焚殉节，宋世祖托孤后御驾亲征。5月29日，宋金双方于尧山展开决战，此战激烈异常，两军大纛最近时相距不足百米，最高指挥官甚至进行了对射，双方士卒死伤近十万。最终金国统帅完颜娄室阵亡，宋军惨胜。</p>
<p>影响：尧山血战，沉重打击了金军主力部队，对宋军抗金战局起到了重大影响，金国最后一次大规模进攻失败，战略优势逐渐消失，宋金进入战略相持阶段，堪称北宋立国之战。</p>
<p>1131年，金军遣返宋朝被俘宗室，意图求和，北宋一面假意议和，一面调兵遣将，成功消灭伪齐。1132年，宋军进攻西夏，金军往援。9月宋、辽、蒙古进行金河泊会盟，建立统一联盟，随即西夏宣告灭亡，宋军收复河套之地。</p>
<p>影响：在战略相持阶段，宋军主动出击，剪除臣服金国的伪齐、西夏，联合抗金欲望强烈的西辽余部、蒙古诸部，迫使高丽保持中立。为战略反击扫除障碍，奠定基础，使得宋金强弱天平发生剧烈变化，坚定了朝廷内部抗金的思想。</p>
<p>第三阶段：宋军反击阶段</p>
<p>1132年11月，宋世祖召马扩奏对，问灭金策，旋即在次年开展军队扩编、税制改革。1134年世祖南巡期间，开始摊丁入亩、着手土断，史称“建炎新政”。</p>
<p>1135年6月东南公阁大会上，通过公开奏对方式统一了内部思想，确定了抗金进程。9月，北宋汇合辽、蒙古诸部组成华夏联军，发布檄文，誓师伐金，史称“建炎北伐”。</p>
<p>30万华夏联军兵分两路，采取东取元城、西取太原，会师北进策略。宋世祖亲任西路军主帅，并以岳飞为东路军主帅；金军以兀术为首，拔离速为帅。12月30日，宋军东西路军运用火药，同时攻破太原、元城。</p>
<p>1136年华夏联军、金军主力相遇于井陉，2月双方在获鹿地区展开最终决战，战事焦灼之时，宋世祖亲自过河，汇合诸部击溃金军，阵斩金帅拔离速。后张荣以水军拦截金军去路，华夏各部衔尾追击，金军大溃，北遁者十不存一，史称“获鹿之战”。</p>
<p>影响：获鹿之战是华夏联军（主要为北宋）所取得的一场巨大胜利，基本摧毁了金军主力，彻底扭转了宋金战争形势，加速了金国灭亡，奠定了东北亚地区此后百年的政治军事格局，令无数世人充满景仰。</p>
<p>文学：咏颂宋金之战期间人物、事件的诗词有很多，其中最著名的就是宋世祖亲题的《青玉案·元夕》，详请参见《宋世祖诗词鉴赏》。</p>
<p>绘画上以张择端于获鹿大捷后所绘的《东京沸腾图》以及日本国宝《方寸之间》最为著名。</p>
<p>根据宋金之战撰写的戏曲、小说浩如烟海、风格多样…。其中以《绍宋读书会》最为荒诞，后来的《高宝奇人》参考了这一风格，采取了恶趣味写法。</p>
<p>历史评价：宋金战争持续十余年之久，在当时堪称东亚霸主、乃至世界霸主之争。战争初期，金军处于军事、政治、外交的上升阶段，兵将素质冠绝当世。金国开国之主完颜阿骨打堪称人雄，为金军南下打下了坚实的经济、政治、军事基础，在灭辽期间获取了对宋战争的借口，舆论上占据有利地位。</p>
<p>在金军前两次南下期间，战术得当，充分利用了自身优势，加之南宋朝廷文恬武嬉，兵备废弛，君昏臣庸，奉行妥协政策，故获得了丰厚战果。但因自身战线过长、士卒水土不服、统治粗暴，得不到汉人认可、对占领区缺乏控制、难以获得补给，且宋朝战略纵深大，文化底蕴丰厚，未能彻底灭亡宋朝。</p>
<p>战争中期，中国历史上著名的宋世祖赵玖继位，采取坚决抵抗之策，北宋虽仅据半壁河山，但军民同仇敌忾、据险坚守，能臣、名将辈出，在寿州攻防战、缩头滩之战、南阳攻防战、鄢陵之战、尧山血战等著名战役中挫伤了金军锐气，消耗了金军大量有生力量，使北宋朝野建立了抗金成功的信心。</p>
<p>战争后期，经过灭夏、灭齐战争，“胡闳休出使西辽”宋朝建立了良好的军事、外交环境。“绍兴会议”后在宋世祖带领下北宋朝廷统一了思想步调、进行了内部改革，量的积累逐渐加快，宋金强弱天平逐渐倾斜，最终以“建炎北伐”、“获鹿大捷”为质变结果，灭亡金国，实现直捣黄龙的目标。</p>
<p>宋金战争的获胜，结束了唐末以来南北分裂的局面，国家重新统一，推进了民族融合，加强了华夏民族的认同感，社会精神风貌为之一新。有利于社会的安定和进步，促进了北方经济的发展，使得南北方经济发展重新均衡，为“绍兴盛世”的建立奠定了基础。</p>
