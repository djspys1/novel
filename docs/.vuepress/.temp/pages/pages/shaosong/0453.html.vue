<p>彬父吾弟，获鹿一别无恙？向者未及一诀，而金虏已崩，兄思王事当先，棠棣为后，随虏北走，建炎以来，此番行止，再而三矣。值五河沦陷，王师挠败，同侪皆没，身为俘囚，然心存大志，忍辱偷生，卧薪尝胆，思图报效。虽天涯海角，山穷水尽，但此身不灭，则吾道必存。今日回顾，乡里俨然，天子雄杰，必有中兴，可称虽死无憾。</p>
<p>余自陷虏营，艰苦日随，剃发易服，血泪满心，观渊而不嫌水冷，志怀北海；目刃而未遇头瘙，心思宫雁。生人之苦，俱已受矣，以惶惶华夏，为腥膻穷囚，寒来暑往，白驹过隙，日落月生，斗转星移，念故国亲朋，倚门而望，思先冢坟茔，松柏青青。煎心熬血，恍惚累恸，嚎啕则闻者蛮夷，泣涕则泪落异土，惶惶终日，辗转彻夜，梦寐之中，魂悸之时，唯倚窗南望，可安一心。</p>
<p>今四海大定，家积余庆，圣主寿康，国祚绵永，戎行有终，治世期临，而仆身不能沐，目不能睹，茕茕孑立，力竭计穷，身遭白刃，死刑虏俗：血祭血神，颅献颅座，一葬草野，一累京观。或有异日，君来燕京，念有旧情，使亡魂归乡，骨为宋鬼，已足见哀矜，更复何望？另有一作《风筝》，盼得君传：蜩鸠宿曲蒿，落凤栖梧桐。夜静岚鸣起，弦声响碧空。知音解曲意，忍辱作北歌。自落青松顶，何衔桧木红。(本章完)</p>
