<p>声声慢-思君恩宋-教坊集</p>
<p>春风去矣，梦远山青，黄鹂幕外声起。燕子穿帘还照，芭蕉声里。孤花片叶断送，坠碧波，水荇长翠。岁岁有，镜前妆，一晌欢愉难忆。</p>
<p>此恨无端能寄，何处觅，栏杆帷帐遗事。汴柳栖鸦，几阵落花满地。行云问谁瘦损，想当年，应是奴辈。怎忍听，更漏滴，瑞脑香坠。</p>
<p>夹河对峙，中原大定，耆老颂曰：自灵祚南迁，祸缠神土，二圣失统，豹狼纵毒，苍生遭兽食之悲，旧都哀黍离之痛。帝以庸庸，欲行昭昭，乘寇虏天亡之期，借两河思奋之愤。义风一鼓，率土响同，威声既张，士卒效勇，殄逆鳞于南阳，扫凶迹于长社，非仰协天恩，俯允群愿，焉能若斯者哉？今东虏遗虐，连兵燕云，东西狼顾，威形连接，致使关西孤危，京东不戍。当简贤讲武，授以威仪，约以兄弟，契以死生。集豪杰忠义之心，复祖宗雄略之迹，练甲进师，结驷连骑，提戈载驱，北出太行，进可以捍凶寇，覆其巢窟，退可以宣国威，镇御旧京。天威既震，奸虏溃亡，遗民小大，咸蒙生造。使汴梁有鸣鸾之响，荒余怀远来之德。此亿兆所望，仁者之邦也。</p>
<p>檄诸完颜</p>
<p>圣君有伐恶之责，王者有定乱之任，故追迈三代，黄帝兴师，览及今日，天兵屡伐。上承嗣明道，欲补金瓯，还苍生以正朔，推华夏于四方。仰太一昊天之灵，凭亿兆肱股之力，自东及西，凡经四战，栉风沐雨，尔来五年。中州已定，淳风遐举，然玉门之使，皆遭张骞之劫，通辽之人，悉蒙苏武之难。此丧道毁德之行，恶名极于西海，妄动干戈之事，污秽闻于契丹。又猛安谋克，肆行圈占，高屋建瓴之州，遍地抔土，泰半九州之地，累遭劫掠。赋敛繁重，人财枯竭，饥者满道，愤者盈途，耆老稚童，皆南望圣泽。古旧都之中，夙夜北顾，切齿泣血，筹谋犁庭。兹尔东虏，桀虐冥顽，上行下效，狼心狗行，秦桧不轨，无忠义之节，兀术不道，逞残忍之志，虽加恩礼，欲壑难填，不思自去，反怀残诈。百代之寇，难寻罄书之竹，华夏之敌，未有无耻尤者。今文武兼备，攻守易形，退保黄龙，或苟延残喘，盘踞燕云，必雷殛无形。中兴诸将韩世忠、李彦仙、岳鹏举、张伯英，并吴玠郦琼，王德张荣，皆威贯日月，奋勇争先，御营上下，皆有虎兕之勇，大河东西，联营迅雷之击，扫浮屠若摧枯，**拐马若拉朽，克复燕京，易如反掌，超迈汉唐，计日可程。若肉袒宫门，顿首请罪，则可留全尸，以示宽仁，如冥顽不灵，兴兵抗甲，则代天诛戮，刑如蚩尤。</p>
<p>谕武林群贤书</p>
<p>《尚书》有云：“知人则哲，安民则惠，能哲而惠，何忧何欢，何迁何畏。”故文王开囿，周室兴隆，汉高推衣，刘姓祚永。圣君治政，莫不如是。吾沧州一脉，累世用武，习将略于北土，建殊勋于多朝，及太祖践祚，英气不减，廓清瓯宇，期复唐业。然天不假年，行于烛影，赫赫隆绩，断于斧声。列圣以降，休沐敦平，居重御轻，崇文制武。此有功于治下，无颜于四海也。及靖康大丧，长河如毁，柱石倾颓，天下板**，八荒涂炭，黎庶蒙灾，寇虏纵横，计无所出，以致君臣惶惶，自缚称降，举众亿万，甘为后主。伯纪气度雄阔，应时而出，怀尚父之奇才，追葛公之宏略。深谋远虑，务求拯溺；每思宏图，志在解悬。朕感其拳拳，授以执政，纵横联袂，纠合义心。后虽分居南北，天下之重，犹在于兹。下蔡之外，南阳之郊，鄢陵之野，尧山之胜，贺兰之捷，河套之盟，汲汲数年，所称非常之业，不世之功者，微伯纪靖康一呼，皆冰消雾卷，东京梦华矣。当满朝蠕蠕之时，首倡抗金，会天下惶惶之世，身定人心，立中兴之基，退江湖之远，盘古以来，盖亦鲜矣。一臣之力，震古烁今，尚萧葛猛，并为同侪。</p>
<p>往以皇嗣夭折，内有怨愤，奸佞小人，乘隙进谗，以致众口铄金，积毁销骨。赖祖宗福佑，醍醐灌顶。追悔前过，复念元功，当武林秋日，群贤毕集，湖山为证，众目为凭，李纲之功，与国同休，赐君铁劵，以示尊容。诸卿之中，亦有类者，克难克艰，东京忠武，耀武扬奋，彦仙张荣，朝夕布划，岳飞世忠，太行河北，马扩英雄。今国势蒸蒸，贤良翼翼，大河以南，可称晏然，长江尽极，未闻五兵。然生民亿万，盼王师于塞北，冀减赋于江南，若无北伐，何日有终？故立功燕云，有德于亿兆，释民生息，亦大于正气。</p>
<p>与宴之人，或明道从朕，共披荆棘；或有心社稷，殊途同归。当共定山河，悉心辅弼，无党无偏，襄举太平！</p>
<p>北伐诏</p>
<p>汉武北伐，单于归德，唐宗亲征，渤海泰宁。蕞尔东虏，不沐王化，契丹中衰，乘衅逞凶，塞北雄国，社稷倾覆，虽英雄续祚，故土难归。</p>
<p>皇宋二竖，君臣奸庸，亿兆赤子，疲于私欲。虏酋北来，凶顽纵虐，险肆横流，金瓯累卵，而二竖宠溺亲党，咀嚼黎庶，重役繁纲，累增大赋，汴梁梦华，冠盖高第，俯首系颈，黍离倒悬，淮扬数路，饿殍盈途，人人北望，思复故土。</p>
<p>明道命世，国运革鼎，大业草创，潜龙在渊。虏酋遗种，欲壑难填，前夺两河，又窥中原，残破京东，肆虐淮北，光世统军，乘隙作乱，贪愚弱私，八公伏诛。</p>
<p>下蔡战后，贼势仍张，外敌内寇，并为祸患，推心忖理，暗必勾连，故有确旨，明定敌我，而方定行在，新旧反复，同侪相恶，倾轧如雠。朝堂合力，非威非诡，以堂皇大道，经鄢陵、尧山、贺兰之威，行宫门、白马、杭州之事。今天降感应，地现表徵，殄灭之期，彰于金圭，定乱之机，显于九月，时和岁稔，政通人豫，华夏同心，宋辽一体，遂命将定帅，总兵大举，混一宇内，同贡禹迹。</p>
<p>岳飞铁卒七万，先定大名，张俊武勇五万，扬旌河济，曲端铁骑二万，直趣泽潞，彦仙精甲六万万，霜锋曜晋，王彦羽林五万，随驾入洛，吴玠剽锐八万，抚定大同，腰胆熊罴十万，经河入汾，张荣海鳅万舳，径掩辽东，王德虎贲五万，泛彼孟津，马扩骁果六万，传檄两河。将帅十员，熊虎百万，迤逦行军，当有总制，专任中外，统辖群雄，枢副颐浩，可进归德节度使，督河北河东燕山诸路军事。</p>
<p>拔山之力，覆海之威，岳动川移，风驰电掣，铁马弥野，楼船塞洋，诸路并进，同会燕京，戡乱翦凶，宣仁风于塞北，屠鲸戮鲵，抚遗民于燕赵，内外九州，共济覆金大业，华夏万民，同享绍宋盛世。此檄，沧州赵玖。</p>
<p>伪金解州知州完颜皋谥抗缪公墓志</p>
<p>天有罡煞，人有贤愚，国有兴替，代有盛衰。愚公有开山之志，昊天降操蛇之神，太行居中国之脊，一岳分两河之地。公初氏石，生太行东之定州，死太行西之安邑，其生也，腐草萤光，其死也，泥丸入河，为宋人，为金鬼，呜呼！竖儒之泥古，无乃过欤？</p>
<p>但以积善之家，必有余庆，积不善之家，必有余殃，可得言矣。皋为人也，生禀庸常，本寡鲜之人，长有短才，遇无识之主，百无一用，所赖者唯“浩然气”也。然追根究本，活一州百姓，焚豪杰名册，谓有小节，扼孤城而守，抗北伐天兵，失其大义。</p>
<p>夫当今之世，非所谓“君择臣，臣亦择君”时，何哉？华夏乱世，有春秋战国，三国两晋，残唐五代，有有义之人，无有义之邦，士人蹑乎其间，以逞志展才为任，人主居乎其位，以破国屠军为先。今天子兴六师北渡，旌甲望燕云而发，乃复山河、全金瓯、驱顽虏、解倒悬之举，皋死而不降，以一城生民，全一人名望，虽二竖弃天下于先，有无奈之降，殊可怜之，当绍宋中兴之时，不思报效，殊可恨之。</p>
<p>仕金而守其土，有忠，然金土实宋土，故无忠，任职而救乱民，有义，然救标而不救本，故无义。</p>
<p>此有忠无忠，有义无义之人，天子特旨，显戮其尸，立碑撰志，以儆后人，永为世戒。《谥》曰：逆天虐民曰抗，名与实爽曰缪，以其完颜氏一部之忠臣，特加行军猛安衔。</p>
<p>故曰：完颜部行军猛安完颜皋谥抗缪挫骨扬灰于此。</p>
<p>此志皇宋中书舍人梅栎。</p>
<p>大石略定钦察诸部，至窝瓦河、敦河夹河处，以其扼居冲要，乃设西北路招讨司，以船运波斯道条石筑塞于此，名新可敦。城成后，积年加军威于斡罗斯，彼合军来攻，大石亲破之，虏王公数十，曰：朕东土天子，国名大辽，有镔铁之意，巡狩至此，有责牧守，诸邦当勤修贡礼。诸王公遂以土语称其国曰斯大林，其城曰斯大林格勒。及丝路大通，泰西行商，多会于此，东风西渐，自是而始。</p>
<p>后十年，辽立碑于城中，其文曰：</p>
<p>天成日月，各有其规，地升万物，自有其理，人主之行也，非徒顺逆而行，当制天命而用之。故遍览圣君，尧舜以降，皆正纲序常，经天纬地，内怀春风之心，外降礼乐之霖，法宇宙以定规，任群贤以教化。故可以移风易俗，安四海夷夏之民，垂拱而治，固中华万国之本者，礼乐也！</p>
<p>夫礼乐者，非雅音歌华，金玉雕饰，文章锦绣，钟磬大声也。周公所传，亲亲尊尊，秦汉所续，威威凛凛，魏晋以降，雅骚胡俗，干戈四纪，乃有造极。其成者，巍巍乎巨唐，其继者，煌煌乎契丹。</p>
<p>天子东来，驻陛河中，兵布四野，威行八荒，非广利耀兵掠马，实定远合民兴业。仰华夏列祖之名，藉东西群臣之力，上下同志，遐迩俱安，东连丝路于汴宋，西振武名于黑海。方圆万里，生民亿兆，虽山川殊异于东土，而风月同属于中国。故礼乐之制，不可不行。</p>
<p>乃尽竭圣明，作礼定仪，广纳贤德，索引散佚。开敦煌之名窟，刻雕版于西土，述六经之微言，译历代之精菁，旧典掩于沙者，发而宏之，西籍利于国者，推而广之，彰堂皇大道，振淳淳文风。以万心为一心，合众情为共情，万里之外，又造一华夏，非精诚之所致，何金石之为开？</p>
<p>今典行十年，特开恩科，各邦各城，同勒先圣之碑，诸族诸姓，同倡华夏之风。</p>
<p>议定都燕京札子宋-梅栎</p>
<p>古圣有逐牧之制，殷商有迁都之俗。昔钛祖建基，属意于洛，钛宗谏箴，遂定于汴。虽有众京之设，而宗庙定一，乃安居枢纽，居重御轻。然二周以降，两汉巨唐，莫不双京，独我朝荟萃于一，以至遭逢二竖，酿祸靖康。</p>
<p>夫天无二日，国无二主，位无二上，人无二首。臣追览周制，岐山有后稷之庙，镐京有文武之殿，洛邑有周公制鼎，镐庙成则岐庙迁，洛邑建而庙未立，故建朔立庙，不可乘一时之强，而贻祖宗子孙危，是以巨唐诸君，虽窘迫就食于洛阳，未尝稍动庙寝于长安。</p>
<p>我钛祖建朔汴京，乃天下未定，以强居中，昆弟不肖，未竟全功，诚可憾也。今天下将安，当另择京邑，福泽子孙。臣览天下形胜，以为燕山、太行、秦岭，恰中国龙脉，仰连昆仑，俯尽大海以长安为首，晋阳为腹，燕京为尾，故周、汉、唐莫不建都长安，而经营晋阳，以安天下。今长安残破，晋阳已隳，则当居龙尾，定都燕京。</p>
<p>今天子绍宋，殊异于前，汴梁犹周公之洛，燕京乃文武之镐。天下知文武周公者，必知钛祖当今之心，前宋绍宋之业，一脉相承，未有异也。若居燕京，上应天命，下顺旧规，内制六合，外控八荒，抚燕山而连大海，形势尤胜关中，此子孙帝王万世之基。我华夏历朝之祖，岂不欢欣？</p>
<p>古人立主依神，立廟依主，而子孙之身，又祖宗所依。今日正当定都专庙于燕，使绍宋社稷，专主于此也。</p>
<p>谕东土契丹书辽-耶律大石</p>
<p>圣主御极，虽自天授，任相命将，当以赤诚，君臣休戚，与共不易，有此初心，方为君始。夫汴寇诸人，寡廉鲜耻，匡胤不忠，棍临孤寡，光义不孝，背盟金匮，后辈不仁，三易回河，讫于赵构，可称不义。昔靖康之难，国家崩摧，阖家北狩，惶惶如犬，赖草莽之中，贤才响应，四海之内，英雄影从，椎心泣血，焚膏继晷，方有中兴之势，明主之称。今获鹿大捷，天地同贺，封王十八，豪俊列座，皆精诚感神，随侍十载，志在兴邦，扈驾御前。帷幄之中，长策制敌，战阵之上，所向无前。收两河山川，复汴梁冠盖。毁家纾难，赤心报国，披坚执锐，每战先登。逢战则喜，遇敌必逆，分领王师，镇抚夷狄。今扫清妖氛，**平海隅，正当息戈放马，共享太平，奈何凯旋式上，陡生疑隙？流言初生，人思杯酒之典；邪论又起，或论武襄之丧。</p>
<p>方忆淮上树旌，人神同鉴，共论君臣际遇，有始有终。孰料天兵北讨，勠力王事，突遭将从中御，风议陡生。纠本溯源，竟自帝发，使人抚掌长叹，果如宗公所言：当今天子，赵氏血脉，虽有振作，实为私心，自任颇类其父，无德直追太宗，有负功臣，大欺天下，为人君者，安能此乎？</p>
<p>吾闻庙定宏图，欲安塞北，今日反侧，必有迁延。赵构轻佻，操履非正，绝恩弃义，违誓背信。国族诸将，久在汴营，逢遭此变，当知寇心。久从此贼，终必无成，一朝祸起，变生肘腋。幸福祸相依，收有桑榆，诸将克复上京，远守陵寝，勋书丹青，像列云台，当携民西行，远避纷扰，厚爵荣禄，一如故旧，青牛白马，共为鉴证，苍狼松漠，同观宣示。</p>
