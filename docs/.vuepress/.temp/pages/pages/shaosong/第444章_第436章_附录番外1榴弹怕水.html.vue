<p>第436章附录番外1——榴弹怕水</p>
<p>“这是何物？”</p>
<p>中午时分，碎叶水畔，秋风萧瑟，野火渐熄，一身素衣的萧塔不烟双目微红，略带警惕的看向了身前的萧斡里剌。</p>
<p>“回禀太后。”</p>
<p>西辽六院司大王、兵马都元帅萧斡里剌低头相对，其人手中赫然抱着一个两尺见长、一尺见宽的精致上锁木匣。“此乃先帝在时，与大宋皇帝书信往来收录……每一年都由先帝亲自持旧钥新锁来换，并将之前一年书信放入……先帝生前有言，待他驾崩后收拢骨殖之日，若太后在，一定要太后来与臣一起看；若太后不在，一定要陛下亲启，然后由臣读给陛下来听。”</p>
<p>萧塔不烟微微放松，同时也想起丈夫死前确系留有一串钥匙，便匆匆着人去取。</p>
<p>不过，就在君臣二人等钥匙的时候，场面上虽然有近百文武臣僚，还有数千兵甲环绕，却还是不免陷入到了某种紧张而又悲伤的沉寂之中。</p>
<p>悲伤当然是因为今日乃是实际上的西辽开国君主、名义上的辽国第十帝耶律大石火葬兼收拢骨殖的仪式。</p>
<p>但紧张，却出自于此时在场两位最大权势者的某种相互忌惮——小皇帝耶律夷列年纪尚小不说，太后萧塔不烟只是肃立不语，而萧斡里剌也只能在一侧抱着匣子不动。</p>
<p>平心而论，萧斡里剌与萧塔不烟非常熟悉，一个耶律大石最信重的皇后，凡十余载，多有在耶律大石出征时负责执政，一个是耶律大石最信重的大臣，担任兵马都元帅兼六院司大王……而且双方还是儿女亲家（耶律大石只有一子一女，女儿就指给了萧斡里剌的长子）……没有理由不熟悉。</p>
<p>甚至更进一步，双方都姓萧，虽然不是亲近同族，但同出述律萧氏，本有香火之情。而萧塔不烟当日能在耶律大石一开始称汗时便成为王后，也免不了有西辽开国过程中二号缔造者萧斡里剌的帮助。</p>
<p>但是，此一时彼一时也。</p>
<p>现在，因为常年征战和奔波而早就撑不住身体的耶律大石发病死了，儿子又年幼，萧塔不烟按照辽国传统，女主当政，改元咸清，首先要面对的最大不稳定因素兼最直接威胁恰恰就是萧斡里剌这个六院司大王兼兵马都元帅。</p>
<p>须知道，西辽国制，遵循昔日大辽体系，分为南北两大系流，北面为中枢官，放在西辽这个体制下，基本上是汉制中枢、契丹宫帐制的混合体，直接统辖碎叶水畔的首都虎思斡鲁朵与绝大部分契丹-奚-汉-女真等所谓的故国众；而南流为分派官，直接负责高昌、东喀喇汗、西喀喇汗、花剌子模在内的数十个大小属国。</p>
<p>内外分流和防范还是很明显的。</p>
<p>这种情况下，萧斡里剌不仅是兵马都元帅，还是统揽王族的六院司大王，其人势力不言自明。</p>
<p>当然了，耶律大石本人作为远走万里的开国君主之威望也是不可复加的，他的遗孀与遗孤同样受到了宫帐军与根本部众的拥护。</p>
<p>总而言之，主少国疑，母后临朝，权臣执军，而且国势还这般特殊……也由不得二人这般尴尬。</p>
<p>钥匙很快送来，尴尬的沉默也被打破，周围的契丹贵人们，包括几名奚-汉-女真近臣，也都早早竖起耳朵，想知道先帝耶律大石与那位赵宋官家到底说了些什么。</p>
<p>匣子的锁被成功打开，里面拿出了足足十二摞、林林总总百余封书信，而且有的信格外之厚。</p>
<p>按顺序读了第一封，果然是当年赵宋官家遣如今的兵部尚书胡闳休前来面谒结盟，邀请夹击西夏的那封著名书信——赵宋官家信中直呼耶律大石与西辽部众为丧家犬，而彼时在场之人，就包括了眼前的西辽都元帅萧斡里剌与上午还曾露面的大宋驻西辽使节梁嘉颖，大家都是知道的。</p>
<p>但也有不知道的……此时读来，众人才醒悟，原来那位官家居然也在信中自称为丧家犬。</p>
<p>昔日之事，考量着两个皇帝后来的成就，早已经成为传奇故事，而故事中的一个主角却又刚刚亡去，偏偏其他人全都尚在，其中似乎还有些秘辛……读起来既有些让人伤感，又有些奇妙的史诗之意。</p>
<p>总而言之，由于这些信件既是当世最尊贵之人写给第二尊贵之人的书信，同时也必然包含了一定的先帝遗言转述，所以没有人敢轻视这些信的政治含义，但是偏偏书信太多、内容太杂，所以经过萧塔不烟与萧斡里剌的商议后，还是有数名通晓文字的近臣上前，协助阅读整理。</p>
<p>可即便如此，从中午读到天色昏暗，也没有在耶律大石骨殖前读完。</p>
<p>所以，众人不得不再次封上匣子，却是太后执匣，都元帅执钥，约定回宫之后，翌日再来齐读，眼下先奉先帝骨殖归城，请僧道小心供奉，以方便数日后按时出发，按照先帝遗言归于临潢府安葬。</p>
<p>而翌日中午，书信终于通读完毕。但说句良心话，大部分书信其实都是又臭又长那种……里面充斥着那位赵官家乱七八糟的叙述，从常规的问候到一些乱七八糟的诗词，从一些得意洋洋的赵宋朝中政策推行到家长里短的抱怨，甚至里面还有一些奇怪的手绘动物。</p>
<p>当然，其中也的确有内容能够呼应两位皇帝的一些著名事例，比如说八年前那场著名的建炎北伐过程，以及后来这位官家花费七年修黄河、迁都的过程。</p>
<p>甚至还有一封信里，明确记录了这位赵宋官家勉励西辽皇帝耶律大石放手与塞尔柱突厥人一搏以定西海霸业之言语。</p>
<p>如果不是这封信，包括萧塔不烟与萧斡里剌在内的西辽核心大臣们死活都想不到，当日战中指挥若定、信心满满的先帝耶律大石，居然在开战前数月还对塞尔柱突厥人的强大感到忧心忡忡，以至于一度犹豫要不要避战，然后等待赵宋援兵。</p>
<p>至于最后一封信，就更加让人感慨了，信中只有一句话：</p>
<p>“旧都河畔芦花正开，大石兄可缓缓归矣。”</p>
<p>结合日期和前文，想到彼时赵宋遣使送药的情状，众人哪里不晓，这是耶律大石自感时日无多，有心想生归故里，结果或者是病发突然，或许是碍于西辽大局稳定，最终放弃了这个决定，转而要求进行火葬，收拢自家骨殖归葬临潢府。</p>
<p>“哀家还是不懂。”</p>
<p>萧塔不烟沉默许久，才放下最后这一封信，然后环顾周边，认真来问。“先帝为何要我们来读这些书信？”</p>
<p>回应这位太后的，也是一段沉默。</p>
<p>“太后。”</p>
<p>片刻之后，还是有人出言了，却是御前腹心部副统制太师奴。“臣冒昧，刚刚悉心来听，察觉到有两处要害的地方……”</p>
<p>“仔细说来。”萧塔不烟当即抬眉示意。</p>
<p>“首先，乃是赵宋官家于我朝大胜后索求河西六州西夏故地之事……信中言语随意，而从后续书信来看，先帝也没有任何迟疑……想来此事与我等昔日所想并不一样，乃是两位陛下早有心照不宣之约。”面颊上还有流放刺字的太师奴认真分析。“这应该是提醒我们，不要把这件事情当成什么羞辱，过分在意。”</p>
<p>萧塔不烟想了想，一时没有言语，只是去看其他人，待看到其他人文武，无论女真还是汉人全都颔首后，这才跟着点了下头：</p>
<p>“不错，是有这个意思……还有呢？”</p>
<p>“还有一件事，乃是陛下去年时便感到身体不行，曾一度忧虑，而赵宋官家的回信中虽然也多有慰问，但更重要的是，信中居然反加了一段警告……结合这这封信后先帝立即发动了对三姓叶护的铲除……想来，先帝既是认可了赵宋官家的意思，也是意识到赵宋官家言语绝非儿戏，同时怕也是在暗示太后与都元帅，这便是赵宋官家维护两国乃至于大辽统续的底线……”</p>
<p>“将那封信取来。”萧塔不烟闻言一振，旋即下令。</p>
<p>而片刻后，立即有近臣捡出那封信，找到那一段，然后由当众读来：</p>
<p>“大石兄何其陋也？突厥之广，岂是突厥血统昌盛？实在于突厥统辖海西数百年，居高临下，故杂胡野种莫不附之，遂有突厥化之滋生，至于入目皆如三姓叶护自诩突厥者也。</p>
<p>比起类者，中国亦有，昔鲜卑之强，高欢汉种而尽习鲜卑，中华之深，刘渊、宇文泰胡种而尽习汉化。今宋辽何以为兄弟之国？互托脊背，在于大石兄以汉文与朕通信，在于宫帐皆言汉语，在于大辽上下皆知儒释道……</p>
<p>若有朝一日，大石兄真有不测，而辽帐皆弃汉从胡，弃儒从伊……则两国虽血缘可数，亦生死敌国也！届时愚弟虽不才，亦可提东西蒙古十万众，仿大石兄昔日西进之举，以清理西海！</p>
<p>反之，虽大石兄不敌天命，而西海河中秩序井然，宫帐亦遵祖宗之法，则大辽虽有万一倾覆之虞，愚弟亦可提十万众，往援河中，使辽朝国祚不断，耶律氏血脉不停！</p>
<p>此所谓根本之事，勿谓言之不预也！”</p>
<p>众人听完，愈发严肃，稍作讨论，都觉这正是耶律大石一定要众人来看的缘故。</p>
<p>至于之前一时忽略，乃是因为在场之人多是‘旧众’，也就是从东面过来的……甭管是怎么来的，一开始跟着耶律大石过来的，还是后来投奔的，又或者是太师奴这种遣送的，乃至于战俘，全都是说汉话、信仰儒释道三教合一的，一直如此，所以并没有把这件事情当做一个‘警告’。</p>
<p>“萧大王以为如何？”萧塔不烟思索再三，看向了萧斡里剌。</p>
<p>萧斡里剌稍作沉默，然后诚恳开口：“太后，恕臣直言，其实先帝的意思已经很明显了，只不过太师奴将军等人碍于身份不好直言，只能说一半留一半罢了……其实，先帝只有两个意思。”</p>
<p>这次轮到萧塔不烟沉默静待了。</p>
<p>而萧斡里剌也没有卖关子，只是稍微一顿便说了下去：</p>
<p>“一则，宋辽之盟乃是立国根本，不可轻易动摇……所谓河西六州故事、先帝骨殖归于临潢府、铲除三姓叶护、赵官家十万众之警告，都是这个意思……所以臣以为，坚持国家大政之余不妨摆出个姿态来，请赵宋官家的一封天子敕封过来，就算是叔封侄了，并不至于丢了体面，想来燕京那里也不会真的有什么为难逼凌的。”</p>
<p>“那就派一使臣专务此事，随先帝骨殖东归。”萧太后稍一思索，便直接应下。</p>
<p>“太后明断。”萧斡里剌赶紧应声。</p>
<p>“这一条应该便是大王的‘说一半’了，那敢问‘留一半’的又是什么？”萧塔不烟继续来问。</p>
<p>“请太后明鉴……盟约稳固如宋辽之间，犹然有‘十万之众’的言语，那敢问太后，我大辽位处西海，到底什么是立国之本？”萧斡里剌恳切来问。</p>
<p>萧塔不烟闻言，终于失笑，然后复又一时哀伤喟然：“哀家懂得先帝的意思了，也晓得大王与诸位臣僚的一片苦心……”</p>
<p>言至此处，尚在缟素中的萧太后站起身来，环顾四面，正色言道：“众所周知，本朝名为大辽统续，其实是远走万里重新立国，去年统计户口，虎思斡鲁朵‘旧众’不过二十四万户，以二十四万户的根本来统揽万里之境，自然是战战兢兢如履薄冰。而外面最大的倚仗，也就是大宋这个盟友都有‘十万之众’的言语，可见联盟固然重要，但外事终究是只是外事，真正内里倚仗，只有咱们自己罢了……诸卿，先帝让我们看这些书信，一来固然是提醒咱们务必要维持盟约，但更重要的，乃是怕他一去之后，国中争权夺利，失了团结一心辗转万里立国的那股心气，乃至于徒生内乱，大厦自倾，所以专门警醒！”</p>
<p>“太后圣明！”</p>
<p>都元帅萧斡里剌听完之后，立即后退数步，当场朝着萧太后下跪，然后从腰中取出匕首来，划开手掌，指天而对：“国家沦丧，先帝辗转数万里，遂有西海河中之基业，臣一丧家之犬，受先帝大恩，随从西征，得封元帅，位列大王……此生此世，必当奉先帝骨血为正统，若有丝毫背离，当生不得好死，死不得归乡好葬！”</p>
<p>其余臣僚，纷纷醒悟，无论契丹奚汉女真渤海，纷纷下跪立誓，以示团结一心。</p>
<p>四月之后，隆冬时节，赵玖在燕京等到了耶律大石的骨殖棺木，其人五味杂陈之余，却是亲自出城相迎，却又在诸多早有预料的外交事务之外，诧异的接到了一封‘回信’。</p>
<p>打开信来，只有寥寥一句话而已。</p>
<p>正所谓：</p>
<p>“陌上花开，自当缓缓归矣，然关山难越，谁复悲失路之人？”</p>
<p>落款有两个，分别是：‘大辽太后萧塔不烟敬安’，与‘大辽兵马都元帅萧斡里剌执笔’。</p>
<p>赵玖看完，足足在寒风沉默了一炷香的时间，方才回过神来，然后只将书信从容收起，便回顾随行枢密院副使岳飞：</p>
<p>“大石虽死，西辽国运未衰，倒不如先定大理。”</p>
<p>岳飞自然拱手称是。</p>
