<p>《淮河神币》</p>
<p>建炎年中，淮南张三以捕鱼为业。</p>
<p>是日，沿河下，遇一鱼自水跃统至舟上，</p>
<p>渔大喜，</p>
<p>此鱼洞赤，身长九寸，口不能合，</p>
<p>内有异物，乃一币。</p>
<p>取观之，亮若银，</p>
<p>一面，有怪象，上书“中国人民银行元”，</p>
<p>一面，雕菊，栩栩如生。</p>
<p>渔人大喜，每逢人即吹嘘之。</p>
<p>日久矣，此事遂至于县，县君使人取之，</p>
<p>大惊，叹曰“此非凡间物也！”。</p>
<p>贡送至京师，不知所踪。</p>
<p>或曰遗矣，</p>
<p>或曰京中王贪腐矣，其后不知所踪。</p>
<p>宋末，南淮反，有人自称“上币仙”，旗帜为“元”，举旗造反。</p>
<p>——《聊斋志异》蒲松龄</p>
<p>《淮河神币》译文:</p>
<p>宋朝建炎年的时候，淮南有个张三以在淮河捕鱼为生。</p>
<p>有一天，沿河南下，遇到一条鱼自水面跃出落到了船上，张三狂喜。</p>
<p>上前去一看，发现这条鱼通体赤红，长约九寸，但是这条鱼嘴巴无法闭合。</p>
<p>原来这鱼嘴里有一枚银币。</p>
<p>银币一面画有奇怪符号，上面写着“中国人民银行元”，</p>
<p>另一面，雕有**栩栩如生。</p>
<p>渔民很开心，便将这枚银币带回家中，每次遇到人便吹嘘这枚银币的神异之处。</p>
<p>时间久了，这件事便传到了县令耳中。</p>
<p>县令便派人索取了这枚钱币，惊讶无比，感叹道“这银币不是人间之物啊！”。</p>
<p>后来，此钱币成了贡品，被送往京城，不知所踪。</p>
<p>有人说是遗失了，有人说是被京中某位郡王贪没了，此后下落不明。</p>
<p>宋朝末年，淮南有人自称“上币仙人”，号“元”，举旗造反，不知和那神币又有什么关系了。</p>
<p>《张生》</p>
<p>宋建炎十七年，即墨有张生，病觉问今是何时。</p>
<p>妻曰：“今为建炎十七年”。</p>
<p>张生疑问“天子何人？构乎？”。</p>
<p>妻骇，见左右无人曰“官家名讳何以直言！”。</p>
<p>张生复问：“金人何往矣？”。</p>
<p>妻笑曰：“七年前以灭国亡种，名为清。”</p>
<p>张生疑，问数事，大喜。</p>
<p>谓其妻曰：“官家吾之乡人矣，将达哉！”。</p>
<p>乃携妻子往京，言己与官家乡人，辱世祖文过抄也，功不过尔尔，我亦可一扫天下矣。</p>
<p>后遇盗，毕。</p>
<p>古往今来，跳梁小丑岂是一人尔？</p>
<p>——《聊斋志异》蒲松龄</p>
<p>《张生》译文:</p>
<p>宋朝建炎十七年时，即墨有一个张书生，大病醒来后问妻子现在是什么时候。</p>
<p>妻子回答说:“现在是建炎十七年”。</p>
<p>张书生疑惑又问“天子何人？赵构吗？”</p>
<p>妻子吓了一跳，</p>
<p>看左右无人说“官家的名字怎么能直接说出来！”。</p>
<p>张生又问“金人去哪了？”</p>
<p>妻子笑着说“七年前以被灭国亡种，现名为清。”</p>
<p>张生疑惑，去打听了很多往事，狂喜。</p>
<p>对自己妻子说“官家是我的老乡，我们要发达了。”</p>
<p>于是带着家眷前往燕京，路上言说自己和官家同乡，诋毁宋世祖文采不过抄袭的罢了，功绩不过平平，我也可以一扫天下。</p>
<p>后来路上遇到了盗匪，一命呜呼。</p>
<p>古往今来，跳梁小丑又何止这一个人呢？</p>
<p>《井中狸》</p>
<p>淮南亳州有庙，名曰“明道宫”。</p>
<p>院中有井，名曰“九龙井”，宋世祖坠井于此，得道祖天授兵法三卷。</p>
<p>建炎元年，井中见一狸，其状似人，时威风，时哀戚。</p>
<p>左右感异，报于班值统领杨沂中，杨大骇，令左右放箭。</p>
<p>狸死书“朕乃构”而狸之尸隐，不见其踪。</p>
<p>洪武年中，有杨生过此宿明道宫。</p>
<p>夜梦一红袍男子叱</p>
<p>“朕乃构！杨贼误朕！尝朕命来！”。</p>
<p>杨生醒，不敢寐，次日天明疾逃。</p>
<p>——《聊斋志异》·蒲松龄</p>
<p>《井中狸》译文:</p>
<p>淮南亳州有一处庙祠，名曰明道宫。</p>
<p>院子里有一口井，名叫“九龙井”，宋世祖在这里坠井，梦到道祖传授兵法三卷。</p>
<p>建炎元年，井中出现了一只狸猫，它的样子很像人，时而威风，时而哀戚。</p>
<p>左右侍卫感觉很奇怪，便汇报给了都统杨沂中，杨沂中大惊失色，下令乱箭射死。</p>
<p>狸猫死前于井中写下“朕乃赵构”后狸猫的尸体便消失了。</p>
<p>明朝洪武时期，有一姓杨的书生途径此处借宿明道宫。</p>
<p>夜晚梦到有一红袍男子怒吼</p>
<p>“朕乃赵构，杨沂中害朕！还我命来！”。</p>
<p>杨书生惊醒，不敢再睡了，第二天一早便逃离了明道宫。</p>
<p>《三赵》</p>
<p>万历年间，湖广有一生名袁宗道，官至员外郎。</p>
<p>袁幼时慕诗词小说，常与人曰</p>
<p>“天下文气莫过于三曹，三苏，三赵。吾欲摆宴请古时之才子”。</p>
<p>是夜，</p>
<p>袁闻庭中喧哗，乃叱仆视，然呼数声无仆应，遂怒披衣至院中视。</p>
<p>见院中宴，酒肴备具。</p>
<p>六人分列左右。</p>
<p>左衣汉服，为长者形貌矮小，双眸狭长，然有帝威。</p>
<p>右衣宋士子服，为长者容貌屹然，雅有文气。</p>
<p>袁生异，前揖问</p>
<p>“门人袁宗道，不知几位前辈何名？”。</p>
<p>左侧长者扶须笑日：</p>
<p>“孤乃魏王曹孟德，今闻足下有宴相邀，特来讨酒一杯。”</p>
<p>继又闻右一人言</p>
<p>“不欲苏某今日遭此无耻汉贼！”。</p>
<p>两方既争。</p>
<p>袁知是三曹三苏驾临，忙上前解后七人共入宴。</p>
<p>宴罢，</p>
<p>袁奇问曰</p>
<p>“既六位前辈皆至，三赵官家于何？”。</p>
<p>苏抚须笑对：</p>
<p>“三赵著小说，无时下人间来宴。”</p>
<p>原是那天帝苦追书久已，</p>
<p>三赵官家日夜著小说不休。</p>
<p>袁慨然道：</p>
<p>“果著小说乃世间最苦之事矣！”。</p>
<p>一夜无言。</p>
<p>次日天明，</p>
<p>袁醒身卧庭中，四下杯盘狼藉，似昨夜在此设宴，仆妇都觉惊奇。</p>
<p>袁和之曰“吾仰之老前辈来客，夜深未叫汝等。”</p>
<p>仆慷慨，遂拾起。</p>
<p>万历末中，</p>
<p>荆州有吴生，</p>
<p>每至夜则梦一红袍人与之讲《西游降魔杂记》。</p>
<p>吴生不堪扰，遂改其事，名曰《西游记》。</p>
<p>书成，夜梦红袍人于梦中曰：</p>
<p>“你我二人不相负也。”</p>
<p>吴生惊，面流泪不止曰“妾负官家矣！”。</p>
<p>而此书刊九州，</p>
<p>天下人皆曰此吴生乃吴贵妃后，赵世祖以补吴妃生未得后之位，故托梦成书，搏世间之名。</p>
<p>世谓《西游记》、《西降魔游记》二书之作者皆曰“吴承恩”。</p>
<p>——《聊斋志异》·蒲松龄</p>
<p>《三赵》译文:</p>
<p>明朝万历的时候，湖广有一书生袁宗道，官至员外郎。</p>
<p>袁宗道小时候爱慕诗词小说，经常和朋友说“天下文采莫过于三曹，三苏，三赵。我真想摆下一桌酒宴宴请这些古时候的才子。”</p>
<p>有一天夜里，袁宗道听到院子里有人喧哗，</p>
<p>便起床呵斥奴仆查看，</p>
<p>然而呼喊数声却无奴仆回应，便生气的披着衣服来到院中查看。</p>
<p>只见院中正在开宴会，酒菜齐全。</p>
<p>复又见六人分列左右。</p>
<p>左侧三人穿着汉朝服饰，为首长者身材矮小，双目狭长，然而却带有帝王威势。</p>
<p>右侧三人穿着宋朝士子服饰，为首长者形貌屹立，颇具文化之气。</p>
<p>袁生心生惊异，</p>
<p>上前行礼问候</p>
<p>“晚生袁宗道，不知几位前辈如何称呼？”</p>
<p>左侧长者扶须大笑着说：</p>
<p>“我是魏王曹孟德，今闻足下此处有宴席邀请，特来讨一杯酒喝。”</p>
<p>紧接又听到右侧一人破口大骂</p>
<p>“不想苏某今日遇到你这无耻汉贼！”</p>
<p>两方随后争吵不休。</p>
<p>袁宗道知道这是三曹三苏大驾光临，忙上前解围，说和两方，然后七人一起进入宴席。</p>
<p>宴会结束，袁宗道好奇询问</p>
<p>“既然六位前辈都来了，那三位赵官家又在何处呢？”。</p>
<p>苏轼摸着胡子笑道</p>
<p>“三赵还在码字，没有时间下凡来参加宴会。”</p>
<p>原来是那玉皇大帝苦于追更小说，三位赵官家在天上只能日夜写书没有休息的时间。</p>
<p>袁宗道感慨道</p>
<p>“果然写小说才是世间最辛苦的事啊！”，</p>
<p>一夜无言。</p>
<p>第二日，</p>
<p>袁宗道醒来后发现自己睡在院子里，四周杯盘狼藉，好似昨晚有人在这里开过宴会一般。</p>
<p>仆役都觉得很惊奇，袁宗道和他们说道</p>
<p>“是我敬仰的老前辈来此做客，昨夜太晚我就没叫你们起来。”</p>
<p>仆役感慨主人家的慷慨，随后便收拾起来。</p>
<p>后万历年中，</p>
<p>荆州有一位吴书生，</p>
<p>每到夜晚便会梦到一位红袍男子与他讲述《西游降魔杂记》，</p>
<p>吴书生受不了这种骚扰，改了其中的故事，</p>
<p>命名《西游记》。</p>
<p>书写完的那天，</p>
<p>晚上那红衣男子在梦里说</p>
<p>“这样一来你我二人就再不相欠了”。</p>
<p>吴书生惊醒，而后流泪不止，哭着说</p>
<p>“妾身负了官家啊”。</p>
<p>后此书刊售九州各处，</p>
<p>天下人都说这吴书生乃是吴贵妃转世，</p>
<p>赵世祖为了弥补吴贵妃生前未得皇后之位，故此托梦成书，来拯救吴氏的名望。</p>
<p>后世称《西游降魔杂记》《西游记》</p>
<p>这两本书的作者都叫“吴承恩”。</p>
<p>《耳中人·二》</p>
<p>曲端，字正甫，建炎十八王之一，</p>
<p>通文武，善诡辩。</p>
<p>端少时遇老道，习导引之术，</p>
<p>冬夏不止，可使耳目清明，</p>
<p>练至大成以耳化人，目能视，口能言。</p>
<p>端聪慧，行之未及一年而已至大成，</p>
<p>耳可化人，喋喋不止。</p>
<p>建炎五年时正旦，</p>
<p>张枢相为其子举满月酒，设宴。</p>
<p>宴中，端以不知东京事，愿听诸同僚议事。</p>
<p>然，诸人皆闻端处私语，似有人多言。</p>
<p>众人诧异，复问端，</p>
<p>“节度不言何屡扰宴？”。</p>
<p>端笑曰“非我言，为吾耳言矣”。</p>
<p>众人望去，见端耳中二小人出，</p>
<p>长三寸许，形与曲端俗倨，</p>
<p>环臂抱胸，狺狺吠，惹人厌。</p>
<p>众宴各散。</p>
<p>——《聊斋志异》蒲松龄</p>
<p>译文:《耳中人·二》</p>
<p>曲端，字正甫，</p>
<p>建炎十八王爵之一，文武双全，善于诡辩。</p>
<p>曲端小时候曾遇一老道士，</p>
<p>学了一门气功，冬夏不停，</p>
<p>可以让耳目清明，</p>
<p>练到大成可以使自己双耳化成人的样子，有眼睛能看，有嘴巴能说话。</p>
<p>曲端很聪明，修行了不到一年便已经达到大成，双耳可化人，喋喋不休。</p>
<p>建炎五年正旦时，张枢相为他的长子举办满月酒，宴请朝中文武大员赴宴。</p>
<p>席间，曲端以自己不知东京城中事为由不愿多口，愿听诸位同僚讨论国家大事。</p>
<p>然而，所有人却都听见曲端处喋喋不休，似乎有很多人说话。</p>
<p>众人诧异，又问曲端，</p>
<p>“节度不愿开口为何屡次扰乱宴会？”</p>
<p>曲端笑着回道</p>
<p>“不是我在说话，是我的耳朵在说话。”</p>
<p>众人看去，</p>
<p>却见曲端双耳中跳出两个小人，</p>
<p>身长三寸，形态与曲端一般倨傲，环臂抱胸，狺狺狂吠，惹人生厌。</p>
<p>众人宴会不欢而散。(本章完)</p>
